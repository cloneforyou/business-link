			<h1>Apply for your first approved driving instructor (ADI) registration certificate</h1>
			<p>You can use this service to apply for your first approved driving instructor (ADI) registration certificate.</p>
			<p>You can use it between 6.00 am and midnight, Monday to Sunday.</p>
			
			<h3>Payment</h3>
			<p>Payment will be taken from your account as soon as your application has been received. If your application is refused, the Driving Standards Agency (DSA) will give you a full refund.</p>
			
			<h3>If you have a photocard licence</h3>
			<p>You can tick a box to give the DSA permission to use your DVLA photo instead of sending one to DSA. You will need:</p>
			<ul>
				<li>your driver number</li>
				<li>your online services user ID</li>
				<li>your password</li>
				<li>a valid credit or debit card to pay the fee</li>
			</ul>

			<h3>If you do not have a photocard licence</h3>
			<p>If you do not have a photocard licence, or do not want the DSA to use the DVLA's photograph, you will have to send a recent passport sized photograph to DSA. You should write your PRN clearly on the back.</p>
			<p>You will need:</p>
			<ul>
				<li>your online services user ID</li>
				<li>your password</li>
				<li>a valid credit or debit card to pay the fee</li>
				<li>your personal reference number (PRN) - this is also known as your ADI number</li>
				<li>a printer</li>
			</ul>

			<p>When you have completed the screens, you will need to print a copy of the final page. You can do this using your browser print option. Send the final printed page and a passport sized photograph to DSA at the following address:</p>
			<p>Driving Standards Agency
				<br /> The Axis Building
				<br /> 112 Upper Parliament Street
				<br /> Nottingham
				<br /> NG1 6LP</p>
			<p>You can get help by phoning the DSA Helpline on Tel 0300 200 1122 or emailing them at <a href="mailto:adireg@dsa.gsi.gov.uk">adireg@dsa.gsi.gov.uk</a>.</p>
			<p>The DSA aims to process your application within ten working days.</p>
			
