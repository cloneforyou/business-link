		<h1>Complain against an undischarged bankrupt or disqualified company director</h1>
		<h2>How to check if a director has been disqualified or if an individual is an undischarged bankrupt or subject to bankruptcy restrictions</h2>
		<h3>Disqualified Directors Register</h3>
		<p><strong>Before</strong> reporting any breach of disqualification to The Insolvency Service, you should check the Disqualified Directors Register to ensure the director has been disqualified.</p>
		<p>The Disqualified Directors Register is kept by Companies House on behalf of the Secretary of State and lists all directors that are currently prohibited from involving themselves with a company. You can <a href="../../../do-it-online/search-company-information-using-companies-house-webcheck.php">find the Disqualified Directors Register on the Companies House website</a>.</p>
		<h3>Individual Insolvency Register</h3>
		<p><strong>Before</strong> reporting any breach of bankruptcy or bankruptcy restrictions to The Insolvency Service, you should check the Individual Insolvency Register to ensure the person is an undischarged bankrupt or is subject to bankruptcy restrictions.</p>
		<p><a href="../../../do-it-online/search-the-individual-insolvency-register.php">Search the Individual Insolvency Register with The Insolvency Service</a>.</p>
		<h3>Reporting disqualified directors, undischarged bankrupts and persons subject to bankruptcy restrictions</h3>
		<p>If you suspect that an individual is acting in breach of a disqualification order, a disqualification undertaking or a bankruptcy order, you should complete the Insolvency Service Hotline questionnaire and send it to The Insolvency Service. The information in the completed questionnaire will help The Insolvency Service make enquiries into your allegations.</p>
		<p>For guidance on what information you should provide and what The Insolvency Service will do after the complaint, see the page in this guide on <a href="complaining-about-disqualified-directors-undischarged-bankrupts-and-persons-subject-to-bankruptcy-restrictions.php">complaining about disqualified directors, undischarged bankrupts and persons subject to bankruptcy restrictions</a>.</p>
