		<h1>Value and market your business</h1>
		<h2>Common methods of valuing a business</h2>
		<p>There's a range of ways to value a business. Valuations based on multiples of future earnings and the capitalisation of future cashflows are the most common. There are a number of common valuation methods:</p>
		<ul>
			<li>Businesses with a record of sustainable profits are often valued at a <strong>multiple of earnings</strong>. Profits are adjusted for any unusual, one-off items to arrive at an estimate of 'normalised' earnings. Smaller businesses are usually valued at a lower multiple than similar, larger companies.</li>
			<li>Mature, cash-generating businesses can be valued in a similar way but based on cashflow. Future cashflows are estimated and discounted - this is known as <strong>discounted cashflow</strong>. Long-term cashflow is worth less than cashflow due shortly.</li>
			<li>An <strong>asset valuation</strong> might be appropriate for stable businesses with significant tangible assets - property or manufacturing businesses, for example. Your starting point is the value of assets stated in the accounts - known as the 'net book value'. These figures are then refined to reflect factors such as changes in the value of assets or bad debts.</li>
			<li>The cost of creating a business similar to yours can be used as a basis for valuation. Costs could include buying equipment, employing staff, developing products, attracting customers, and so on. It may be possible to estimate this '<strong>entry cost</strong>' as a benchmark of your business' value. Of course, if the cost of entry is low there's little likelihood of you achieving a successful sale.</li>
			<li>In some industries, there are established criteria for valuing businesses, eg by the number of branches an estate agency has.</li>
		</ul>

		<p>A potential buyer may use more than one method to get a range of values for your business. In the end, however, any price will be a matter for negotiation.</p>
		
