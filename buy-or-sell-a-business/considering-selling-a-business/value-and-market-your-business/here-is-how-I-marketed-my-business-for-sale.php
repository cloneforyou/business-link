		<h2>Here's how I marketed my business for sale</h2>
		<p>John Hunter set up print supply company Premier Colour Limited in 1995. Nine years later, having built the business to a point where it turned over &pound;800,000 a year and employed eight staff, he decided it was time to sell.</p>
		<p><strong>What I did</strong></p>
		
		<h3>Draw up a marketing document</h3>
		<p>"I met an agent through a local business club and he talked me through drawing up a marketing document. We did it in the back-to-front way of agreeing what we were trying to achieve and writing the summary first.</p>
		<p>"Only then did we draw up the contents, including headings like history and company ethos, business strategy, awards and achievements, customer profile and management organisation. Appendices formed a large part of the document, with financial reports and information for the previous three years.</p>
		<p>"We geared everything in it to the particular nature of Premier Colour. We stressed how valuable the business' database was and, importantly, included customer quotes. As a brokerage our customer list was vital to the sale and the customer comments added to our saleability."</p>
		
		<h3>Identify potential purchasers</h3>
		<p>"It was always likely we'd sell to someone bigger who could find synergies and savings by bolting two businesses together. With my industry knowledge I drew up two lists of suitors.</p>
		<p>"List number one was people in the industry that I knew - from reading the trade press or going to my trade association or because they supplied us - to be proactive players. They were all from a radius of about 50 miles as printing is pretty parochial.</p>
		<p>"List number two was drawn from going through Yellow Pages and trade magazines. These were print-related groups I knew less about."</p>
		
		<h3>Approach potential purchasers</h3>
		<p>"My agent wrote to those on the first list. He only identified my business by turnover though, describing the profitability without giving figures. To actually understand what made the company tick and what made it valuable they needed more detailed information.</p>
		<p>"It was enough to elicit interest though and five people signed confidentiality agreements to be sent the marketing document.</p>
		<p>"As it turned out, by coincidence, at the same time we were marketing to list number one a company actually approached us. Although that was who we ended up selling to, I remain convinced that going through the marketing process still made our sale competitive and increased the eventual price we got."</p>
		<p><strong>What I'd do differently</strong></p>
		<p>"As a brokerage, the value of my business was not from an asset register but from goodwill. That's difficult to put a figure on. At first, I went to four national sales agents and it was amazing the different figures I got. The difference between the top and bottom end was five times.</p>
		<p>"I wouldn't go to the large agents again. The highest valuer I ruled out straight away as their figure wasn't realistic. The lowest valuer just didn't understand our business. I think it's best where possible to stick to local agents who understand your industry sector."</p>
		
