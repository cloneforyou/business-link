		<h1>Value and market your business</h1>
		<h2>Research sources of potential buyers</h2>
		<p>Potential purchasers of your business are likely to come from a number of key sources:</p>
		<ul>
			<li>Your own knowledge of your sector should mean you know whether any of your competitors, customers or suppliers would be interested in buying your business.</li>
			<li>Trade magazines, business directories and the financial press may provide you with ideas.</li>
			<li>Your existing management team may be interested - but you'll need to be sure they can raise the necessary finance.</li>
			<li>Your corporate finance adviser or business broker should be able to help you identify possible buyers, both in the UK and abroad. They will have access to databases of prospective purchasers as well as an extensive network of contacts. For example, they may be able to advise you on equity investors or management buy-in teams who might be interested. They will also be able to help you assess whether buyers are capable of funding a purchase.</li>
			<li>For certain types of business it can be worth advertising for potential buyers. For example, to find buyers for a shop, hotel, restaurant or pub you can advertise in publications such as Daltons Weekly or use online business for sale listings. <a href="https://uk.businessesforsale.com/uk/sell-a-business" target="_blank" rel="noopener noreferrer">Find information on selling your business on the BusinessesForSale website</a>.</li>
		</ul>

		<p>Together with your adviser, you should draw up a shortlist of potential purchasers to approach. It can be a good idea to split this into two - a list of favoured prospective buyers you'll approach first and a back-up list if this doesn't produce results.</p>
		<p>It's important that you don't focus all your efforts on a single prospect. If the buyer knows they are the sole interested party, they can call all the shots.</p>
		<p>For information on the advisers you'll need when selling your business, see our guide on <a href="/buy-or-sell-a-business/considering-selling-a-business/preparing-to-sell-your-business.php">preparing to sell your business</a>.</p>
		
