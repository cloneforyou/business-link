		<h1>Value and market your business</h1>
		<h2>Key factors affecting the value of your business</h2>
		<p>There's a range of key factors that can affect the value of your business.</p>
		
		<h3>Finance</h3>
		<ul>
			<li>
				Historical, current and projected profits and cashflow.
			</li>
			<li>How well you control costs.</li>
			<li>Need for capital expenditure in the near future. </li>
		</ul>
		
		<h3>External factors</h3>
		<ul>
			<li>State of the economy in general including interest rate levels and the level of demand in your market in particular.</li>
			<li>How similar businesses are being valued.</li>
			<li>How many potential purchasers are interested in the business.</li>
			<li>How many similar businesses in your sector are on the market.</li>
		</ul>
		
		<h3>Intangibles</h3>
		<ul>
			<li>
				Goodwill and intellectual property such as patents.
			</li>
			<li>Strength of customer relationships - and how profitable they are.</li>
			<li>Your business' growth potential.</li>
			<li>Economies of scale a new owner could leverage.</li>
		</ul>
		
		<h3>Assets and liabilities</h3>
		<ul>
			<li>Value of assets such as property, equipment, debtors and stock-in-hand.</li>
			<li>How full your order book is.</li>
			<li>Level of debt and other existing liabilities.</li>
		</ul>
		
		<h3>People</h3>
		<ul>
			<li>
				The management's record of success.
			</li>
			<li>How dependent the business is on your own skills - and the likely extent of your future involvement.</li>
			<li>Experience and commitment of key staff.</li>
		</ul>

		<p>While some of these factors are outside your control and may affect the timing of your sale, you can take steps to make your business as valuable as possible. You need to start planning well in advance. Consider inserting an exit strategy into your original business plan. For more information, see our guide on <a href="/buy-or-sell-a-business/considering-selling-a-business/preparing-to-sell-your-business.php">preparing to sell your business</a>.</p>
		<p>Remember that any valuation you and your advisers come up with is likely to be subjective. Business owners often place too high a value on their business. In the end, the value of your business is only as much as a purchaser is prepared to offer.</p>
			
