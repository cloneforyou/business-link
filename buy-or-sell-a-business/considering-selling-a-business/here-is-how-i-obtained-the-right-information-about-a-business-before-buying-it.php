		<h2>Here's how I obtained the right information about a business before buying it</h2>
		<p>Headquartered in Glossop, Derbyshire, CEL Limited provides a diverse portfolio of management services to public sector organisations such as social housing providers, local and national government bodies and the student travel and education sector. The business underwent a management buyout last year, headed by current chief executive Paul Kennedy. Here Paul describes how he collected and analysed information about the business before making the decision to purchase.</p>
		
		<h3>What I did</h3>
		<h4>Initiate discussions</h4>
		<p>&quot;After many years spent working in large blue-chip organisations, I was ready for a change of direction and ultimately wanted to own my own business. I was approached by a head-hunter about CEL, whose owners were considering a sale.</p>
		<p>&quot;The owners had considered a trade sale and had also researched selling to venture capital companies, but had decided on a management buyout as the best way to preserve what they had built up and to safeguard employees. They were therefore looking for a chief executive who would subsequently buy out the business.</p>
		<p>&quot;After extensive discussions with the vendors, I was appointed chief executive with a firm commitment to undertake a management buyout in the near future.&quot;</p>
		<h4>Appoint advisors</h4>
		<p>&quot;I was in a privileged position as chief executive to find out more about the company from the inside during the few months preceding the actual sale, but I still needed to gather detailed information on a more formal basis.</p>
		<p>&quot;I appointed a financial director who also became my partner in the buyout. I also researched and appointed experienced professional advisors to handle the purchase, including a legal firm who I had worked with previously and an accountancy firm.</p>
		<p>&quot;In addition, I did my own research into the business and sought general advice from banks, private equity firms and trusted business associates.</p>
		<p>&quot;It's not uncommon for professional advisors on opposite sides of a deal to become adversarial to the point of hampering a successful outcome. One thing we found useful was to arrange an initial meeting with ourselves, the vendors and the appointed advisors for both parties. This helped to ensure that the legal and financial teams on both sides were working towards a common goal.&quot;</p>
		<h4>Assess the information</h4>
		<p>&quot;The more information you have, the better prepared you are, particularly when it comes to agreeing on a price. Things we took into account included establishing legal ownership of key assets, checking software licences, checking employment contracts and examining the company's balance sheet and customer contracts.</p>
		<p>&quot;Remember that whoever is funding your purchase, such as a bank or private investor, will take a keen interest in any information you obtain. They need to be confident that you've minimised financial risk and got the legal issues covered. For example, in our case, there was an issue with the company's final salary pension scheme. We were concerned about the financial risks associated with the scheme going forward, so we employed specialist lawyers and an actuary who clearly explained these risks and the options open to us when it came to managing them.</p>
		<p>&quot;Overall, our appointed advisors gave us invaluable professional opinions on the documentation received and were also able to do a lot of the legwork, such as reading through individual employment contracts. But ultimately it was up to me and my business partner to assess the information and make a judgement before making a formal offer to buy the business.&quot;</p>
		<h3>What I'd do differently</h3>
		<h4>Get the vendors to agree a fee cap</h4>
		<p>&quot;As buyers, we agreed a fee cap with our own professional advisers upfront so that costs could be controlled. In retrospect, I wish we'd encouraged the vendors to do the same with theirs. The process was held up at various points simply because the vendors' advisers had no financial incentive to get a move on - in fact, quite the opposite!&quot;</p>

