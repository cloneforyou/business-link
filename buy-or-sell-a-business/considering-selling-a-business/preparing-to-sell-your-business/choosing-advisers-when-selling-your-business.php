		<h1>Preparing to sell your business</h1>
		<h2>Choosing advisers when selling your business</h2>
		<p>Experienced advisers are essential for an effective sale. The right adviser can have a big impact on the success of your sale and the amount you receive.</p>
		<h3>Accountants, solicitors and tax advisers</h3>
		<p>You will need an <b>accountant</b> and a <b>solicitor</b>. The accountant concentrates on the financial aspects of the sale - like preparing accounts for the business. The solicitor focuses on legal issues - like drafting a sale agreement.</p>
		<p>You also need to use a specialist <strong>tax adviser</strong> to handle business and personal tax planning. Your accountant may be a tax specialist, or may be able to introduce you to one.</p>
		<p>For more information, see our guides on how to <a href="../../../finance-and-grants/financial-planning-and-accounts/choose-and-work-with-an-accountant.php">choose and work with an accountant</a> and <a href="../../../finance-and-grants/expert-financial-advice/choose-and-work-with-a-solicitor.php">choose and work with a solicitor</a>.</p>
		<h3>Brokers and corporate finance specialists</h3>
		<p>Most businesses choose to use a specialist <strong>business broker</strong> or <strong>corporate finance adviser</strong>. These are involved at an early stage to take care of:</p>
		<ul>
			<li>producing credible sales documents</li>
			<li>researching, finding and vetting potential buyers</li>
			<li>pre-sale grooming</li>
			<li>negotiating a sale on your behalf</li>
		</ul>
		<p>These can all be very time-consuming - so the adviser can manage the whole process, leaving you free to continue running the business.</p>
		<p>To find a suitable corporate finance adviser, do research, look for recommendations and check that a broker has the necessary experience and proven track record. You can start by asking your accountant or solicitor if they can recommend someone who specialises in your sector. Maybe a business acquaintance, friend or colleague has had a good experience selling their own business.</p>
		<h3>Making your choice and agreeing fees</h3>
		<p>Always examine advisers' skills and expertise carefully. For example, you should look at:</p>
		<ul>
			<li>what experience they have of selling similar businesses and how successful they have been</li>
			<li>how they can help you to market the business</li>
			<li>what contacts they have among potential buyers</li>
			<li>what references they can provide</li>
			<li>what the fees involved are and what they cover</li>
			<li>how they will keep a sale confidential</li>
		</ul>
		<p>If you're using a firm of advisers, make sure you feel comfortable with the people you'll be dealing with. Make sure the firm you choose is suitable for your business. A specialist in selling fish and chip shops or pubs is unlikely to help sell a recruitment agency or precision engineers.</p>
		<p>You will have to pay your advisers. Many advisers charge an hourly rate or up-front fees. Alternatively, you may be able to negotiate a fixed rate for a particular piece of work. Some advisers, particularly corporate finance specialists and business brokers, are prepared to negotiate a success fee as part of their payment. For example, you might pay lower fees if you don't get your target price.</p>
