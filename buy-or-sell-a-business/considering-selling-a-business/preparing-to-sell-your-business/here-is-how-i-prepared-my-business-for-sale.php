		<h1>Here's how I prepared my business for sale</h1>
		<h2>Here's how I prepared my business for sale</h2>
		<p>When Julian Harley and Ian West bought their IT training business in 1991 for &pound;55,000, they had high hopes for expansion. After six years, their vision and hard work had built the company from seven employees to over 200. When Harley West Training was sold in 1997 it was valued at &pound;8.5 million. Julian Harley describes the long and often winding road that led to the sale.</p>
		<p><strong>What I did</strong></p>
		<h3>Know the market</h3>
		<p>"Our knowledge of the IT training marketplace meant that we had a clear idea of which companies would want to buy us from the outset. If you know the market well there's less need for outside advice. Knowing the market also means being aware if it's going up or down. It's obviously harder to sell when the market's dropping, but we didn't want to wait until it had reached its peak either. Our buyer wanted to know there was still some market growth left."</p>
		<h3>Get things running smoothly</h3>
		<p>"Before you can even think about selling a business you need to make sure it is running smoothly. We undertook a careful review of our operation and looked at everything from leases to company cars, pension issues to legal disputes - anything that might put potential purchasers off. We dealt with all items that needed attention. The business was eventually sold in 1997, but the process started back in early 1996. You need to allow plenty of time, not just to find the right buyer, but to consider all the implications of sale."</p>
		<h3>Treat employees with integrity</h3>
		<p>"We didn't want to create uncertainty among employees when the deal was still in the early stages, but when we did announce our intention to sell we made sure it was done respectfully. We told senior managers before making a general announcement and they played a key role in communicating a positive message to the rest of the workforce. We also wanted to recognise the contribution of employees in making the business successful so, when the deal was signed, every employee received a share of the proceeds."</p>
		<p><strong>What I'd do differently</strong></p>
		<h3>Manage your advisers</h3>
		<p>"We used professional advisers to handle the tax and legal aspects of the deal, but advice on the sale itself was limited to one individual who specialised in acquisitions and disposals. The real advantage of involving a third party during the negotiation process was that it acted as a buffer between the purchaser and us. It also meant we could retain good relations with the purchaser, which was important because we agreed to stay on afterwards as executive directors to help grow the business. This worked well since we contracted to stay for a year but remained for over three.</p>
		<p>"Although business owners do the deal, not lawyers, it's amazing how much time lawyers and accountants can spend on immaterial matters if they're allowed to. No matter how small or large the business there's a general tendency for meetings to drag on. Looking back, I wish we'd been firmer and insisted on calling it a day at a reasonable hour. At 3 o'clock in the morning, no one's in the best state of mind to make sensible decisions."</p>
		<h3>Plan what to do with the money</h3>
		<p>"I was so involved in the sale, not to mention running the business day-to-day, that I didn't really give much thought to what I would do with the money if it materialised. A bit more forward planning would have been sensible because it would have allowed me to seek more appropriate tax and investment advice."</p>
