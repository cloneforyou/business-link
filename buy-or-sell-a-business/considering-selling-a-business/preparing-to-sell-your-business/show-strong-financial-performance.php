		<h1>Preparing to sell your business</h1>
		<h2>Show strong financial performance</h2>
		<p>Planning well ahead helps you ensure that your business has a financial record that attracts buyers. A first step is to ensure that your finances are in good order. Although this should be the case at any time, planning to sell your business can push you to focus on this area.</p>
		<p>One major area is <strong>control of working capital</strong>, through reducing stock levels and controlling creditors. There may also be opportunities to <strong>cut costs</strong>, such as renegotiating supply contracts and eliminating unnecessary perks. You can also sell underused equipment to <strong>reduce debt</strong>.</p>
		<p>You will want to present your accounts as attractively as possible. Buyers usually prefer businesses that show increasing profits year on year. If possible, your financial performance should be reasonably stable throughout the year. You may be able to bring forward or delay purchases and sales to help with this. You may also want to change some of your accounting policies.</p>
		<p>Good <b>sales forecasts</b> will help to increase prospective buyers' confidence in your business - but you must ensure they're realistic and can be supported with evidence. A full order book is a good sign.</p>
		<p>It's important that buyers believe your accounts. For example, you should make realistic provisions for bad debts. Buyers and their advisers will usually see through any quick fixes you try to use to boost profits.</p>
		<p>To maximise short-term profits you can reduce longer-term investment. For example, you might avoid expenses like advertising heavily or taking on new staff. But <strong>avoid excessive cost-cutting</strong> - you need to maintain spending in essential areas, otherwise the business suffers and so does the price buyers will be prepared to offer.</p>
		<p>For advice on these and other options, consult your accountant and your corporate finance adviser.</p>
