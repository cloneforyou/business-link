		<h1>Floating on the stock market</h1>
		<h2>Is your business suitable to be a public company?</h2>
		<p>Before you even consider floating your business, you need to determine whether it is suitable and ready for life on a public market.</p>
		<p>Investors will only be interested in buying shares in companies that have <strong>secure earning streams</strong> and <b>strong growth prospects</b>. They will look for a good rate of return on any investment but will require a higher rate of return from an unproven, smaller business than from a large established company to compensate for the greater risks involved.</p>
		<p>It is harder to guarantee a successful investment in companies new to the market. Smaller companies are more likely to suffer should market or financial conditions change - making investment in them risky.</p>
		<p>You will need to consider whether your business can deliver that rate of return. You should ask yourself whether:</p>
		<ul>
			<li>your business has a strong record of delivering profits and growth</li>
			<li>your sector is attractive to investors</li>
			<li>your business plan sets out how you will deliver strong growth in earnings in the future</li>
			<li>your management team is up to the task of delivering the performance required</li>
		</ul>
		<p>If you feel your business is not at the right stage of development or will not be able to meet investor expectations of growth, you may want to consider other financing options.</p>
		<p><a href="http://www.londonstockexchange.com/companies-and-advisors/listing/float/float.htm" target="_blank" rel="noopener noreferrer">Find out whether your business is suited to flotation on the London Stock Exchange website</a>.</p>

