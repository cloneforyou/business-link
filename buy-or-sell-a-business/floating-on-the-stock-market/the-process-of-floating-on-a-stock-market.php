		<h1>Floating on the stock market</h1>
		<h2>The process of floating on a stock market</h2>
		<p>When seeking to join a stock market, your business will be subject to legal and financial due diligence to help attract investor interest and to fulfil the entry requirements and documentation of the relevant market.</p>
		<p><a href="../../_files/london-stock-exchange_guide-to-capital-markets.pdf" target="_blank" rel="noopener noreferrer">Download information on the AIM and Main Market admission criteria from the London Stock Exchange website (PDF, 1.68MB)</a>.</p>
		<p>Your business also needs to have the right legal structure. The legal structure of a sole trader or a partnership is not suitable for a public market listing and as such a change to the company's legal structure would be necessary.</p>
		<p>Public companies have different obligations to private companies under the Companies Act 2006. For example, private companies are no longer required to hold an annual general meeting (AGM) though they may opt to do so or be required to hold one if sufficient shareholders demand one. However, all public companies and private companies with traded shares must hold an AGM. See our guide on <a href="/tax-payroll-and-company-information/company-administration-an-introduction/company-administration-the-basics.php">company administration: the basics</a>.</p>
		<p>You will also have to make the following key information available:</p>
		<ul>
			<li>who the directors are and what service contracts they have with the company</li>
			<li>who the major shareholders are and details of the new and existing shares being offered for sale</li>
			<li>information on the company's key contracts</li>
			<li>the memorandum and articles of association</li>
		</ul>
		<p>Typically, the admission process may take between three and six months to complete, but it could take as long as a year to prepare the business and conduct the due diligence. To help with this process, you will need to appoint a set of advisers with relevant experience in helping businesses like yours seek capital via the public markets.</p>
		<p><a href="http://www.londonstockexchange.com/companies-and-advisors/main-market/advisors/advisors.htm" target="_blank" rel="noopener noreferrer">Find information on how advisers can help you float your company on the stock exchange on the London Stock Exchange website</a>.</p>

