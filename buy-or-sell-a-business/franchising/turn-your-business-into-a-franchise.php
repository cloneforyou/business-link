		<p>If you have a successful business, franchising can be a very effective way of growing. You use your existing business as a blueprint for expansion. But instead of setting up and running new outlets yourself, you find independent franchisees that you help to set up their own businesses using your blueprint.</p>
		<p>The franchisees finance and manage their businesses themselves but pay you fees. Fees are for the right to use your business model, brand and ongoing advice and support throughout the length of the agreement. This may allow your business to grow faster and give a better return on capital than if you own all the outlets yourself  - however, the profit per unit will be significantly less. Some of the best known companies in the country have become household names by franchising their businesses.</p>
		<p>This guide outlines the different kinds of franchising, and will help you identify whether it might suit your business. It also explains what you need to do to build a successful franchise operation.</p>
		
