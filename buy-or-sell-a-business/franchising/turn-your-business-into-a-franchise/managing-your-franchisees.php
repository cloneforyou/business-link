		<h1>Turn your business into a franchise</h1>
		<h2>Managing your franchisees</h2>
		<p>Franchising your business isn't about selling franchises and then forgetting about them. You have a continuing relationship with your franchisees.</p>
		<p>As part of this, you must provide the support detailed in the franchise agreement. This can include:</p>
		<ul>
			<li>
				helping them with initial set up of their franchise
			</li>
			<li>
				providing training in how to run the business
			</li>
			<li>
				running national promotional campaigns to increase sales
			</li>
			<li>
				helping them manage their business effectively
			</li>
			<li>
				innovating to keep your product or service ahead of the competition
			</li>
			<li>
				monitoring their performance and processes against the standards contained in the operations manual
			</li>
		</ul>

		<p>Doing all this isn't just a matter of fulfilling your contractual obligations. By supporting franchisees, you help them succeed - and increase the fees you receive.</p>
		<p>Although it isn't included in the agreement, you also have a role to play in <strong>motivating franchisees</strong>. New franchisees can find the early months difficult and may become discouraged. Successful franchisees may reach their own comfort level and stop trying so hard to increase sales. Again, by motivating them you help yourself.</p>
		<p>At the same time, you need to ensure that they are running their businesses the way they should. One of the keys to building a successful brand is <strong>consistency</strong>. If different franchisees run their businesses differently, your brand can suffer. Your operations manual should give clear information on the correct procedures. For more information, see the page in this guide on how to <a href="/buy-or-sell-a-business/franchising/turn-your-business-into-a-franchise/develop-your-business-format.php">develop your business format</a>.</p>
		<p>Last but not least, you need efficient administration to ensure that you collect the right franchise fees.</p>
		
