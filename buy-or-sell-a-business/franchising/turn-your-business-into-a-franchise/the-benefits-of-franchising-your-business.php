		<h1>Turn your business into a franchise</h1>
		<h2>The benefits of franchising your business</h2>
		<p>The benefits of franchising include using the franchisee's capital to develop a brand at a local rather than national level. The customer then receives a better and more localised service.</p>
		<p>Growing your business can be difficult and expensive. The more you grow, the more capital you need, for example, to finance new outlets. At the same time, managing the business becomes more difficult, particularly if your business is spread across the country. You will need to be prepared to travel around the country when giving support.</p>
		
		<h3>Finance</h3>
		<p>Each 'franchisee' finances their own outlet. While the franchisee meets all the costs and collects the income, you receive franchise fees or a mark-up on products sold by your franchisee. For more information, see the page in this guide on <a href="/buy-or-sell-a-business/franchising/turn-your-business-into-a-franchise/franchise-fees-and-royalties.php">franchise fees and royalties</a>.</p>
		
		<h3>Management demands</h3>
		<p>The franchisees also run their businesses, reducing the management demands placed on you. The best franchisees will be highly motivated and have local expertise, making your life much easier.</p>
		<p>Rather than managing their business, your role involves supporting your franchisees. This is likely to include:</p>
		<ul>
			<li>helping the franchisee find and fit out premises</li>
			<li>training</li>
			<li>creating operating systems to help franchisees set up and run their franchise</li>
			<li>providing marketing materials and resources such as a branded website</li>
			<li>running advertising campaigns to build brand awareness</li>
			<li>protecting and defending your intellectual property</li>
		</ul>

		<p>In the early stages you are likely to spend a lot of time growing the franchise by recruiting other franchisees - perhaps through attending franchise exhibitions and running marketing and advertising campaigns.</p>
		<p>As your business grows, there can be additional benefits. The more franchisees you have, the better known your <strong>brand</strong> becomes. Your <strong>purchasing power</strong> may also increase as you buy more, allowing you to negotiate discounts.</p>
		
		<h3>Potential drawbacks</h3>
		<p>Franchising can have its drawbacks. You need to invest in developing and marketing the franchise. You also need to make sure that you get the right franchisees and control what they do. A bad franchisee can damage your reputation and brand, hurting all your franchisees.</p>
		<p>Despite this, franchising could still be the best way to grow your business. For more information, see the page in this guide: <a href="/buy-or-sell-a-business/franchising/turn-your-business-into-a-franchise/is-franchising-right-for-your-business.php">is franchising right for your business?</a></p>
		
