		<h1>Turn your business into a franchise</h1>
		<h2>Marketing your franchise opportunity</h2>
		<p>You need to be realistic about how quickly you can grow. Although new franchisees usually provide the capital for their operations, you will be involved helping them set up. For example, you will have to run initial training, or provide hands-on support when they first start trading.</p>
		<p>This means that you may only be able to cope with one or two new franchisees at a time. You may also prefer new franchisees to be relatively nearby, making it easier to visit them. Many new franchising companies aim for a gradual roll out across the country in this way.</p>
		<p>Once you have decided what to offer franchises, you need to prepare a prospectus. This tells potential franchisees what you are offering and what they can expect. It should include:</p>
		<ul>
			<li>
				an explanation of your product or service
			</li>
			<li>
				what franchise territories you are offering
			</li>
			<li>
				what the franchise fees are
			</li>
			<li>
				what financial returns the franchisee might expect
			</li>
			<li>
				information about your experience, franchise package, training, timescales, next steps and application form
			</li>
		</ul>

		<p>Bear in mind that you are generally competing with other businesses to attract potential franchisees. At the same time, think carefully about what kinds of people you want to apply. They should have the finances needed to invest in the business, and the right management skills and attitude.</p>
		
		<h3>Advertising and promotion</h3>
		<p>To attract franchisees, you may want to advertise in the business opportunities section of a newspaper. Franchising exhibitions can also be a good way of recruiting potential franchisees. <a href="https://www.thebfa.org/events/" target="_blank" rel="noopener noreferrer">Find information about major franchising exhibitions on the British Franchise Association (BFA) website</a>. There are also several websites where you can advertise.</p>
		<p><a href="https://www.daltonsbusiness.com/selling-a-business" target="_blank" rel="noopener noreferrer">Read about advertising your business on the Daltons Business website</a>.</p>
		<p><a href="https://uk.businessesforsale.com/uk/sell-a-business" target="_blank" rel="noopener noreferrer">Find information on selling your business on the BusinessesForSale website</a>.</p>
		<p>Joining the BFA can increase awareness of your franchise. <a href="https://www.thebfa.org/about-franchising-the-bfa/" target="_blank" rel="noopener noreferrer">Find out about the benefits of joining the BFA on the BFA website</a>.</p>
		
