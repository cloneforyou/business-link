		<h1>Turn your business into a franchise</h1>
		<h2>Develop your business format</h2>
		<p>To franchise your business, you need to convince potential franchisees that they will make money. Franchisees will want to see evidence that the business model is sound, that you deliver what you have committed to in the franchise agreement and that they can make a good living running a franchise. You do this by having a successful business, and an operations manual that shows how franchisees can replicate it.</p>
		<p>Market testing is an important part of this. If you cannot prove that there is a demand for the product or service your franchisees will be offering, they will be doomed to failure.</p>
		<p>If you can demonstrate a clear demand for your product and service, you then need to prove that the franchise model works through the establishment of a pilot operation. The pilot operation will establish that all the back-up systems including training, operating manuals, financial support and marketing campaigns are effective. It will also give franchisees an indication of likely set-up costs, break-even points and how long it will take to become profitable.</p>
		<p>The <strong>operations manual</strong> gives detailed information on how to set up and manage a new outlet. It highlights all the key activities, and explains how to do them the right way.</p>
		<p>As well as preparing an operations manual, you also need to decide what <strong>support</strong> you will provide. This typically includes initial training for new franchisees, and continuing marketing and administration support.</p>
		<p>The British Franchise Association (BFA) offers support for companies planning to franchise their business. Your bank may also have specialist franchise advisers who can help.</p>
		
		<h3>Protecting your brand</h3>
		<p>Your brand is likely to be an important part of what you offer franchisees. Even if they know how to run a successful outlet, they stay with you because your brand helps them get customers. Protecting your brand is essential. For more information on brand management, see our guide on <a href="/sales-and-marketing/advertising-branding-and-design/branding-the-basics.php">branding: the basics</a>.</p>
		<p>It is important that you put in place relevant protections to prevent your intellectual property (IP) being infringed (for example by registering your trade marks and company name or obtaining patents for your products). Once you have adequate protections in place you can then benefit from your IP through licensing. It is also easier to protect your IP if it is registered and you can prove ownership. See our guides on how to <a href="/create-innovate-and-protect/protecting-your-intellectual-property/trade-marks-protecting-your-brand/use-trade-marks-in-your-business.php">use trade marks in your business</a> and <a href="/create-innovate-and-protect/protecting-your-intellectual-property/find-value-you-didnt-know-existed-in-your-business/protecting-intellectual-property.php">protecting intellectual property</a>.</p>
		<p>Your operations manual and support services are an important part of this. They help to ensure that different franchisees do things the right way and provide consistent quality.</p>
		
