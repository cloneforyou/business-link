		<h2>Hospitality and Catering tips</h2>
		<p>Here are some case studies showing how your business can save money and increase efficiency</p>
		
		<h3>1. Reduce, reuse, and recycle where possible</h3>
		<img height="140" alt="Reduce, reuse, and recycle where possible" src="../../_img/re_hospitality_1.jpg" width="250">
		<p>Independent family run hotel Strattons in Norfolk considered a wide range of ways in which they could achieve cost savings and environmental benefits. Their commitment to eliminate, reduce, re-use and recycle as much waste as possible led to a total cost savings of over &pound;10,000 a year.</p>
		<ul>
			<li>The hotel refillable toiletry dispensers saved nearly &pound;2,000 a year in purchase costs and water saving initiatives reduces dependency on mains water</li>
			<li>Good housekeeping and laundry policy saves &pound;4,050 a year, and over 98% of all waste is re-used or recycled</li>
		</ul>
		<p><a href="../../_files/Envirowise_Strattons_case_study.pdf" target="_blank" rel="noopener noreferrer">Read the full case study (PDF, 2694KB)</a></p>
