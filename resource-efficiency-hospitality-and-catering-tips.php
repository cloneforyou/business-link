		<h3>2. Cut energy costs</h3>
		<img height="140" alt="Cut energy costs" src="../../_img/re_hospitality_2.jpg" width="250">
		<p>Surrey's Rhodes Foods has operated a chain of "Superfish" restaurants and takeaways for the last 30 years. Their out-of-date fryers were contributing to high energy costs, and so they applied for an interest-free Carbon trust loan of &pound;21,000 to invest in seven new models. As a result, these will save the company around &pound;4,000 a year, so the loan should be paid back within five years, after which savings go straight to the company.</p>
		<p><a href="#">Read the full case study</a> </p>
		
		<h3>3. Look into recycling food waste with local businesses</h3>
		<img height="140" alt="Look into recycling food waste" src="../../_img/re_hospitality_3.jpg" width="250">
		<p>The Best Western Premier Mount Pleasant Hotel in Doncaster used to send its catering waste to pig farms as feed, but since the ban on this practice in 2003, the hotel was left with little option but to send their food waste to landfill. Through working with local businesses the hotel trialled an alternative way of getting rid of food waste using Grott boxes.</p>
