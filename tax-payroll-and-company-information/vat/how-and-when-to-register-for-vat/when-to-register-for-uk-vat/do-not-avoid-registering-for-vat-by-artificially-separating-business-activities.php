		<h1>When to register for UK VAT </h1>
		<h2>Do not avoid registering for VAT by artificially separating business activities</h2>
		<p>If you run more than one business, the sales in all those businesses must normally be added together to determine whether or not you must register for VAT.</p>
		<p>However, if you are involved in the running of several separate legal entities, you may not need to combine the sales of those businesses to find whether you need to be VAT registered.</p>
		<p>If HM Revenue &amp; Customs (HMRC) decides that you have artificially separated one business into smaller parts to avoid registering for VAT, it can decide that the entire business is a single taxable person and therefore must be registered for VAT. For more information, see the page in this guide on <a href="/tax-payroll-and-company-information/vat/how-and-when-to-register-for-vat/when-to-register-for-uk-vat/who-can-and-cannot-register-for-vat.php">who can and cannot register for VAT</a>.</p>
		<p>Situations that HMRC may consider a single taxable person for VAT purposes include:</p>
		<ul>
			<li><strong>Separate entities selling to registered and unregistered customers</strong>. The VAT-registered entity sells only to VAT-registered customers and the entity not registered for VAT sells to customers who are not registered for VAT.</li>
			<li><strong>The same equipment or premises being used by different entities on a regular basis.</strong> The premises and/or equipment are owned by one of the parties, who charges rent to the others. This situation may occur in businesses such as launderettes and takeaway food operations.</li>
			<li><strong>Splitting up what is usually a single sale.</strong> This is common in industries such as the bed and breakfast trade, where one business supplies the bed and another the breakfast.</li>
		</ul>

		<p>If you deliberately avoid registering for VAT, you may be liable for a penalty. For serious offences, the matter will be investigated and you may be prosecuted. You can read about how to register for VAT, what happens if you do not register for VAT on time and more about artificial separation of business activities in the guides below.</p>
