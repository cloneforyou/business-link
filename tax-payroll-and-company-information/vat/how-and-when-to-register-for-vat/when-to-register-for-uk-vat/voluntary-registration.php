		<h1>When to register for UK VAT </h1>
		<h2>Voluntary registration</h2>
		
		<h3>Benefits of voluntary registration</h3>
		<p>There are potential cashflow advantages of being able to charge VAT on your sales and claim back VAT on your purchases, which you may benefit from depending on your circumstances. For example:</p>
		<ul>
			<li>if you sell zero-rated items and buy standard-rated items you would receive a VAT refund from HM Revenue &amp; Customs (HMRC)</li>
			<li>if you have not yet sold anything or don't sell anything during a VAT accounting period, you may still be able to claim VAT back on your purchases</li>
		</ul>

		<p>If you're thinking about registering voluntarily, you might want to check the rules for reclaiming VAT on purchases made before registration, since it is often possible to reclaim some of the VAT you are charged on goods or services that you use to set up your business.</p>
		<p>You can apply to backdate your voluntary VAT registration by up to four years.</p>
		<p>You will have to account for VAT on any VAT taxable supplies you have made after your chosen date, and you won't be able to reclaim any VAT on your purchases unless you have the right evidence, and meet the other conditions for reclaiming VAT.</p>
		<p><a href="/tax-payroll-and-company-information/vat/how-and-when-to-register-for-vat/purchases-made-before-vat-registration-reclaiming-the-vat.php">Purchases made before VAT registration: reclaiming the VAT</a></p>
		
		<h3>Responsibilities of voluntary registration</h3>
		<p>If you decide to voluntarily register for VAT, you have exactly the same responsibilities as someone who must register. You must keep all required VAT records and issue VAT invoices. You also have to complete and submit a VAT Return at regular intervals, along with your payment if one is due.</p>
