		<h1>When to register for UK VAT </h1>
		<h2>You supply goods or services from the UK to other countries</h2>
		<p>If you supply goods and services both inside and outside the UK, then you may need to register for UK VAT if the value of your UK supplies alone exceeds the registration threshold.</p>
		<p>You don't need to include supplies you make in other countries when calculating your VAT taxable turnover for registration purposes - so leave out of your calculation any goods and services you supply where the place of supply is another country rather than the UK.</p>
		<p><a href="/tax-payroll-and-company-information/vat/international-trade-international-visits-and-vat/exports-supplies-of-goods-and-services-overseas-charging-vat/how-to-work-out-your-place-of-supply-of-services-for-vat.php">How to work out your place of supply of services for VAT</a></p>
		
		<h3>Distance selling</h3>
		<p>If you supply and deliver goods, or have them delivered - for example, by mail or another delivery service - to customers in other European Union (EU) countries who are not registered for VAT and don't have to be registered, this is known as distance selling. If you are required to register and account for VAT on distance sales in another EU country then you don't include the value of those distance sales when working out whether you need to register for UK VAT. You can find out whether you need to register for VAT in other EU countries in our guide below.</p>
