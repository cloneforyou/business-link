		<h1>When to register for UK VAT </h1>
		<h2>Who can and cannot register for VAT</h2>
		
		<h3>Who can register for VAT</h3>
		<p>You can register for VAT if you're in <strong>business</strong> and you are one of these:</p>
		<ul>
			<li>an individual</li>
			<li>a partnership</li>
			<li>a company</li>
			<li>a club</li>
			<li>an association</li>
			<li>a charity</li>
			<li>any other organisation or group of people acting together under a particular name, such as an educational or health institution, exhibition, conference, etc</li>
		</ul>

		<p>For VAT purposes, the individual or organisation that is in business is known as a 'taxable person'.</p>
		
		<h3>Who cannot register for VAT</h3>
		<p>You cannot register for VAT if either of these is true:</p>
		<ul>
			<li>you sell only goods or services that are exempt from VAT</li>
			<li>you are not in business according to the definition that HM Revenue &amp; Customs (HMRC) uses for VAT purposes</li>
		</ul>

		<p>You can find out more about exempt goods and services in our guide on <a href="/tax-payroll-and-company-information/vat/reclaiming-vat-on-your-purchases/exempt-and-partially-exempt-businesses-for-vat-purposes.php">exempt and partially-exempt businesses for VAT purposes</a>.</p>
		
		<h3>What is business for VAT purposes?</h3>
		<p>You can only register for VAT if you're in business. HMRC defines a business as a continuing activity involving getting paid for providing goods or services - in money or another form of payment such as in-kind or barter.</p>
		<p>You are in business when, for example:</p>
		<ul>
			<li>you earn an income by carrying on a trade, vocation or profession - by being self-employed or through another entity such as a limited company</li>
			<li>you provide membership benefits as a club, association or similar body in return for a subscription or other form of payment</li>
			<li>you provide certain other activities as a club or other recreational body, charity or other non-profit making body</li>
			<li>you charge admission to a premises</li>
		</ul>
		
		<p>To be in business, these activities must have a degree of frequency and scale and be continued over a period of time.</p>
		<p>Even if your activities have some or all the characteristics of a business, they may not be considered a business for VAT purposes if they are essentially a recreation or hobby, or an isolated transaction. So if you only make occasional VAT taxable supplies, or your supplies are minimal, it may be that you don't need to register for VAT. The one-off or infrequent sale of your personal belongings at a car boot sale or auction, for example, would fall into this category - but buying goods for resale on a regular basis is definitely a business activity.</p>
