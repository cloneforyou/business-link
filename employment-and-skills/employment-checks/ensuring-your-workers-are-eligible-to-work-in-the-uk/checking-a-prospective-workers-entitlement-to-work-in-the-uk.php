		<h1>Ensuring your workers are eligible to work in the UK</h1>
		<h2>Checking a prospective worker's entitlement to work in the UK</h2>
		<p>As an employer, you need to ensure that each prospective worker is eligible to work in the UK before their employment commences. By doing this, you will establish a 'statutory excuse' against being liable to pay a fine (known as a 'civil penalty') for employing an illegal worker.</p>
		<p>You should make the necessary checks even if you think a potential worker has the right to work in the UK. For more information, see our guide on <a href="/employment-and-skills/different-types-of-worker/employing-migrants-an-overview.php">employing migrants - an overview</a>.</p>
		
		<h3>Who is an employer?</h3>
		<p>An employer is defined as a person who employs an individual under a contract of employment.</p>
		<p>This can be a contract of service or an apprenticeship. The contract can be expressed either orally or in writing. See our guide on <a href="/employment-and-skills/staff-documents-and-employment-policies/the-employment-contract.php">the employment contract</a>.</p>
		
		<h3>Checking the right to work in the UK</h3>
		<p>The UK Border Agency (UKBA) publishes two lists - List A and List B - of documents which can demonstrate someone's right to work in the UK.</p>
		<p>For you to have a statutory excuse against liability for a civil penalty, your prospective worker must provide you with an original version of one of the specified documents from List A, or a specified combination of two documents from List B.</p>
		<p><a href="../../../_files/home-office_comprehensive-guidance-for-employers-on-preventing-illegal-working_february-2008.pdf" title="Guidance on List A and List B documents" target="_blank" rel="noopener noreferrer">Download guidance on checking List A and List B documents from the UKBA website (PDF, 2.76MB)</a>.</p>
		<p>You should ask them to provide the documents before they start work, eg you could ask:</p>
		
		<ul>
			<li>shortlisted applicants to bring the appropriate document(s) when they come for an interview</li>
			<li>the successful applicant to provide the appropriate document(s) before you make them an unconditional job offer</li>
		</ul>
		
		<p>Once the worker has presented you with the necessary document(s), you must:</p>
		<ul>
			<li>satisfy yourself that the document(s) are valid and genuine, and have not been tampered with</li>
			<li>check that any photographs and birth dates on the documents match the applicant's appearance</li>
			<li>check information in the job application against the documents to ensure the details match up</li>
			<li>check that the document(s) allow the worker to do the work on offer</li>
		</ul>
		
		<p>Note that you are not expected to be an expert on forged documents. All you need to do is conduct simple - but careful - visual checks without the need for training or technological aids, to satisfy yourself that the document(s) presented are genuine, and relate to the person presenting them.</p>
		<p>If you are not sure whether or not a document is valid, call the UKBA Sponsorship and Employers' Helpline on Tel 0300 123 4699.</p>
		<p>If the worker provides a document or documents from List B, you must repeat the checks at least once every 12 months to retain the excuse until they provide a specified document or documents from List A, or they leave your employment.</p>
		<p><a href="/employment-and-skills/employment-checks/check-the-eligibility-of-your-workers.php">Use our interactive tool to check if your workers are eligible to work in the UK</a>.</p>
		<p>In order to avoid racial discrimination, you should check all applicants' right to work in the UK - regardless of their race, ethnic or national origin, colour or (apparent) nationality.</p>
		<p><a href="../../../_files/home-office_anti-discrimination-code_2008.pdf" title="How to prevent illegal working, including checking documents in List A or B" target="_blank" rel="noopener noreferrer">Download the code of practice on preventing unlawful discrimination when checking the right to work in the UK from the UKBA website (PDF, 199K)</a>.</p>
		
		<h3>Penalties for employing an illegal worker</h3>
		<p>If you are found to be employing illegal migrant workers and you have not established a statutory excuse, you may have to pay a civil penalty of up to &pound;10,000 per illegal migrant worker.</p>
		<p>However, if you <strong>know</strong> that you are employing a person who is not permitted to work, you will not be entitled to the excuse.</p>
		<p>You could also be prosecuted for the offence of knowingly employing an illegal migrant worker. If you are convicted of this offence, you could face an unlimited fine and/or a prison sentence of up to two years. You can <a href="http://webarchive.nationalarchives.gov.uk/20131002012318/http://www.ukba.homeoffice.gov.uk/sitecontent/documents/employersandsponsors/preventingillegalworking/" title="Find the support available to employers online" target="_blank" rel="noopener noreferrer">find guidance on preventing illegal working on the UKBA website</a>.</p>
		
		<h3>Reporting immigration offences</h3>
		<p>If you suspect that a job applicant is in the UK illegally or has no right to work in the UK, you should report them by sending an email to <a href="mailto:UKBApublicenquiries@ukba.gsi.gov.uk">UKBApublicenquiries@ukba.gsi.gov.uk</a>.</p>
		<p>You can <a href="http://webarchive.nationalarchives.gov.uk/20121030132733/http://www.ukba.homeoffice.gov.uk/aboutus/contact/report-crime/" title="Reporting immigration crime, including smuggling" target="_blank" rel="noopener noreferrer">read guidance on reporting an illegal immigrant on the UKBA website</a>.</p>
		
