		<h2>Here's how I chose the name for my business</h2>
		<p>Managing director Nick Jenkins founded Moonpig, a London-based company selling personalised greeting cards over the internet.</p>
		<p><strong>What I did</strong></p>
		<h3>Consider how a name could work as a memorable brand</h3>
		<p>"I wanted business to spread by word-of-mouth so it was important my company's name was both catchy and memorable. I decided it had to be only two syllables so that people wouldn't forget it, and phonetic so that they wouldn't misspell it. Moonpig is fun and cheeky. Most of our cards are humour based and Moonpig encapsulates that.</p>
		<p>"The name also had to be easily represented in graphical format. Once you've seen our logo you never forget it. I commissioned a cartoonist to design the moonpig. The whole branding exercise cost &pound;200 and three days' work, but it has been far more effective than we could ever have expected."</p>
		<h3>Ensure the name is available on the internet</h3>
		<p>"Control of a domain name was very important for us. To check what was available, I sat on Nominet for four days plugging in variations of names. I originally came up with lots of different names - red-dog, green-carrot, that sort of thing - but they were all already taken.</p>
		<p>"Moonpig was actually my nickname at school. It was available as both a '.com' and a '.co.uk' which was also an important consideration.</p>
		<p>"Moonpig is a completely unique word. The only references to it on Google are links to our website."</p>
		<h3>Trade mark the name</h3>
		<p>"Having the '.com' and '.co.uk' gave the name Moonpig some protection against copiers but we thought people might also try other permutations of Moonpig to capitalise on our success. It wasn't that expensive to go for trade marking and it gave the name an extra layer of protection.</p>
		<p>"I employed a trade mark attorney and Moonpig is now a trade mark in the UK and the US.</p>
		<p>"It's such a small insurance premium. In a customer-facing brand I think it's critical as the brand name is the thing that customers really recognise."</p>
		<p><strong>What I'd do differently</strong></p>
		<h3>Start out with a general, bland registered-company name</h3>
		<p>"Although we continue to trade under the name Moonpig, we changed our name at Companies House to Altergraphics Limited in 2001, when we received venture-capital investment. The new investors seemed reluctant to write out a cheque with the word Moonpig in it.</p>
		<p>"It might have been easier if we'd started out with Altergraphics as our company name anyway. Moonpig pigeonholes us as funky and humorous which is great as a trading name for our current website, but having Altergraphics as a corporate name gives us the flexibility to develop other brands under different trading names in the future."</p>
