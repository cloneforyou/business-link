		<h1>Choose the right name for your business</h1>
		<h2>Displaying and disclosing your business, company or limited liability partnership name</h2>
		<p>Every business must display its business name - and other details - to inform customers and suppliers who they are dealing with. You should not print your stationery until you're certain your proposed name is acceptable.</p>
		<p>Limited companies and limited liability partnerships (LLPs) must wait until registration is complete and a Certificate of Incorporation has been issued. This shows the company's registered name and number - see the page in this guide on <a href="/starting-up/name-your-business/choose-the-right-name-for-your-business/names-for-limited-companies-and-llps.php">names for limited companies and LLPs</a>.</p>
		<p>A sole trader or partnership must obtain prior approval to use a sensitive word in their proposed business name. See the page in this guide on <a href="/starting-up/name-your-business/choose-the-right-name-for-your-business/names-for-sole-traders-partnerships-and-limited-partnerships.php">names for sole traders, partnerships and limited partnerships</a>.</p>
		<h3>Displaying a limited company or LLP name</h3>
		<p>You must display a sign with your company or LLP name:</p>
		<ul>
			<li>in characters that can be easily read</li>
			<li>in a place where visitors can easily and clearly see it at any time and not just during business hours</li>
			<li>continuously</li>
		</ul>
		<p>You must also include your company's or LLP's registered name on all hard copy and electronic business correspondence and documents including:</p>
		<ul>
			<li>letters, notices and other official publications</li>
			<li>emails</li>
			<li>bills of exchange, promissory notes, endorsements and order forms</li>
			<li>cheques signed by or on behalf of the company</li>
			<li>orders for money, goods or services signed by or on behalf of the company</li>
			<li>bills of parcels, invoices and other demands for payment, receipts and letters of credit</li>
			<li>your website - you do not need to include the company name on every page but it must be displayed so it can be easily read</li>
		</ul>
		<h3>Information you must display</h3>
		<p>On all business letters, order forms and websites, you must display:</p>
		<ul>
			<li>the place of registration</li>
			<li>the registered number</li>
			<li>the address of registered office</li>
			<li>the fact that it is a limited company or LLP</li>
			<li>the amount of paid up share capital, if you have chosen to display your share capital</li>
			<li>information if the company or LLP is being wound up</li>
		</ul>
		<p>You do not have to state directors' names on business letters unless you want to do so. However, if you do decide to include directors' names, then you must state all the directors' names.</p>
		<p>If you are an LLP with more than 20 members, you don't need to display the members' names. However, you must keep a list of members at your principal place of business and state that the list is available for inspection.</p>
		<h3>Displaying a sole trader or partnership business name</h3>
		<p>If you are a sole trader or partnership, your business name, your own name, or the partners' names and business address must be clearly displayed in most cases:</p>
		<ul>
			<li>wherever you run your business and deal with customers or suppliers</li>
			<li>on all business letters, orders, payments, invoices, receipts and other business documents</li>
		</ul>
		<h3>Displaying a name online</h3>
		<p>If your business has a website, you must display:</p>
		<ul>
			<li>general information about your business - including business name, address, email address, VAT registration number (if applicable)</li>
			<li>details of any relevant professional body that you belong to or any authorisation scheme to which your service is subject</li>
		</ul>

