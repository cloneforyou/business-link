		<h1>Choose the right name for your business</h1>
		<h2>Names for sole traders, partnerships and limited partnerships</h2>
		<p>People operating as sole traders or in general partnerships can trade under their own names, or choose a different business name.</p>
		<h3>Sole trader, partnership and limited partnership names - the rules</h3>
		<p>If you decide to use a business name, it must not:</p>
		<ul>
			<li>be offensive</li>
			<li>include the terms public limited company (plc), limited (ltd), limited liability partnership (LLP) or their Welsh equivalents</li>
			<li>contain prescribed or sensitive words and expressions, unless you have obtained permission to use them - see the page in this guide on the <a href="/starting-up/name-your-business/choose-the-right-name-for-your-business/use-of-sensitive-words-and-expressions-in-business-names.php">use of sensitive words and expressions in business names</a></li>
		</ul>
		<p>If you register a limited partnership you must include either 'Limited Partnership' or 'LP' (or their Welsh equivalent if registered you are in Wales) at the end of your business name.</p>
		<p>For more information, see our guide on <a href="/starting-up/name-your-business/rules-for-naming-your-limited-company-or-limited-liability-partnership-llp.php">rules for naming your limited company or limited liability partnership (LLP)</a>. Alternatively, you can contact the Companies House Contact Centre on Tel 0845 604 88 88 for a copy.</p>
		<h3>Is anyone else using your proposed business name?</h3>
		<p>Before using your chosen name, check that it isn't already being used.</p>
		<p>If a sole trader at the other end of the country is using it, there may not be a problem. However, if another local business, company or national firm is using it, you should choose a different name.</p>
		<ul>
			<li>Check local phone books, business directories and the internet.</li>
			<li>Make sure that your proposed name - or something similar - hasn't been registered by a company.</li>
			<li>Make sure that the name isn't too similar to a word or expression that has been registered as a trade mark.</li>
			<li><a href="https://www.gov.uk/get-information-about-a-company" target="_blank" rel="noopener noreferrer">Use our interactive tool to search for an available company name and trade mark</a>.</li>
		</ul>
		<p>If you're in any doubt about your business name, get expert advice from your local Business Link.</p>
		
