		<h1>Choose the right name for your business</h1>
		<h2>Getting your business name on the internet</h2>
		<p>Even if you are not intending to create a website for your business immediately, you'll probably be using email and want to have a presence on the web at some point in the future. This could be a single screen advertising your business and giving contact details, or it could be a site that allows customers to browse through products, place orders and make payments online.</p>
		<h3>Choosing a domain name</h3>
		<p>The website address - for example, my-new-business.co.uk - is known as a <b>domain name</b>. For most businesses based in the UK, a name ending with .co.uk is suitable. Your <b>email address</b> will normally include this name - for example, enquiries@my-new-business.co.uk.</p>
		<p>Businesses and individuals that meet certain criteria can apply for the .eu domain extension - for example, www.my-new-business.eu.</p>
		<p>If your business is active in other European Union countries, the .eu domain name can help you market your company as a pan-European business. <a href="https://eurid.eu/en/register-a-eu-domain/" target="_blank" rel="noopener noreferrer">Find out how to register .eu domain names on the European Registry of Internet Domain Names (EURid) website</a>.</p>
		<p>To reserve a domain name for your business, you need to register it through an agent, who will charge a small annual fee. You should do this as soon as possible - even if you're not going to use your domain name straight away.</p>
		<p>You cannot have a domain name that is the same name as a company registered with Companies House.</p>
		<h3>Registering your domain name</h3>
		<ul>
			<li>
				<strong>Decide on a suitable domain name for your website</strong> - if you are unable to use one that exactly matches your business name. You can use numbers as well as letters. Hyphens can be used to separate words - but you can't include spaces, full stops or other punctuation. It's a good idea to have a few alternative names in case your first choice has already been taken.
			</li>
			<li>
				<b>Check whether the name is available -</b> the official registry for UK domain names is Nominet. <a href="http://www.nominet.uk/" target="_blank" rel="noopener noreferrer">Check whether your chosen domain name is available on the Nominet website</a>.
			</li>
			<li>
				<strong>Register the name</strong> - this is a simple process which you can carry out online with any registration agent. There are hundreds of registration agents to choose from - a good starting point is Nominet. <a href="https://www.theukdomain.uk/advice/choosing-a-registrar/" target="_blank" rel="noopener noreferrer">Find out about choosing a registration agent on the Nominet website</a>.
			</li>
		</ul>

