			<h1>Set up and register a limited company (private or public)</h1>
			<h2>The company's officers</h2>
			<p>The officers of your company are the people formally appointed to run it - the company directors and if it has or requires one, the company secretary. Among their other duties the directors have a responsibility to prepare and deliver documents - such as the accounts and annual return to the registrar. They also have a duty to notify changes to, for example, its registered office or directors.</p>
			<p>If officers resign or new ones are appointed, or if their personal details change, the Registrar of Companies must be informed straight away. You can <a href="https://www.gov.uk/topic/company-registration-filing/forms" target="_blank" rel="noopener noreferrer">find downloadable company forms on the Companies House website</a>.</p>
			<p>You can find out more about company directors and secretaries in our guides on <a href="/tax-payroll-and-company-information/company-administration-an-introduction/company-directors-responsibilities.php">company directors' responsibilities</a> and <a href="/tax-payroll-and-company-information/company-administration-an-introduction/company-secretaries-responsibilities.php">company secretaries' responsibilities</a>.</p>
			
			<h3>How many officers does a company need to have?</h3>
			<p>The number of officers required depends on the type of company:</p>
			<ul>
				<li>
					<strong>Private companies</strong> must have at least one director, one of whom must be an individual person (as opposed to another company). A private company is not required to have a secretary but can choose to appoint one if they wish. A sole director may also be the company secretary.
				</li>
				<li>
					<strong>Public limited companies</strong> must have at least two directors, one of whom must be an individual person and a company secretary. The company secretary must be formally qualified.
				</li>
			</ul>

			<h3>Restrictions on company directors</h3>
			<p>It is up to the members to appoint the people they believe will run the company well on their behalf. The only restrictions that prevent anyone becoming a director are:</p>
			<ul>
				<li>they must not have been disqualified from acting as a company director - unless the court has given them permission to act for a particular company</li>
				<li>they must not be an undischarged bankrupt - unless they have been given permission by the court to act for a particular company</li>
				<li>they must not be under the age of 16</li>
			</ul>
			
			<h3>Can one person form a company?</h3>
			<p>An individual can form a private company but the sole director must be an individual. A public company must be formed by at least two persons one of which must be an individual.</p>
