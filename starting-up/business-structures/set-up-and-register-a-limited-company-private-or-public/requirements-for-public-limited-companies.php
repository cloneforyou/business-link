			<h1>Set up and register a limited company (private or public)</h1>
			<h2>Requirements for public limited companies</h2>
			<p>A public limited company (plc) must have:</p>
			<ul>
				<li>at least two directors - who may also be members of the company</li>
				<li>have at least one director who is an individual</li>
				<li>all individual directors must be aged 16 or over</li>
				<li>at least one qualified secretary</li>
			</ul>
			
			<p>The secretary must:</p>
			<ul>
				<li>have held the office of secretary of a plc for at least three of the five years before their appointment</li>
				<li>be a barrister, advocate or solicitor called or admitted in any part of the UK</li>
				<li>by virtue of their previous experience or membership of another body, appear to the directors to be capable of discharging the functions of secretary</li>
				<li>be a member of the Institute of Chartered Accountants in England and Wales, Scotland or Ireland, the Institute of Chartered Secretaries and Administrators, the Association of Chartered Certified Accountants, the Chartered Institute of Management or the Chartered Institute of Public Finance and Accountancy</li>
			</ul>
			
			<h3>When can a plc start business?</h3>
			<p>A company incorporated as a plc cannot conduct business or exercise borrowing powers unless it has obtained a trading certificate from Companies House, confirming that it has the minimum allotted share capital. To obtain a trading certificate for a plc, you must file form SH50. You can <a href="https://www.gov.uk/government/publications/apply-for-trading-certificate-for-a-public-company-sh50" target="_blank" rel="noopener noreferrer">download form SH50 from the Companies House website (PDF, 717K)</a>.</p>
			<p>It is an offence to trade without a trading certificate and the directors are liable, on conviction, to a fine. For information on share capital and trading certificates, see our guide on <a href="/tax-payroll-and-company-information/company-administration-an-introduction/reporting-changes-to-companies-house/change-of-address-directors-and-more-what-to-report-to-companies-house.php">change of address, directors and more - what to report to Companies House</a>.</p>
			<p>Different rules apply if you intend to re-register from a private company limited by shares or a private unlimited company to a plc. You can <a href="https://www.gov.uk/government/publications/life-of-a-company-event-driven-filings#ch8" target="_blank" rel="noopener noreferrer">read about re-registration on the Companies House website</a>.</p>
			<p>Please note the name of a public company must end with 'Public Limited Company', or 'plc' or, if it is registered in Wales, the Welsh equivalent.</p>
			
