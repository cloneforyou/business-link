			<h1>Set up a social enterprise</h1>
			<h2>Limited companies with a social purpose</h2>
			<p>The limited company is an organisational structure which gives limited liability to its members. Some social enterprises take on the form of a limited company. This is a more accountable form than, for example, an unincorporated association.</p>
			<p>Limited companies may have an 'objects' clause that sets out the company's aims or purposes. Although these objects can be commercial, if your business is <strong>a social enterprise</strong>, they must also relate to social and/or environmental objectives such as regenerating an area or providing employment and/or training for people disadvantaged in the labour market. Social enterprises that are registered charities must only have objects that the law defines as charitable, such as relieving financial hardship or promoting education.</p>
			
			<h3>Types of limited company</h3>
			<p>There are two incorporated forms to choose between when setting up a social enterprise as a limited company:</p>
			<ul>
				<li>
					<strong>Company limited by shares (CLS)</strong> - shareholders each hold shares in the company. Their liability is limited to the amount unpaid on shares they hold. A public limited company (plc) differs from a CLS in that its shares can be sold to the general public.
				</li>
				<li>
					<strong>Company limited by guarantee (CLG)</strong> - each of the members gives a guarantee for a certain sum that will be put towards the company's finances if the company is wound up. A CLG cannot raise finance by issuing shares, nor pay dividends to its members.
				</li>
			</ul>
			<p>The issues are complex and you should seek professional advice. See our guide on how to <a href="/starting-up/business-structures/set-up-and-register-a-limited-company-private-or-public.php">set up and register a limited company (private or public)</a>.</p>
			
			<h3>Limited company registration and costs</h3>
			<p>All limited companies - including Community Interest Companies (CICs) - must register (incorporate) and file annual returns at Companies House.
				If you are in England, Scotland or Wales, you can register your limited company with Companies House using our online company incorporation service. Limited companies must also submit a set of memoranda and articles of association. A standard incorporation certificate costs &pound;20.</p>
			<p>There is a fee for incorporating your company with Companies House. The fee varies depending on:</p>
			<ul>
				<li>whether you submit the necessary documentation as hard copies or electronically</li>
				<li>whether or not you want to incorporate on the same day that you submit the documentation</li>
			</ul>
			<p><a href="https://www.gov.uk/government/organisations/companies-house/about/about-our-services#companies-house-fees" target="_blank" rel="noopener noreferrer">Find a full list of Companies House fees on the Companies House website</a>.</p>
			
			<p>Limited companies, with the exception of CICs, can also be set up as charities if the organisation has exclusively charitable objects and is for the public benefit, and should (in most cases) apply to the Charity Commission to be registered as a charity. However, it is very rare for a CLS to be a charity. See the pages in this guide on <a href="/starting-up/business-structures/set-up-a-social-enterprise/social-enterprises-as-registered-charities.php">social enterprises as registered charities</a> and on <a href="/starting-up/business-structures/set-up-a-social-enterprise.php">Community Interest Companies</a>.</p>
			
