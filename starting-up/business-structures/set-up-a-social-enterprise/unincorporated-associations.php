			<h1>Set up a social enterprise</h1>
			<h2>Unincorporated associations</h2>
			<p>The unincorporated association form is usually chosen when a number of individuals agree or 'contract' to come together for a common purpose - which may be of a social nature.</p>
			
			<h3>How unincorporated associations operate</h3>
			<p>Unincorporated associations are relatively straightforward to run and cost nothing to set up. They make their own rules for running the organisation and set these down in a democratic constitution. A management committee is elected to run the organisation on behalf of the members (if it has any).</p>
			<p>Unincorporated associations do not need to register with or be regulated by either Companies House or the Financial Services Authority. They enjoy greater freedom of operation than a company. For example, they don't have to submit annual returns.</p>
			<p>If an unincorporated association's objects are exclusively charitable<strong> </strong>and those objects are for the public benefit, the association should apply to the Charity Commission to be registered as a charity. All charities must follow the requirements of charity law, and most registered charities must also submit annual returns to the Charity Commission. <a href="https://www.gov.uk/topic/running-charity/setting-up" target="_blank" rel="noopener noreferrer">Read frequently asked questions on registering a charity on the Charity Commission website</a>.</p>
			<p>Unincorporated associations may also have trading or business objectives or carry on commercial activities.</p>
			<p>Although an unincorporated association cannot own property, it may be able to set up a trust to legally hold property and assets for the community they are intended to benefit. See the page in this guide on <a href="/starting-up/business-structures/set-up-a-social-enterprise/trusts.php">trusts</a>.</p>
			
			<h3>Unincorporated associations and personal risk</h3>
			<p>Unincorporated associations have <strong>no separate legal identity</strong>. This means that their members will have to sign loans and contracts as individuals and carry the risk of personal liability.</p>
			<p>This form is unlikely to offer a long-term solution if you intend to sign contracts or expand the enterprise. You should consider incorporation if you intend to:</p>
			<ul>
				<li>take on employees</li>
				<li>raise finance, apply for grants or open bank accounts</li>
				<li>issue shares</li>
				<li>enter into large contracts</li>
				<li>take on a lease or buy freehold property</li>
			</ul>
			<p>This should help you to gain access to a wide range of financing sources that will not put your personal assets at risk.</p>
			<p><a href="https://www.ncvo.org.uk/practical-support/information/governance" target="_blank" rel="noopener noreferrer">Read about good governance and leadership on the National Council for Voluntary Organisations (NCVO) website</a>.</p>
			
