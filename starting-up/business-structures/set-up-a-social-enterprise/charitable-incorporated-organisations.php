			<h1>Set up a social enterprise</h1>
			<h2>Charitable incorporated organisations</h2>
			<p>The new legal structure known as the charitable incorporated organisation (CIO) will be available to new organisations and existing charities that wish to convert into the CIO form.</p>
			<h3>Converting to a CIO </h3>
			<p>Under certain conditions, the following organisations may be able to apply for conversion to a CIO through a special conversion process:</p>
			<ul>
				<li>an existing company which is registered as a charity</li>
				<li>a community interest company</li>
			</ul>
			<p>In future a conversion process may be established for charities which are regulated industrial and provident societies.</p>
			<p>Other forms of charity will be able to convert into a CIO by setting up a new CIO, transferring the charity's assets to it, and then winding up the old charity.</p>
			<p>Exempt charities and companies or regulated societies with a share capital - where any of the shares are not fully paid up - will not be able to apply for conversion to a CIO. <a href="https://www.gov.uk/topic/running-charity" target="_blank" rel="noopener noreferrer">Read about exempt charities on the Charity Commission website</a>. </p>
			
			<h3>Special features of CIOs </h3>
			<p>CIOs will differ from other charity forms in the following ways:</p>
			<ul>
				<li>Although CIOs will not use company terminology - directors will be called charity trustees - they will be more like company forms than other types of charity forms.</li>
				<li>As CIOs are always incorporated, they will be separate legal entities and their members will have either no liability or limited liability.</li>
				<li>CIOs - like Community Interest Companies and community benefit societies - will have their assets locked in for their charitable purposes. They will not be able to distribute profits or assets to their members. See the pages in this guide on <a href="/starting-up/business-structures/set-up-a-social-enterprise.php">Community Interest Companies</a> and <a href="/starting-up/business-structures/set-up-a-social-enterprise/community-benefit-societies.php">community benefit societies</a>.</li>
				<li>Unlike some of the other forms used by charities, CIOs will only register with and report to the Charity Commission, not to Companies House or the Financial Services Authority.</li>
				<li>A choice of formats and administration will be available to suit organisations of all sizes, with or without a membership structure.</li>
			</ul>

