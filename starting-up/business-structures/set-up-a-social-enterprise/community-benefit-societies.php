			<h1>Set up a social enterprise</h1>
			<h2>Community benefit societies</h2>
			<p>Community benefit societies (BenComs) are incorporated industrial and provident societies (IPS) that conduct business for the benefit of their community. Profits are not distributed among members, or external shareholders, but returned to the community. For example, a nursery school might use this form to let staff take part in decision-making.</p>
			
			<h3>How BenComs operate</h3>
			<p>As IPS, some key characteristics of BenComs are as follows:</p>
			<ul>
				<li>
					They are set up with social objectives to conduct a business or trade.
				</li>
				<li>
					They are run and managed by their members.
				</li>
				<li>
					They must submit annual accounts.
				</li>
				<li>
					They can raise funds by issuing shares to the public.
				</li>
				<li>
					They can be established as charities, providing they have exclusively charitable objects that are for the public benefit, allowing them to raise capital through public grants and charitable trusts. If approved, they're known as exempt charities - reporting to the Financial Services Authority (FSA), not the Charity Commission. <a href="https://www.gov.uk/topic/running-charity" target="_blank" rel="noopener noreferrer">Read about exempt charities on the Charity Commission website</a>.
				</li>
			</ul>
			
			<p>BenComs are not to be confused with another form of IPS - co-operatives. Co-operatives operate for the mutual benefit of their members and may or may not be a social enterprise, depending on their activities and how they distribute their profits. Co-operatives cannot be established as charities. <a href="http://www.fsa.gov.uk/Pages/Doing/small_firms/MSR/Societies/index.shtml" target="_blank" rel="noopener noreferrer">Read information on IPS on the FSA website</a>. BenComs and co-operatives are both regulated by the FSA. <a href="../../../_files/fsa_mutual-societies-application-form.pdf" target="_blank" rel="noopener noreferrer">Download information on BenComs and their registration from the FSA website (PDF, 98K)</a>.</p>
			
			<h3>BenCom registration and costs</h3>
			<p>To register as a BenCom, you must demonstrate your social objectives and your reasons for registering as a society, rather than a company.</p>
			<p>It can cost between &pound;40 and &pound;950 to register a BenCom with the FSA - payable each year. The fee depends on the BenCom's assets and whether it registers under self-written rules or FSA-approved rules. <a href="../../../_files/fsa_mutual-societies-application-fees.pdf" target="_blank" rel="noopener noreferrer">Download information on application fees from the FSA website (PDF, 23K)</a>.</p>
			<p>You should seek legal advice, particularly if creating your own rules.</p>
			
			<h3>Adopting or opting out of the BenCom structure</h3>
			<p>A registered company may, by special resolution and under certain circumstances, convert into a registered society. Conversely, members can vote to change the objectives of an IPS and convert it into a company.</p>
			
			<h3>Asset locks</h3>
			<p>Charitable BenComs must have an asset lock. Non-charitable BenComs can apply an asset lock, which protects their assets for the future benefit of the community. BenComs that do so may only convert to a Community Interest Company (CIC). See the page in this guide on <a href="/starting-up/business-structures/set-up-a-social-enterprise.php">Community Interest Companies</a>.</p>
			
