			<h1>Set up and register as self-employed</h1>
			<h2>Setting up as self-employed</h2>
			<p>Anyone can set up in business as a sole trader, although for certain types of work you may need a licence or permission from your local authority. Restaurants, childminders, cab drivers and street traders, for example, all need to have a <strong>local authority licence</strong>. Your qualifications and business premises may be inspected beforehand to ensure you comply with regulations.</p>
			<h3>Working from home</h3>
			<p>If you run a business from home, you may have to pay <strong>business rates</strong> for the part of your home that you use for your business. This mainly depends on whether the business area of your home is also used for domestic purposes. If you simply work on a computer in a bedroom, for example, you will probably not have to pay business rates. See our guide: <a href="/tax-payroll-and-company-information/business-rates.php">business rates - an overview</a>.</p>
			<p>You may need to get planning permission to set up a business at home. For example, if your business could cause a nuisance to your neighbours, or if you intend to do any building work to adapt your home. If in doubt, contact the planning department of your local authority. <a href="https://www.planningportal.co.uk/info/200125/do_you_need_permission" target="_blank" rel="noopener noreferrer">Find planning permission guidance on the Planning Portal website</a>.</p>
			<p>Bear in mind that there may also be restrictions in the deeds of your property or, if you rent, in the rental or lease agreement.</p>
			<h3>Choosing a name for your business</h3>
			<p>You can trade under your <strong>own name</strong>, eg M.Y.Self, or use another business name, eg Peerless Promotions.</p>
			<p>If you decide to use a business name, you must make sure that your business <strong>stationery</strong> displays your name as well as the trading name of the business for example, M.Y.Self, trading as Peerless Promotions.</p>
			<p>Your trading name should not be the same as, or too similar to, that of a business which already exists. Be careful that it does not contain words that people might find <strong>offensive or misleading</strong>. See our guide on how to <a href="/starting-up/name-your-business/choose-the-right-name-for-your-business.php" title="How to find the right name for your business and the rules and regulations involved in registering a name">choose the right name for your business</a>.</p>
			
