			<h1>Set up and register as self-employed</h1>
			<h2>Checklist: setting up as self-employed</h2>
			<p>To set yourself up as self-employed there are several things you must do. Make sure that you:</p>
			<ul>
				<li>register straight away for Self Assessment with HM Revenue &amp; Customs (HMRC) so they can set up your tax and National Insurance records</li>
				<li>obtain any permits and planning permission that you may need from your local authority</li>
				<li>contact your local authority to find out whether you need to pay business rates</li>
				<li>contact HMRC to register for VAT if you expect to have turnover of more than &pound;73,000 a year</li>
				<li>register with HMRC for PAYE (Pay As You Earn) if you employ staff</li>
				<li>register with HMRC if you are a contractor or subcontractor in the construction industry</li>
				<li>set up a financial record-keeping system</li>
				<li>put your name on all your business stationery, including letters, invoices, receipts and cheques</li>
			</ul>

			<p>It's worth remembering, though, that this is just a start. As you continue in business, you may well have other legal and tax issues to bear in mind. <a href="/tax-payroll-and-company-information/find-out-what-records-you-should-be-keeping.php">Use our interactive tool to find out the records you must keep as a business</a>.</p>
			<p>For more information on National Insurance contributions, <a href="/tax-payroll-and-company-information/national-insurance/identify-the-national-insurance-contributions-you-must-pay.php">use our interactive tool to identify the National Insurance contributions you must pay</a>.</p>
			<p>The Citizens Advice Bureau provides additional information on a wide range of self-employment topics and gives a list of other organisations to contact for specialist advice. <a href="https://www.citizensadvice.org.uk/work/self-employed-or-looking-for-work/self-employment-checklist/" target="_blank" rel="noopener noreferrer">Find sources of advice on self-employment issues on the Advice Guide website</a>.</p>
			
