		<h1>Set up a social enterprise</h1>
		<h2>Introduction</h2>
		<img src="../../_img/TR1270.jpg" width="78" height="118" border="0" alt="" />
		<p>A social enterprise is a business with primarily social objectives whose surpluses are principally reinvested for that purpose in the business or in the community, rather than being driven by the need to maximise profit for shareholders and owners.</p>
		<p>Within this definition, social enterprises can take on a variety of legal forms, including:</p>
		<p>
			<ul>
				<li>unincorporated associations</li>
				<li>trusts</li>
				<li>limited companies</li>
				<li>some industrial and provident societies such as community benefit societies</li>
				<li>Community Interest Companies</li>
				<li>charitable incorporated organisations</li>
			</ul>
		</p>
		<p>A social enterprise should also consider whether or not to set itself up as a charity. Doing so offers a number of benefits, including significant tax reliefs, but results in increased regulation and less flexibility.</p>
		<p>It is what a business does with its profits that determines whether it is a social enterprise, rather than its specific legal structure.</p>
		<p>However, you should carefully consider the various options to ensure that you choose the legal structure that most suits your social enterprise in terms of management style and mission. Although professional advice is not always necessary, it is a good idea to seek the opinion of an expert before coming to a decision.</p>
		<p>This guide outlines the differences between these legal forms.</p>

