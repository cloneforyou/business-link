		<h1>Legal structures: the basics</h1>
		<h2>Becoming a sole trader</h2>
		<p>Being a sole trader is the simplest way to run a business - it does not involve paying any registration fees, keeping records and accounts is straightforward, and you get to keep all the profits. However, you are personally liable for any debts that your business runs up, which make this a risky option for businesses that need a lot of investment.</p>
		<h3>Management and raising finance</h3>
		<p>You make all the decisions on how to manage your business.</p>
		<p>You raise money for the business out of your own assets and/or with loans from banks or other lenders.</p>
		<h3>Records and accounts</h3>
		<p>You must keep records showing your business income and expenses.</p>
		<h3>Profits</h3>
		<p>Any profits go to you.</p>
		<h3>Tax and National Insurance</h3>
		<p>As you are self-employed:</p>
		<ul>
			<li>
				your profits are taxed as income
			</li>
			<li>
				you pay fixed-rate Class 2 National Insurance contributions (NICs) regardless of any profits you make
			</li>
			<li>
				you pay Class 4 NICs on any profits
			</li>
			<li>
				you need to register for Self Assessment and complete a tax return each year
			</li>
		</ul>
		<p>See our guide on <a href="/tax-payroll-and-company-information/self-assessment/your-tax-return-the-basics/registering-for-self-assessment.php">registering for Self Assessment</a>.</p>
		<h3>Liability</h3>
		<p>As a sole trader, you are personally responsible for any debts run up by your business. This means your home or other assets may be at risk if your business runs into trouble.</p>
		<p>For more information on starting your own business, see our guide on how to <a href="/starting-up/business-structures/set-up-and-register-as-self-employed.php" title="How to meet your legal and tax responsibilities">set up and register as self employed</a>.</p>
		
