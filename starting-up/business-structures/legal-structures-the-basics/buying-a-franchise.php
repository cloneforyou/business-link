			<h1>Legal structures: the basics</h1>
			<h2>Buying a franchise</h2>
			<p>A franchise is not a legal structure but is a business model that can operate under one of the legal structures, ie as a sole trader, or type of partnership or limited company - see the pages in this guide on:</p>
			<ul>
				<li><a href="/starting-up/business-structures/legal-structures-the-basics/becoming-a-sole-trader.php">becoming a sole trader</a></li>
				<li><a href="/starting-up/business-structures/legal-structures-the-basics/partnerships.php">going into partnerships</a></li>
				<li><a href="/starting-up/business-structures/legal-structures-the-basics/starting-a-private-company.php">starting a private company</a></li>
				<li><a href="/starting-up/business-structures/legal-structures-the-basics/setting-up-specific-types-of-limited-company.php">starting a public limited company</a></li>
			</ul>
			<p>Buying a franchise is a way of taking advantage of the success of an established business. As the '<strong>franchisee</strong>', you buy a licence to use the name, products and services of the 'franchisor' business. This licence normally covers a particular geographical area and runs for a limited time, after which it should be renewable as long as you meet the terms of the franchise agreement.</p>
			<p>Franchise agreements usually set out how the franchised business should be run, although they may allow some flexibility. Franchisors usually provide management help and training to franchisees.</p>
			<p>The way you pay for the franchise may be through an initial fee, ongoing management fees, a percentage of your turnover, purchases of goods from the franchisor, or a combination of these.</p>
			<p>You often pay a percentage of your turnover to the franchisor, which reduces your overall profits.</p>
			
