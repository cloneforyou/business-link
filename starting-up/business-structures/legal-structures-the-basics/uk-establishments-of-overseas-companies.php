			<h1>Legal structures: the basics</h1>
			<h2>UK establishments of overseas companies</h2>
			<p>If you are an overseas company, you must - within one month of opening a UK establishment - register with Companies House. Registration is via paper application only.</p>
			<p>If this is going to be your first UK establishment, you must also send Companies House:</p>
			<ul>
				<li>a certified copy of your company's constitutional documents, eg charter, statute, memorandum and articles of association, with a certified translation in English if the original is in a language other than English</li>
				<li>a copy of your company's latest set of accounts, again with a certified translation in English if the original is in a language other than English</li>
			</ul>
			<p>You only have to submit the accounts if either:</p>
			<ul>
				<li>the accounts must be filed under the law of country in which your company is incorporated - the 'parent law'</li>
				<li>your company is incorporated in an European Economic Area state and the parent law requires you to prepare and disclose accounts but does not require such accounts to be audited or delivered</li>
			</ul>
			<p>If this is going to be an additional UK establishment, you don't have to deliver the constitutional documents. You may instead state in the registration document that you have delivered them in respect of another UK establishment - but you must give the registered number of that establishment.</p>
			<p>You must notify Companies House of any changes to the original registration information. You must deliver the forms notifying the changes to Companies House within 21 days of the change.</p>
			<p>If there is a change in the parent state that affects your company, you must deliver the notifying form to Companies House within 21 days of the date on which notice of the change could have been received by post in the UK.</p>
			
