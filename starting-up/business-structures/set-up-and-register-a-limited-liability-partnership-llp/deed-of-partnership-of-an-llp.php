			<h1>Set up and register a limited liability partnership (LLP)</h1>
			<h2>Deed of partnership of an LLP</h2>
			<p>A deed of partnership (or 'partnership agreement') is a legally binding agreement between the partners who are in business together. It describes how the partnership will be run and the rights and duties of the members themselves. The deed of partnership is usually drawn up by a solicitor, who will consult with the partners about exactly what should be in it.</p>
			<p>It's not absolutely necessary to have a deed in order to set up a limited liability partnership, but it's a good idea as it can help to prevent misunderstandings and disputes between members.</p>
			
			<h3>What does the deed of partnership cover?</h3>
			<p>As well as giving basic information about the partnership, such as its business name and the names of the partners, the type of business and business address, the deed will usually set out:</p>
			<ul>
				<li>the amount of capital that each partner is to contribute to the business</li>
				<li>the way in which profits or losses are shared between partners, and whether any of the partners should be paid a salary</li>
				<li>working arrangements, such as how much time each partner should contribute to the business, who does what management tasks and what type of decisions need collective agreement between the partners</li>
				<li>
					changes to the partnership, such as how new partners can be appointed and what happens if a partner dies or wishes to leave
				</li>
			</ul>
			
			<p>If members do not have a deed, they will be governed by the terms of the Limited Liability Partnerships Act 2000 (LLP Act 2000), which does not offer solutions to many of the problems that can arise and may not suit the way that you and your members want to work together. <a href="https://www.gov.uk/hmrc-internal-manuals/inheritance-tax-manual/ihtm25094" target="_blank" rel="noopener noreferrer">Read about the LLP Act 2000 on the HM Revenue &amp; Customs (HMRC) website</a>.</p>
			
