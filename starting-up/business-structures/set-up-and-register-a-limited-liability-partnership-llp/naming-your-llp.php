			<h1>Set up and register a limited liability partnership (LLP)</h1>
			<h2>Naming your LLP</h2>
			<p>A limited liability partnership (LLP) can trade under the names of the partners, or some of the partners. You might choose to use an established business name of this kind if you are converting your ordinary partnership to an LLP. Alternatively you can use another business name, for example H &amp; S LLP.</p>
			<p>The trading name should not be the same as, or too similar to, that of any business that already exists, and it should not contain words that people might find offensive or misleading. The name must end with 'Limited Liability Partnership' or 'LLP'.</p>
			<p>You will need to obtain permission from the relevant professional bodies to use professional words, such as architect or solicitor, in your business name.</p>
			<p>You will also need to obtain permission to use words connected with public authorities, such as health, education, etc.</p>
			<p>You must make sure that your business website and stationery - such as letters and invoices - display the trading name, the fact that it is an LLP, the place of registration, the registration number, and the address of the registered office.</p>
			<p><a href="https://www.gov.uk/government/publications/limited-liability-partnership-incorporation-and-names#ch7" target="_blank" rel="noopener noreferrer">Find out how and where you must disclose the name of your LLP on the Companies House website</a>.</p>
			<p>For more information, see our guides on how to <a href="/starting-up/name-your-business/choose-the-right-name-for-your-business.php">choose the right name for your business</a> and <a href="/starting-up/name-your-business/rules-for-naming-your-limited-company-or-limited-liability-partnership-llp.php">rules for naming your limited company or limited liability partnership (LLP)</a>.</p>
			
