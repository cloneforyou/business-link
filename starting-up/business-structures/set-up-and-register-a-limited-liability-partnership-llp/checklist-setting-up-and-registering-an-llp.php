			<h1>Set up and register a limited liability partnership (LLP)</h1>
			<h2>Checklist: setting up and registering an LLP</h2>
			<p>As well as registering your limited liability partnership (LLP) with Companies House, there are several other things that you need to do to put it on a proper legal footing and maintain its status. Make sure you:</p>
			<ul>
				<li>send Companies House a completed and signed incorporation document - form LL IN01 - together with the appropriate filing fee</li>
				<li>display your LLP's name on the outside of all its offices or other places of business</li>
				<li>display your LLP's name on all its business stationery, including letters, invoices, receipts and cheques</li>
				<li>show your LLP's place of registration, registered number and registered office address on all its business letters, order forms and electronic business communications</li>
				<li>check that you have received a Certificate of Incorporation from Companies House</li>
				<li>register with HM Revenue &amp; Customs (HMRC) for VAT, PAYE or CIS if appropriate</li>
				<li>make sure all partners are registered with HMRC for Self Assessment</li>
				<li>send a Self Assessment tax return for the LLP to HMRC</li>
				<li>send a set of accounts to Companies House every year</li>
				<li>send Companies House an annual return - form LL AR01 - in order to keep your LLP's records up to date</li>
				<li>inform Companies House of any changes to your LLP's membership, the personal details of its members or the address of its registered office</li>
			</ul>

			<p>If the partnership has a website, it will need to show the partnership's name, registered office and geographic addresses, registered number, VAT number and the place of registration. It must also include an email address.</p>
			
