			<h1>Set up and register a partnership</h1>
			<h2>Types of partner</h2>
			<p>There are three main types of partner, each of which has different rights and responsibilities.</p>
			<p><strong>General partners</strong> invest in the business, take part in running it and share in its profits. Each general partner is fully liable for any debts that the partnership may have. This means that they could lose more than their initial investment in the business if it runs into trouble, and that their personal assets could be at risk. Every ordinary and limited partnership must have at least one general partner.</p>
			<p><strong>Limited partners</strong> are not permitted to participate in the day-to-day running of the business. Their debt is limited to the amount of their initial investment.</p>
			<p><strong>Sleeping partners</strong> invest money in the business and share in its profits, but do not take part in running it. Like general partners, they are fully liable for the partnership's debts.</p>
			<h3>Limited partnerships and limited liability partnerships</h3>
			<p>For information about setting up limited partnerships and limited liability partnerships, see our guides on how to <a href="/starting-up/business-structures/set-up-and-register-a-limited-partnership.php">set up and register a limited partnership</a> and <a href="/starting-up/business-structures/set-up-and-register-a-limited-liability-partnership-llp.php">set up and register a limited liability partnership (LLP)</a>.</p>
			<h3>Companies and limited liability partnerships (LLPs) as members of partnerships</h3>
			<p>As well as individuals, other legal entities - such as companies and LLPs - can also be members of partnerships. They have the same rights and responsibilities within the partnership as other partners. See the page in this guide on <a href="/starting-up/business-structures/set-up-and-register-a-partnership/tax-matters-of-a-partnership.php">tax matters of a partnership</a>.</p>
			<p>Partnerships whose members are all companies have to prepare 'partnership accounts' and send them to Companies House each year. Members of such partnerships must attach a copy of these documents to their own company accounts when they submit them to Companies House.</p>
			
