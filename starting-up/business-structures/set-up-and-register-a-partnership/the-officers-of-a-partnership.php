			<h1>Set up and register a partnership</h1>
			<h2>The officers of a partnership</h2>
			<p>Any group of people who want to set up a profit-making business together can form a partnership. However, if a member of a partnership is under the age of 18, they cannot be legally bound by the terms of the partnership agreement.</p>
			<p>It is also possible for companies and limited liability partnerships to be members of a partnership.</p>
			
			<h3>How many officers can a partnership have?</h3>
			<p>Forming a partnership allows two or more people to set up in business together, sharing profits, managing burdens and risks.</p>
			
			<h3>The rights and responsibilities of partners</h3>
			<p>Partners normally share in both the responsibilities of running the business and the profits or losses that it makes. However, their precise rights and responsibilities will depend on:</p>
			<ul>
				<li>what type of partner they are</li>
				<li>any partnership agreement or 'deed of partnership' that they have entered into</li>
			</ul>

