			<h1>Set up and register a partnership</h1>
			<h2>Checklist: setting up a partnership</h2>
			<p>In order to set up business as a partnership there are certain things you need to do - some must be done as a group and others as individual partners. You should:</p>
			<ul>
				<li>Display all the partners' names at all your business premises together with the address to which official documents should be sent.</li>
				<li>Display all the partners' names on your business website and stationery, including letters, invoices, receipts and cheques along with your principal place of business. If the partnership has more than 20 partners you need only display your principal place of business.</li>
				<li>Register the partnership, and each partner, for Self Assessment with HM Revenue &amp; Customs (HMRC).</li>
				<li>Contact HMRC to register your partnership for VAT if you expect a turnover of more than &pound;73,000 a year.</li>
				<li>Register with HMRC for PAYE (Pay As You Earn) if the partnership employs staff.</li>
				<li>Register with HMRC for the Construction Industry Scheme if the partnership is a contractor or sub-contractor.</li>
			</ul>

			<p>It's worth remembering that this is just a start. As an ongoing business, your partnership will have many other legal and tax obligations to bear in mind. <a href="/tax-payroll-and-company-information/find-out-what-records-you-should-be-keeping.php">Use our interactive tool to find out the records you must keep as a business</a>.</p>
			
