			<h1>Set up and register a limited partnership</h1>
			<h2>Dissolving a limited partnership</h2>
			<p>You can dissolve your limited partnership for any number of reasons - for example financial reasons, business purposes or personal disagreements. For limited partnerships, the dissolution must be handled by the general partners unless the court decides otherwise.</p>
			<p>A limited partnership cannot be dissolved through any of the following circumstances:</p>
			<ul>
				<li>a limited partner giving notice - unless there is a previous agreement between the partners</li>
				<li>a limited partner offering his share as security for a debt - unless there is a previous agreement between the partners</li>
				<li>the death or bankruptcy of a limited partner</li>
				<li>a limited partner is considered a 'person of unsound mind' - unless their share cannot be determined</li>
			</ul>
			
			<h3>Notifying Companies House</h3>
			<p>The general partners are responsible for filing Forms LP5 and LP6 at Companies House even if accountants or other professionals have helped in their preparation.</p>
			<p>The Limited Partnerships Act 1907 provides for the levying of penalties for failing to send the required forms to the Registrar.</p>
			<p>There is no specific requirement for you to notify Companies House when dissolving a limited partnership. However, if you do notify Companies House of the dissolution of your limited partnership by filling in a form LP6, it will be accepted in good faith but the name will still remain on the index of live company names.</p>
			
