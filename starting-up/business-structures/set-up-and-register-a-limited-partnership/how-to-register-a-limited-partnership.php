			<h1>Set up and register a limited partnership</h1>
			<h2>How to register a limited partnership</h2>
			<p>You must register a limited partnership in accordance with the Limited Partnership Act 1907.</p>
			<p>To register, you need to complete a limited partnership registration form - LP5 - have it signed by all the partners of the business, and return it to the Registrar of Companies. Once form LP5 has been accepted and registered, the limited partnership will officially come into existence.</p>
			<p>You can <a href="https://www.gov.uk/government/publications/apply-for-a-registration-of-a-limited-partnership-lp5" target="_blank" rel="noopener noreferrer">download limited partnership registration form LP5 from the Companies House website (PDF, 244K)</a>.</p>
			<p>You must include all possible information on the limited partnership registration form, specifically the:</p>
			<ul>
				<li>firm's name and appropriate ending - for example 'Limited Partnership' or 'LP'</li>
				<li>nature of the business</li>
				<li>business address</li>
				<li>full names of each partner - with separate lists for general and limited partners</li>
				<li>length of the partnership - if applicable</li>
				<li>statement that the partnership is limited and a description of each limited partner</li>
				<li>amount that each limited partner is contributing - and the form that the contribution is to take</li>
			</ul>

			<p>If all this information - the particulars - is included correctly, the Registrar of Companies will issue a certificate of registration.</p>
			<p>The standard fee for registering a limited partnership is &pound;20 - or &pound;100 for a same-day registration.</p>
			<p>Overseas limited partnerships cannot be registered as the business address must be an area of the UK where the principal place of business is situated.</p>
			
			<h3>Changing limited partnership details</h3>
			<p>You can change any details about your limited partnership by completing and returning form LP6 to Companies House. You must notify the Registrar of Companies within seven days of the change being made.</p>
			<p><a href="https://www.gov.uk/government/publications/specify-the-nature-of-a-change-in-the-limited-partnership-lp6" target="_blank" rel="noopener noreferrer">Download limited partnership form LP6 from the Companies House website (PDF, 324K)</a>.</p>
			
