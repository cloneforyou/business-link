			<h1>Set up and register a limited partnership</h1>
			<h2>Definition of a limited partnership and partners</h2>
			<p>A limited partnership is any partnership that includes one or more general partners and one or more limited partners among its officers. Limited partners differ to general partner by contributing money as either capital or property valued at a specific amount.</p>
			<p>A limited partnership is a different business model to a limited liability partnership. For more information, see the page on <a href="/starting-up/business-structures/legal-structures-the-basics/partnerships.php">partnerships</a> in our guide on <a href="/starting-up/business-structures/legal-structures-the-basics.php">legal structures: the basics</a>.</p>
			
			<h3>Differences between general and limited partners</h3>
			<p>Any individual or legal body - for example a company - may be a partner in a limited partnership, either as a general or as a limited partner. However, you cannot be a general and a limited partner at the same time.</p>
			<p>You will have different debts and obligations in a limited partnership, depending on which type of partner you are:</p>
			<ul>
				<li>general partners are liable for all debts and obligations of the partnership</li>
				<li>limited partners are only liable for the debts or obligations they put into the business</li>
			</ul>

			<p>Because limited partners have some form of legal protection from incurring any additional debts, they may not:</p>
			<ul>
				<li>take out their contribution to the partnership for as long as it exists</li>
				<li>control or manage the business</li>
				<li>have the power to make any binding decisions for the firm</li>
			</ul>

			<p>If a limited partner chooses to remove any of their original contribution they will lose their protection, and become liable for debts or obligations up to the amount they have received from the partnership. In addition, if a limited partner opts to manage the business for a length of time, they will become liable for any debts or obligations incurred throughout this period.</p>
			<p>A limited partnership must be registered with Companies House. For more information, see the page in this guide on <a href="/starting-up/business-structures/set-up-and-register-a-limited-partnership/how-to-register-a-limited-partnership.php">how to register a limited partnership</a>.</p>
			
