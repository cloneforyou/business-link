		<h1>Family-run businesses</h1>
		<h2>Managing conflict in family businesses</h2>
		<p>The potential for conflict in family businesses can be greater than for other businesses - typically due to a clash between commercial and emotional concerns.</p>
		<p>However, conflict can be seen as a <strong>challenge</strong> - or even as a positive driver for change. For example, a disagreement between family members on the strategic direction of your business may result in a much-needed rethinking of your business plan and a new agreed vision for the business.</p>
		<p>Such outcomes, though, are only possible if techniques for avoiding, managing and resolving disputes have already been instituted.</p>
		<h3>Ways to avoid conflict</h3>
		<p>Think about how people in your family business communicate with each other. Are emotional issues kept separate from business discussions? Are mechanisms in place to allow all employees - not just family members - to contribute their views? Or does one person tend to dominate?</p>
		<p>The best way of avoiding conflict is to prevent misunderstandings from happening in the first place. Drawing up a family constitution can help you achieve this.</p>
		<p>Plan how you'll deal with conflict and disagreements and set this out in the family business constitution, see our guide on <a href="/employment-and-skills/problems-at-work/managing-conflict.php">managing conflict</a>.</p>
		<p>Holding a <strong>meeting</strong> of the business' management may be appropriate for addressing relatively minor disputes.</p>
		<p>For more serious matters you may want to involve an independent <strong>third party</strong> - many family businesses benefit from having a non-executive director or business adviser - to act as an impartial mediator.</p>
		
