		<h1>Here's how having a family member in the business made a difference</h1>
		<h2>Here's how having a family member in the business made a difference</h2>
		<p>Peter Henman was 15 when he joined his brother as the fourth generation in his family's Bedfordshire-based construction and funeral businesses. He worked his way up from teaboy to chairman and now both he and the businesses are reaping the benefits of the fifth generation to join the companies. His daughter Vicky Trumper is a director and company secretary of both Neville Construction Group and Neville Funeral Service.</p>
		<p><strong>What we did</strong></p>
		<h3>Brought in a new era of confidence and long-term planning</h3>
		<p>"Vicky has brought in a new period of confidence for the future. In most companies you can do three- or five-year plans but when I came into the business we talked about ten or even 15 years ahead. Now we can go back to doing that.</p>
		<p>"For example, on the funeral side of the business we've invested further and advanced it greatly since Vicky came in. With Vicky's arrival, shareholders could see we had the ability to make longer-term investment plans that she would bring to fruition and were happy the investment was made. Without the confidence in the future that Vicky provides, the decisions may not have been so easy to make."</p>
		<h3>Brought in experience from outside the business</h3>
		<p>"Vicky's entrance into the business has brought a lot of people-focused skills she gained from her previous positions in the National Health Service. The health service is a people business like the construction and funeral businesses and her skills gained in another environment have helped us in sharpening our people policies.</p>
		<p>"Now the ownership and leadership of the company is spread between the older and experienced family members and the enthusiastic younger generation, giving balance and stability and a mixture of ideas gained from different environments. It also gives confidence to the staff for the long-term future of the business."</p>
		<h3>Allowed me to take a sideways step</h3>
		<p>"It's almost inconceivable that a chairman of a family firm could ever walk out and shut the door on the business completely, but personally I am in favour of moving sideways. I've got the confidence to do that with Vicky there.</p>
		<p>"The first generation of our company was still signing cheques at 90, the next still coming into work when he was 80 and the third died when still working at 75. Vicky's arrival means that I may still be able to contribute for as long as I like, but with the reassurance that the core of the business is being well run by the next generation."</p>
		<p><strong>What could have happened without Vicky</strong></p>
		<h3>Fallen behind with technological developments</h3>
		<p>"Vicky did a business administration course and she did her project work on IT related to the business. That was particularly useful because she's from a generation at home with IT. Because of my age we were behind with this technology before she joined but now it's spread throughout the business. She'll perhaps bring the business closer to the world of the paperless office and instant communication than I ever could have."</p>
		
