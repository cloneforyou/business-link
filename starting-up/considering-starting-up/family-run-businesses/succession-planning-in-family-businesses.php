		<h1>Family-run businesses</h1>
		<h2>Succession planning in family businesses</h2>
		<p>How best to pass on your business to the next generation will be one of the biggest challenges you face. You need to make the right decisions for you, your family and your business - balancing the needs of all three.</p>
		<p>It can be made easier if you plan the succession process early - ideally when you set up the business.</p>
		<p>Your <strong>succession plan</strong> should include:</p>
		<ul>
			<li>your key goals for the succession process</li>
			<li>a timetable of the transition stages, from identifying a successor to the staged and then full transfer of responsibilities</li>
			<li>contingency plans in case the unforeseen happens, such as your intended successor declining the role</li>
		</ul>
		<p>Questions you should ask yourself include the following:</p>
		<ul>
			<li>Does my intended successor have the right <strong>skills</strong> and <strong>abilities</strong>?</li>
			<li>Does my intended successor actually want to take over?</li>
			<li>Is my plan <strong>fair</strong> to all family members?</li>
			<li>Does it minimise the potential for conflict?</li>
			<li>Will family succession be <strong>tax-efficient</strong>? <a href="/tax-payroll-and-company-information/selling-or-closing-your-business/check-tax-and-legal-issues-when-selling-a-business.php">Use our interactive tool to investigate the tax and legal issues when selling or closing your business</a>.</li>
			<li>Is family succession the best option or would an <strong>alternative exit strategy</strong> - such as a trade sale or management buy-out - be a better option?</li>
		</ul>
		<p>For further information, see our guides on <a href="/buy-or-sell-a-business/transferring-your-business/selling-or-passing-a-business-to-a-family-member.php">selling or passing a business to a family member</a> and how to <a href="/buy-or-sell-a-business/considering-buying-a-business/consider-your-exit-strategy-when-starting-up.php">consider your exit strategy when starting up</a>.</p>
