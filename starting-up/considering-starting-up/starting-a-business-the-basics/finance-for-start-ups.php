		<h1>Starting a business: the basics</h1>
		<h2>Finance for start-ups</h2>
		<p>When you start up in business or self employment you will need money to get under way and to keep the business going until you get paid by your customers. You will also need to cover your personal expenses until the business can support you.</p>
		<p>There are many different sources of funding for start-ups including personal savings, money from family and friends, bank loans, grants and equity funding from private investors. Creative thinking and planning your cashflow might save you money and make it less costly than you think to start your own business.</p>
