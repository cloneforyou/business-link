		<h1>Common mistakes when starting up - and how to avoid them</h1>
		<h2>Weak financial planning</h2>
		<p>Financial planning is extremely important for most new businesses. A lack of capital, lack of a contingency plan and reluctance to seek professional advice can all bring major problems.</p>
		<h3>Lack of capital</h3>
		<p>Having sufficient capital is essential for the survival and prosperity of your business, and is a primary indicator of your business' health.</p>
		<p>It is important to create a high-quality <strong>business plan</strong> to attract and secure the right type and amount of funding that you need to make your business successful. A business plan can:</p>
		<ul>
			<li>be used as a tool to structure the financial side of your business and can be updated and changed as your business grows</li>
			<li>keep your expectations grounded for what the business can deliver</li>
		</ul>
		<p>For more information, see our guide on how to <a href="/starting-up/business-planning/prepare-a-business-plan.php">prepare a business plan</a>.</p>
		<h3>Lack of a contingency plan</h3>
		<p>Without a contingency plan you can leave yourself exposed to the unexpected.</p>
		<p>Situations beyond your control that may impact on your business and cashflow include interest rate rises, transport strikes and political instability. While your business can survive periods where there are no sales or profits, it cannot survive without cash. Building up cash reserves will ensure that you can trade effectively and develop your business.</p>
		<p>For more information, see our guides on <a href="/finance-and-grants/manage-your-cashflow/cashflow-management-the-basics.php">cashflow management: the basics</a> and <a href="/starting-up/considering-starting-up/how-do-I-survive-until-my-business-is-off-the-ground.php">how do I survive until my business is off the ground?</a></p>
		<h3>A reluctance to seek professional advice</h3>
		<p>Failing to seek professional advice will make any financial troubles worse. Few new business owners can claim expertise in all areas of their business. Using an accountant or financial adviser can help you ensure you borrow and manage money cost-effectively.</p>
		<p>For more information, see our guides on how to <a href="/finance-and-grants/financial-planning-and-accounts/choose-and-work-with-an-accountant.php">choose and work with an accountant</a>.</p>
		<p>You can also <a href="http://www.apfa.net/APFA/Consumers/Find_a_financial_adviser/APFA/Consumers/Find_a_financial_adviser.aspx" target="_blank" rel="noopener noreferrer">find a local independent financial adviser on the Association of Independent Financial Advisers (AIFA) website</a>.</p>

