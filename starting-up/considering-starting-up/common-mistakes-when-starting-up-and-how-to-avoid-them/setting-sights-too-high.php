		<h1>Common mistakes when starting up - and how to avoid them</h1>
		<h2>Setting sights too high</h2>
		<p>It is important to make realistic forecasts about your business' potential. During the start-up phase, it can be easy to make over-optimistic forecasts, however there can be serious consequences for your business if your projections are not realistic.</p>
		<h3>Over-optimistic forecasts about market size</h3>
		<p>Inaccurate forecasting of market size is a common mistake when starting up.</p>
		<p>Cash levels can be quickly depleted if you recruit too many people, buy unnecessary equipment or spend too much on business premises. Effective cashflow and income forecasting can help you avoid this.</p>
		<p>Inaccurate forecasting is often linked to poor market research, so it is essential to get your research right. For more information, see our guides on <a href="/sales-and-marketing/marketing/market-research-and-market-reports.php">market research and market reports</a> and <a href="/finance-and-grants/manage-your-cashflow/cashflow-management-the-basics.php">cashflow management: the basics</a>.</p>
		<h3>Focusing on sales volume or size not profit</h3>
		<p>A common mistake for new businesses is to focus too much on growing the sales volume or size rather than profit.</p>
		<p>Overtrading can occur during the rapid expansion of a new business when it takes on more orders than can be supported by its working capital or net current assets. This can have serious repercussions.</p>
		<p>For more information, see our guides on how to <a href="/sales-and-marketing/selling/forecast-and-plan-your-sales.php">forecast and plan your sales</a> and <a href="/finance-and-grants/financial-planning-and-accounts/avoid-the-problems-of-overtrading.php">avoid the problems of overtrading</a>.</p>
		<h3>Diversifying too soon</h3>
		<p>There may be a temptation for you to tap into a new market or geographical area, but keeping a clear focus on your core business is crucial. Diversifying too quickly can actually increase your business risks during the vulnerable start-up stage.</p>
		<h3>Poor planning</h3>
		<p>Poor planning will increase your chances of making business mistakes and will reduce the probability of achieving your goals.</p>
		<p>Drawing up a high-quality and realistic business plan is essential. A business plan will help to secure external funding, pre-empt problems and measure how well your business is doing.</p>
		<p>Writing a marketing plan will also ensure that you take into account your target customers, your marketing objectives and will help you set goals to address these.</p>
		<p>For more information, see our guides on how to <a href="/starting-up/business-planning/prepare-a-business-plan.php">prepare a business plan</a> and <a href="/sales-and-marketing/marketing/write-a-marketing-plan.php">write a marketing plan</a>.</p>

