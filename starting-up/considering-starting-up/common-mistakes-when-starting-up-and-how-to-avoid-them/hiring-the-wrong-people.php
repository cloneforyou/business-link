		<h1>Common mistakes when starting up - and how to avoid them</h1>
		<h2>Hiring the wrong people</h2>
		<p>A large part of your new business' success will be determined by the <strong>quality of the people you recruit</strong>. Taking on people will always mean some form of investment for your business and requires careful consideration. Taking this investment seriously can make it more valuable and improve your chances of success.</p>
		<p>Ensuring that you hire high-calibre people with the right mix of skills is not an easy process but one that will pay dividends.</p>
		<p>How you go about employing new people will depend on your business needs, eg whether the work is constant, how long it will last and the number of hours available.</p>
		<p>You need to explore all the options available to you. These include:</p>
		<ul>
			<li>recruiting permanent staff on a full or part-time basis</li>
			<li>fixed-term contract employee</li>
			<li>temporary staff</li>
			<li>freelancers</li>
			<li>consultants</li>
			<li>contractors</li>
		</ul>
		<p>Employing relatives and friends may appear an easy solution to staffing issues, but they may not have the right mix of skills that you need. It can also be more difficult to bring a period of employment to a close when a personal relationship exists.</p>
		<p>For more information, see our guide on <a href="/employment-and-skills/take-on-staff/taking-on-staff-the-options.php">taking on staff - the options</a>.</p>
		<h3>Failing to delegate</h3>
		<p>Being your own boss may be a key motivator to setting up your own business. However, delegating the right task to the right person is important for both you and your business. Failing to delegate could mean you take on too much and increase your stress levels.</p>
		<p>A good way to tackle delegation is to identify a few key tasks of your own that are very valuable to the business and hand over the rest to your team. For more information, see our guide on <a href="/employment-and-skills/skills-and-development/skills-and-training-for-directors-and-owners.php">skills and training for directors and owners</a>.</p>

