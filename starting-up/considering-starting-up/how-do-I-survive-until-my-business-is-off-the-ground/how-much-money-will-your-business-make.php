		<h1>How do I survive until my business is off the ground?</h1>
		<h2 >How much money will your business make?</h2>
		<p>You need to identify how much money your business is likely to bring in over the coming year and then how much profit you hope to make.</p>
		<p>You can do this by:</p>
		<ul>
			<li>estimating your total income from sales</li>
			<li>estimating your expenses</li>
			<li>working out a figure for salaries and dividends, including tax</li>
			<li>working out the difference between your financial requirements and the amount you are prepared to take out of the business</li>
		</ul>
		<p>This will leave you with the amount you potentially need to find from other sources. For advice on what you will need to take into account and plan for, see our guide on how to <a href="/starting-up/business-planning/prepare-a-business-plan.php">prepare a business plan</a>.</p>
		<h3>Profit and cashflow</h3>
		<p>It may not be easy to calculate exactly how much your business will make in its first year, so it is important to concentrate on managing cashflow rather than profit.</p>
		<p>Profit is the difference between the total amount your business earns and the costs it must pay out over the trading period - usually a year. Even a nominally profitable business can find itself in trouble if it runs out of cash.</p>
		<p>Cashflow is the balance of all the money flowing into, and out of, your business. It covers actual payments of money, as opposed to what is owed by your debtors or to your creditors. Cash pays the bills and allows trading to continue. The need for cash is even greater if your business is growing and extending credit to more customers.</p>
		<p>The main outflows of cash are:</p>
		<ul>
			<li>wages and salaries</li>
			<li>overheads such as rent and rates</li>
			<li>capital spending on plant and equipment</li>
			<li>working capital such as stock and raw materials</li>
		</ul>
		<p>If you sell on credit, your cash inflow is delayed until you are actually paid so effective credit control is important. A business that buys on credit and is paid in cash, such as a retailer, is at a great advantage in cashflow terms. Businesses which make sales over the internet can also be cash positive.</p>
		<p>Many businesses rely on bank overdrafts and quickly reach their borrowing limits. It is therefore important to think carefully about your cashflow and reduce the need to rely on an overdraft. See our guide on <a href="/finance-and-grants/manage-your-cashflow/cashflow-management-the-basics.php" title="How to manage your cashflow and avoid cash problems in your business">cashflow management: the basics</a>.</p>

