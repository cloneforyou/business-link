		<h1>How do I survive until my business is off the ground?</h1>
		<h2>Other sources of income</h2>
		<p>You will almost certainly have to find other ways to finance your needs during the early days of your business.</p>
		<p>There are a number of options open to you. You could:</p>
		<ul>
			<li>Use savings - make sure that you have an emergency rainy-day fund which should add up to three months spending.</li>
			<li>Start up a business while retaining your existing employment - using your spare time to run your business.</li>
			<li>Release equity from an existing asset - for example, trading in your car for a cheaper one.</li>
			<li>Sell unwanted assets to create income - many people have things that they do not use or want that can be sold at auctions, online or private sales.</li>
			<li>Get a loan from your family and friends. In most cases people who borrow from family or friends do not pay as much interest on such loans. However, be aware of ill feeling that may be caused if you are unable to repay on time. See our guide on <a href="/finance-and-grants/finance-options/borrowing/financing-from-friends-and-family.php">financing from friends and family</a>.</li>
			<li>Seek finance from a low-cost fund managed by a local enterprise agency or community organisation.</li>
			<li>Borrow against future income by selling debts owed to you to a third party - see our guide on <a href="/finance-and-grants/manage-your-cashflow/factoring-and-invoice-discounting-the-basics.php">factoring and invoice discounting: the basics</a>.</li>
			<li>Get an overdraft or loan from your bank. Remember that the overdraft or loan will have to be repaid, and the interest rate may be high. See our guide on <a href="/finance-and-grants/finance-options/borrowing/bank-finance.php">bank finance</a>.</li>
			<li>Use leasing or hire purchase to fund fixed assets such as vehicles or equipment.</li>
			<li>Seek investment from external sources in return for a share in your business. See our guide on <a href="/finance-and-grants/finance-options/shares-and-equity-finance/equity-finance.php">equity finance</a>.</li>
			<li>Take on a second or part-time job - this will provide a useful source of income but it is important that it does not distract you from your priority of running your business.</li>
		</ul>
		<p><a href="/finance-and-grants/finance-options/choose-the-right-finance-options/identify-the-right-finance-options-for-your-business.php">Use our interactive tool to identify the right finance options for your business</a>.</p>
		
