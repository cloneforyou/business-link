		<h1>How do I survive until my business is off the ground?</h1>
		<h2>Make savings</h2>
		<p>There are ways you can save money on goods and services, both at home and in your business.</p>
		<h3>Making personal savings</h3>
		<p>You may be able to reduce the amount of money you pay out each month, simply by shopping around for services and loans. If you already have personal debts, for example on credit cards or personal loans, seek advice from an independent adviser or your bank as it may be possible to reduce your outgoings this way as well.</p>
		<p>There are many ways you can save money on essential goods and services. Many <strong>utility companies</strong> offer attractive deals when you change to a new supplier. Look carefully at exactly what is being offered - you may find, for example, that you can make savings if you receive both your gas and electricity from a single supplier.</p>
		<p>You can also try to reduce your everyday expenses. For example, you could sell your car and buy one that is cheaper to run or use public transport. You could also choose not to update personal equipment such as your laptop or mobile phone until it is absolutely necessary.</p>
		<h3>Making savings in your business</h3>
		<p>You could implement simple cost control systems across your whole business to identify scope for savings. You could cut unnecessary or excessive costs, for example, by not heating your premises at night or finding low price suppliers for goods or services. Adopting 'green' practices such as switching off computers when not in use can also save money as well as reducing your carbon footprint. Consider leasing goods or buying them second hand. Consider whether you can save money by running your business from home. See our guide on how to <a href="/premises-and-property/choosing-the-right-premises/use-your-home-as-a-workplace.php" title="Tax implications, financial and legal issues, security and setting up your work area if you plan to work from home">use your home as a workplace</a>.</p>
		
