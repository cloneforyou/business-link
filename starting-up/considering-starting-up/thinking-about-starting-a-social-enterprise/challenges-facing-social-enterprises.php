		<h1>Thinking about starting a social enterprise?</h1>
		<h2>Challenges facing social enterprises</h2>
		<p>Starting a social enterprise can be both exciting and rewarding, but it is also full of challenges. Social enterprises have to compete in the commercial market and face the same challenges and risks as all businesses.</p>
		<p>For your social enterprise to be successful, you need to work to a 'double or triple bottom line' - social and/or environmental and financial - and in competitive markets. This can be a challenge when competing against traditional businesses working to a purely financial bottom line.</p>
		<p>You will need to use your entrepreneurial drive to achieve social and/or environmental and financial aims without relying on grants to succeed. However, your independence will help to avoid excessive bureaucracy and allow you to change and innovate more quickly.</p>
		<p>If you are successful, you could do well financially and have an interesting and fulfilling business career.</p>
		
