		<h1>Thinking about starting a social enterprise?</h1>
		<h2>Social enterprise skills</h2>
		<p>As with any business, to run a successful social enterprise you need a wide variety of skills, so employing staff with business, financial, promotional and communication skills will give your company a strong, broad-ranging skills base.</p>
		<p>Typical skills needed to run a social enterprise include:</p>
		<ul>
			<li>Leadership - showing confidence while being energetic and likeable can motivate and inspire.</li>
			<li>Teamwork - all members of the team should be able to work together to achieve shared aims.</li>
			<li>Finances and administration - keeping good financial records, sticking to a budget and monitoring your cashflow to keep the business running.</li>
			<li>Communication - the ability to create new business opportunities can depend on effective communication skills. A social enterprise has to communicate clearly and effectively to people both in and outside of their team.</li>
			<li>Problem solving - finding flexible and innovative solutions to problems while making sure you stick to your social goals.</li>
			<li>Understanding - being aware of the social and/or environmental problems you are trying to solve through your social enterprise.</li>
			<li>Business skills &ndash; these can be gained by taking up short courses, attending workshops and seminars.</li>
		</ul>
		
