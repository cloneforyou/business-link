		<h1>Thinking about starting a social enterprise?</h1>
		<h2>What is a social enterprise?</h2>
		<p>Social enterprises are businesses driven by social or environmental purposes. As with all businesses they compete to deliver goods and services, but the difference is that their social purpose is at the heart of everything that they do and all profits that they make are reinvested towards achieving that social purpose.</p>
		<p>They are run under a range of legal forms. Some will be incorporated as companies and they may or may not also take charitable status. Other social enterprises will form as Industrial and Provident Societies. The Community Interest Company - a relatively new legal form - is a bespoke company form designed for social enterprises with a built-in asset lock.</p>
		<p>Social enterprises work across a wide range of industrial sectors, including growth sectors such as recycling, renewable energy and health and social care. The sector is diverse and can include:</p>
		<ul>
			<li>co-operatives - (not all co-operatives are social enterprises)</li>
			<li>credit unions</li>
			<li>housing associations</li>
			<li>community development trusts - their aim is to use ownership of buildings and land, trading for social purpose and self-help to bring about long-term economic, social and environmental benefits to their local areas</li>
			<li>social firms - their specific purpose is to create jobs for people who typically struggle to find employment</li>
			<li>community businesses</li>
		</ul>
		<p>Well-known social enterprises include the Big Issue, Jamie Oliver's restaurant, Fifteen and Divine chocolate.</p>
		
