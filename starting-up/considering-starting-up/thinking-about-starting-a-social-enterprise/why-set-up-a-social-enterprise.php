		<h1>Thinking about starting a social enterprise?</h1>
		<h2>Why set up a social enterprise?</h2>
		<p>If you are thinking about setting up a business but you want to achieve more than just financial returns, then the social enterprise model may be the most appropriate available.</p>
		<p>Social enterprises have led the way in using commercial business principles to promote the ethical trading of goods and services and achieve social and/or environmental aims. Although such organisations have traditionally been associated with the delivery of public services, many operate very successfully across a wide variety of commercial sectors.</p>
		<p>The commercial sectors in which social enterprises operate include furniture restoration, the supply of bottled water, chocolate and coffee production, the hospitality industry, graphic design, call centres and consultancy firms and many more.</p>
		<h3>Examples of social enterprises</h3>
		<p>Social enterprises are often formed to address a particular social or environmental need, such as in the provision of community transport for schoolchildren, the elderly and the disabled or in recycling services. For example, 'Women like Us' is a social enterprise that supports women to get into part-time, flexible employment.</a>
		</p>
		<p>Many social enterprises address a variety of needs at once, such as the Eden Project in Cornwall which educates visitors on global environmental issues while also regenerating and bringing tourism to the area.</p>
		<p>You may have identified a gap in local service provision or a failure of the commercial market to address a more widespread community problem.</p>
		<p>Consumers are increasingly demanding ethically-sourced and produced goods, whilst the reform of public services has led to a growing desire for greater choice, eg in the provision of health and social care. Central Surrey Health (CSH) is a social enterprise providing community nursing and therapy services in Surrey. CSH began life within the local primary care trust but is now an independent organisation that is co-owned by its employees. It exists to serve the local community which started life within a primary care trust, and now operates as a separate nursing and therapy body operating alongside the NHS.</p>
		<p>Social enterprises are well-placed to address these demands, using commercially viable business models to innovate and effect long-term social and environmental change. See the page in this guide on <a href="/starting-up/considering-starting-up/thinking-about-starting-a-social-enterprise/social-enterprises-and-the-public-sector.php">social enterprises and the public sector</a>.</p>
		
