		<h2>Here's how I decided I was ready to start up my business</h2>
		<p>Paul Kruzycki discovered an entrepreneurial streak while running a not-for-profit charity convention and he decided to set up his own venture. When he did, a cautious approach to the launch of his real-ale mail-order company, Ales By Mail, proved important in giving the business a sound start.</p>
		<p><strong>What I did</strong></p>
		<h3>Research the business proposition fully</h3>
		<p>"I continued working full time as a building surveyor while I developed my business plan, built contacts and got my idea to a stage where I felt I could get it off the ground. I wanted to prove fully to myself that the business could work.</p>
		<p>"I also knew that if I wanted to get finance to help me start up I would need to answer some tough questions such as who are your customers, where will you advertise and so on.</p>
		<p>"I spent a lot of time talking to people in the industry and finding information on websites - seeing how other people were doing things, at what level and how differently."</p>
		<h3>Take the opportunity to work part-time to provide a financial buffer</h3>
		<p>"My research showed that Ales by Mail couldn't pay me a salary at first so I decided I needed to save at least six months' living expenses. Then an opportunity to work as a property consultant came up.</p>
		<p>"I now do that two days a week and spend two days on Ales by Mail. On the fifth and sixth days I work on whatever needs doing most urgently.</p>
		<p>"I'm building the business on a tight budget and am going for slow growth. My consultancy work allows me to take this approach."</p>
		<h3>Consider domestic circumstances</h3>
		<p>"I spent 18 months preparing to start the business. There were things that needed to be resolved before I gave up my job. For example, I needed to wait until my house had been refurbished so that I could work from home.</p>
		<p>"I've had a lot of support from my wife. Knowing I can go to my family after a bad day and receive that support helps me remember that the decision I made was the right one."</p>
		<p><strong>What I'd do differently</strong></p>
		<h3>Think about the impact of routine paperwork</h3>
		<p>"Before starting up I'd find out more about the organisational aspects of running a business. I underestimated the amount of time needed to deal with tax matters, chase up clients and do all the other bits of paperwork."</p>
