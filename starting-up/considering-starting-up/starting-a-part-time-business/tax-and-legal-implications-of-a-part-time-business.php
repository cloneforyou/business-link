		<h1>Starting a part-time business</h1>
		<h2>Tax and legal implications of a part-time business</h2>
		<p>When you set up a part-time business you need to inform HM Revenue &amp; Customs (HMRC) and if it is a limited company you'll need to register it as well.</p>
		<p>You also have an obligation to maintain accurate financial records - see our guide on how to <a href="/tax-payroll-and-company-information/set-up-a-basic-record-keeping-system.php">set up a basic record-keeping system</a>.</p>
		<p>These requirements are the same whether you are starting a full or part-time business but there are other issues to consider which are specific to part-time businesses.</p>
		<h3>Continuing in paid work?</h3>
		<p>If you continue in paid work when you are not running your business, you have to decide whether to tell your employer. You should look at your contract of employment, as this may require you to tell your employer if you have another source of income. In addition, if your new venture is likely to compete with your employer, you have a conflict of interest which could cause problems.</p>
		<p>You must be very careful about separating your personal business activities from those of your employer. Taking phone calls, sending emails, and writing letters that relate to your business may constitute a breach of contract, unless you have permission to do so. You should also not make use of your employer's supplies, materials or intellectual property. For example, taking copies of proprietary software to use in your business is a criminal offence.</p>
		<p>If you discuss your plans with your employer, they could become a source of encouragement or work, especially if you are a valued employee.</p>
		<h3>Just running the business?</h3>
		<p>If you don't have other paid employment - eg you might be a parent or carer with family responsibilities - you still need to tell HMRC and, if appropriate, register with Companies House. If you were previously unwaged and had been receiving benefits, you will need to check whether your new venture affects that income, and report it to HMRC if necessary.</p>
		<p>If you are working from home, you need to take into account insurance, health and safety issues. You can read about this in our guide on <a href="/starting-up/choose-and-set-up-your-workplace/starting-a-business-from-home.php">starting a business from home</a>.</p>

