		<h1>Starting a part-time business</h1>
		<h2>What to consider when starting your part-time business</h2>
		<p>You should consider a number of issues when you start your own business, even if it is only on a part-time basis.</p>
		<h3>Are you ready to start up?</h3>
		<p>There are lots of reasons to consider starting a business but you need to be sure that you are ready. You'll need to assess your own skills and where you might need some extra development or support - for example, looking after the business' finances. You should also think carefully about the product or service that you want to sell, the audience you're selling to and what you have that makes you stand out from the crowd. You'll also have to be ready to face the challenges and pressures on yourself and your finances.</p>
		<h3>What legal structure?</h3>
		<p>You will need a formal legal structure for your business. This can take the form of anything from a sole trader to a limited company. When you have decided the structure, you will need to register with HM Revenue &amp; Customs and (if it is a limited company) with Companies House.</p>
		<h3>What about premises?</h3>
		<p>You may have decided to run your business from home but you need to be aware of the implications of this as there are tax, health, safety and security issues. You might find that separating your work area from your living area will maximise efficiency. For more information, see our guide on <a href="/starting-up/choose-and-set-up-your-workplace/starting-a-business-from-home.php">starting a business from home</a>.</p>
		<p>If you decide not to work from home, read our guide on how to <a href="/premises-and-property/choosing-the-right-premises/choose-the-right-premises-for-your-business.php">choose the right premises for your business</a>.</p>
		<h3>Money matters</h3>
		<p>Your business will start out small and you will still need to fund both the set-up and the development of your business to help it grow. Creative thinking and planning your finances might save you money and make it less costly than you think to start your own business. You will find help in our interactive guide on how to <a href="/finance-and-grants/finance-options/choose-the-right-finance-options/identify-the-right-finance-options-for-your-business.php">identify the right finance options for your business</a>.</p>
		<h3>Professional advice</h3>
		<p>Depending on the scale of your business, you may need professional advice or assistance, which can range from accountancy and bookkeeping through to marketing and IT support. You'll find more information in our section of guides on <a href="/starting-up/further-support-for-your-business.php">further support for your business</a>.</p>

