			<h1>Non-disclosure agreements</h1>
			<h2>What is a non-disclosure agreement?</h2>
			<p>A non-disclosure agreement (NDA), also known as a <strong>confidentiality</strong> <strong>agreement</strong>, is a low-cost way to protect your business' ideas.</p>
			<p>An NDA is a legal contract between you and another party. Typically, you agree to disclose information to them for a specific purpose, while they agree not to disclose that information to anyone else. This allows you to share your trade secrets with business partners while preventing them from passing this information on.</p>
			<p>For example, you may have come up with a product design and need to get an estimate from a manufacturer of how much it will cost to make. An NDA can ensure that your partner does not pass details of your idea to one of your competitors.</p>
			<p>As well as using NDAs with your clients, suppliers, resellers and advisers, you can also use these agreements with your employees and with journalists to whom you want to give a 'sneak preview' of financial, technical or new product information.</p>
			<p>NDAs can cover:</p>
			<ul>
				<li>trade secrets - eg a formula, programme or process</li>
				<li>technical drawings and designs</li>
				<li>mathematical and chemical formulae</li>
				<li>business plans</li>
				<li>customer and prospect lists</li>
			</ul>

			<p>Information qualifies as a <strong>trade secret</strong> or confidential when it is not known in the public domain and is valuable only as long as it remains secret or confidential.</p>
			<p>NDAs between you and public bodies need to be carefully considered because any information that you might provide may be disclosed under the terms of the Freedom of Information Act 2000. This gives anyone the right of access to information held by public authorities. <a href="http://webarchive.nationalarchives.gov.uk/20150603171202/https://www.justice.gov.uk/information-access-rights/foi-guidance-for-practitioners" target="_blank" rel="noopener noreferrer">Read about the Freedom of Information Act on the Ministry of Justice (MoJ) website</a>.</p>
			<p>If you need to approach potential business partners, have a clear idea beforehand of what you want to share with them - and stick to it. This minimises the risk of breaches of confidentiality.</p>
			
