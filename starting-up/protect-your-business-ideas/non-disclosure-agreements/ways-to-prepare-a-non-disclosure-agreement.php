			<h1>Non-disclosure agreements</h1>
			<h2>Ways to prepare a non-disclosure agreement</h2>
			<p>Non-disclosure agreement (NDA) templates can be bought <strong>off the shelf</strong>, allowing you to adjust them for your needs. This is inexpensive and the template can be reused.</p>
			<p>You can:</p>
			<ul>
				<li><a href="https://www.compactlaw.co.uk/confidentiality-agreement.html" target="_blank" rel="noopener noreferrer">Buy an NDA from the CompactLaw website</a>. There are many other websites that offer similar services.</li>
				<li><a href="/_files/ipo_non-disclosure-agreements.pdf" target="_blank" rel="noopener noreferrer">Download an example of an agreement from the Intellectual Property Office website (PDF, 149K)</a>.</li>
			</ul>
			
			<p>If you are dealing with a public body that is covered by the Freedom of Information Act 2000, you can <a href="http://webarchive.nationalarchives.gov.uk/20150603171202/https://www.justice.gov.uk/information-access-rights/foi-guidance-for-practitioners" target="_blank" rel="noopener noreferrer">read more about the Freedom of Information Act on the Ministry of Justice website</a>.</p>
			<p>If an NDA is flawed, eg the definitions are not specific, it won't provide enough protection for you or your business. You should consider asking a lawyer to draw up a bespoke confidentiality agreement for added protection.</p>
			<p><a href="http://solicitors.lawsociety.org.uk/" target="_blank" rel="noopener noreferrer">Find a solicitor on the Law Society website</a>.</p>
			
