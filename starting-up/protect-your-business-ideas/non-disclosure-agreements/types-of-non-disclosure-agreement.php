			<h1>Non-disclosure agreements</h1>
			<h2>Types of non-disclosure agreement</h2>
			<p>A non-disclosure agreement (NDA) is a <strong>legal contract</strong> between you and another party not to disclose information you have shared for a specific purpose.</p>
			<p>This allows businesses to approach potential partners, suppliers, consultants and customers with their idea, knowing that the other party is legally forbidden to pass on that information. These agreements can be <strong>tailored</strong> to your requirements.</p>
			<p>They can be used to:</p>
			<ul>
				<li>share intellectual property</li>
				<li>share commercial or trading information</li>
				<li>formalise a relationship, eg between an employer and employee</li>
			</ul>

			<h3>NDAs for employees</h3>
			<p>Current and former employees are responsible for most breaches of confidentiality. Employees are under an <strong>implied duty</strong> not to use trade secrets in a manner that will harm your business, but it is best to get this in writing and to specify to the employee exactly what is confidential. Equally, you should ensure that the NDA makes provision for when the employee leaves your business.</p>
			
			<h3>Other types of NDAs</h3>
			<ul>
				<li>
					A <strong>precedent confidentiality agreement</strong> is used for mergers and acquisitions. If a company wants to buy your business, it will want to look at your books and assets. An NDA can provide an element of protection for your confidential information and trade secrets if the prospective buyer pulls out of the deal.
				</li>
				<li>If you don't know exactly what information you will need to disclose during a <strong>commercial relationship</strong>, you can still use an NDA. It is a good idea to classify as confidential any information that will be disclosed later, so that the NDA still applies.</li>
			</ul>

			<h3>One-way and two-way NDAs</h3>
			<ul>
				<li>A <strong>one-way</strong> NDA is used when only one business is sharing information and the other agrees to keep it confidential.</li>
				<li>A <strong>two-way</strong> NDA is used when both businesses are sharing confidential information with each other and want to be sure that neither will disclose their trade secrets.</li>
			</ul>
			
