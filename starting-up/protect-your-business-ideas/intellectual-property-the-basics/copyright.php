			<h1>Intellectual property: the basics</h1>
			<h2>Copyright</h2>
			<p>When you create a literary, dramatic, musical or artistic piece of work, you will be granted copyright automatically. If someone you employ creates this for you during their normal course of employment, then you will own the copyright unless an agreement has been signed stipulating otherwise.</p>
			<p>Copyright covers works such as company documentation, software, songs, original paintings, drawings or photographs. Copyright also protects sounds recordings and films.</p>
			<p>If you own a copyright, you can decide whether to:</p>
			<ul>
				<li>allow other businesses or people to use the copyrighted work</li>
				<li>allow work to be copied, adapted, published, performed or broadcast</li>
				<li>allow other businesses to use work for a royalty or licence fee</li>
				<li>sell the copyright</li>
			</ul>

			<p>For information on copyright protection and what to do if you think there has been a copyright infringement, see our guide on <a href="/create-innovate-and-protect/protecting-your-intellectual-property/copyright-design-and-protecting-your-ideas/copyright-for-your-business.php">copyright for your business</a>.</p>
			
