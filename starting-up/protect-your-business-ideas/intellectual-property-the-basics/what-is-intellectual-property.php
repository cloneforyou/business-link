			<h1>Intellectual property: the basics</h1>
			<h2>What is intellectual property?</h2>
			<p>Intellectual property lets people own the work they create. It can be any of the following:</p>
			<ul>
				<li>brands and logos</li>
				<li>inventions</li>
				<li>software</li>
				<li>designs</li>
				<li>music</li>
				<li>books</li>
				<li>poems</li>
				<li>paintings</li>
				<li>photography</li>
				<li>other kinds of creative work</li>
			</ul>

			<p>Intellectual property can be very valuable, and there are businesses, such as computer games companies, that exist simply to develop intellectual property or even take advantage of it.</p>
			<p>It is important to protect your business by securing your intellectual property rights. It is equally important to avoid infringing the rights of other businesses.</p>
			<p>For more information, see the following guides on this site:</p>
