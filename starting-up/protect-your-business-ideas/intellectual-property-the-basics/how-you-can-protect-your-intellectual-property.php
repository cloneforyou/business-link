			<h1>Intellectual property: the basics</h1>
			<h2>How you can protect your intellectual property</h2>
			<p>The way you can protect your intellectual property depends on what exactly it is:</p>
			<ul>
				<li><strong>Copyright</strong> is granted to the person or organisation that creates published artistic work, which includes writing, film music and computer software. Unlike most other forms of intellectual property, copyright is granted automatically when the work is first published. For more information, see our guide on <a href="/create-innovate-and-protect/protecting-your-intellectual-property/copyright-design-and-protecting-your-ideas/copyright-for-your-business.php">copyright for your business</a>.</li>
				<li><strong>Patents</strong> protect inventions, including the features and processes that make things work. For more information, see our guide on how to <a href="/create-innovate-and-protect/protecting-your-intellectual-property/patent-protection-and-your-business/get-patent-protection-for-your-business.php">get patent protection for your business</a>.</li>
				<li><strong>Trade marks</strong> are symbols that differentiate between goods and services and can be logos or brand names. For more information, see our guide on how to <a href="/create-innovate-and-protect/protecting-your-intellectual-property/trade-marks-protecting-your-brand/use-trade-marks-in-your-business.php">use trade marks in your business</a>.</li>
				<li><strong>Designs</strong> can be protected by registering them, and through design right. For more information, see our guide on <a href="/create-innovate-and-protect/protecting-your-intellectual-property/copyright-design-and-protecting-your-ideas/design-right-and-registration.php">design right and registration</a>.</li>
				<li><strong>Digital intellectual property</strong> can be protected in other ways than using the law, such as encryption and using digital signatures. For more information, see our guide to <a href="/create-innovate-and-protect/protecting-your-intellectual-property/find-value-you-didnt-know-existed-in-your-business/digital-intellectual-property-and-your-business.php">digital intellectual property and your business</a>.</li>
			</ul>

