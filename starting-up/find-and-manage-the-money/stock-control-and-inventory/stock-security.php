		<h1>Stock control and inventory</h1>
		<h2>Stock security</h2>
		<p>Keeping stock secure depends on knowing what you have, where it is located and how much it is worth - so good records are essential. Stock that is portable, does not feature the business' logo, or is easy to sell on, is at particular risk.</p>
		<h3>Thieves and shoplifters</h3>
		<p>A thief coming in from outside is an obvious threat. Check the security around your premises to keep the risk to a minimum. In a shop, thieves may steal in groups - some providing a distraction while others take goods. Teach your staff to be alert and to recognise behaviour like this. Set up a clear policy and make sure staff are trained in dealing with thieves.</p>
		<p>Offering to help a customer if you are suspicious will often prevent a theft. Avoid using confrontational words like 'steal' if you do have to approach a suspected thief, and avoid getting into a dangerous situation.</p>
		<h3>Protect your stock</h3>
		<ul>
			<li>Identify and mark expensive portable equipment (such as computers). If possible, fit valuable stock with security tags - such as Radio Frequency Identification tags - which will sound an alarm if they are moved.</li>
			<li>Don't leave equipment hanging around after delivery. Put it away in a secure place, record it and clear up packaging. It is a good idea to dispose of packaging securely - leaving boxes in view could be an advertisement to thieves.</li>
			<li>Carry out regular stocktakes.</li>
			<li>Put CCTV in car parks and other key locations.</li>
		</ul>

		<p><a href="http://www.met.police.uk/business/index.htm" target="_blank" rel="noopener noreferrer">Read crime prevention advice for businesses on the Metropolitan Police website</a>.</p>
		
		<h3>Theft by staff</h3>
		<p>Theft by employees can sometimes be a problem. To prevent this:</p>
		<ul>
			<li>Train staff about your security systems and your disciplinary policies and procedures. Training about the cost of stock theft will help, as many people aren't aware of the implications for company turnover and job security.</li>
			<li>Set up procedures to prevent theft. Staff with financial responsibilities should not be in charge of stock records.</li>
			<li>Restrict access to warehouses, stockrooms and stationery cupboards.</li>
			<li>Regularly change staff controlling stock to avoid collusion or bad practice.</li>
		</ul>

