		<h1>Stock control and inventory</h1>
		<h2>Stock control administration</h2>
		<p>There are many administrative tasks associated with stock control. Depending on the size and complexity of your business, they may be done as part of an administrator's duties, or by a dedicated <strong>stock controller</strong>.</p>
		<p>For security reasons, it's good practice to have different staff responsible for finance than those responsible for stock.</p>
		<p>Typical paperwork to be processed includes:</p>
		<ul>
			<li>delivery and supplier notes for incoming goods</li>
			<li>purchase orders, receipts and credit notes</li>
			<li>returns notes</li>
			<li>requisitions and issue notes for outgoing goods</li>
		</ul>

		<p>Stock can tie up a large slice of your business capital, so accurate information about stock levels and values is essential for your company's accounting.</p>
		<p>Figures should be checked systematically, either through a regular audit of stock - <strong>stocktaking</strong> - or an ongoing programme of checking stock - <strong>rolling stocktake</strong>.</p>
		<p>If the figures don't add up, you need to investigate as there could be stock security problems or a failure in the system.</p>
		<h3>Health and safety</h3>
		<p>Health and safety aspects of stock control are related to the nature of the stock itself. Issues such as where and how items are stored, how they are moved and who moves them might be significant - depending on what they are.</p>
		<p>You might have hazardous materials on your premises, goods that deteriorate with time or items that are very heavy or awkward to move.</p>
		<p>Find more information in our guide on <a href="/workplace-health-and-safety/safer-ways-of-working/storing-goods-and-materials.php">storing goods and materials</a>.</p>
		
