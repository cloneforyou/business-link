		<h1>Here's how I control my stock</h1>
		<p>Andrea Jones is managing director of Liversedge-based Systems (Telecoms) Limited, a business specialising in the next-day delivery of refurbished telecommunications equipment. </p>
		<h2>What I did</h2>
		<h3>Invest in a computerised system</h3>
		<p>"We bought our computer system with its accounting software when we were turning over &pound;500,000 a year. It cost a lot of money but I got it on a five-year lease and I only paid &pound;50 a week. I couldn't have got anybody to do the stock work for &pound;50 a week.</p>
		<p>"I chose this system because I wanted something that integrated all my accounting functions - my stock control, my buying &hellip; basically, to have everything under one roof, as it were. And, importantly, I wanted barcoding. I did a lot of phoning around software companies before making my choice.</p>
		<p>"As an item comes in it gets barcoded and then it's logged on to the system under a purchase order with the serial number, stock code and details of the product. The product then goes down to our test room for refurbishing.</p>
		<p>"The system can tell you whether any item has been tested or not and exactly where it's located in the warehouse."</p>
		<h3>Hold enough equipment to fulfil customer orders</h3>
		<p>"For the refurbished products side of my business there's no such thing as having too much stock. We can't ask people to wait a week if they want something so I buy anything that comes up for sale if it's cheap enough and I know it will move. I wouldn't buy stuff if there was no demand for it but that doesn't happen. I know my market very well.</p>
		<p>"We sell new installation products too and we re-stock that on a demand-led basis. We set minimum and maximum stock levels on the computerised accounting system and when the stock hits the minimum level the screen lights up telling me to reorder. But I prefer to print a report on stock levels every Friday. That tells me what we're low on and I always top up to the maximum level."</p>
		<h3>Reorder consumables only when necessary</h3>
		<p>"We use lots of paper, lever arch files, pens and ink cartridges. Everything is kept on bookshelves and I can see instantly what's in and what we're running low on. I do a stationery order every couple of weeks or so - though I try to time it with when there are special offers in the stationery catalogue I use."</p>
		<h2>What I'd do differently</h2>
		<h3>Install a computer connection from work to my home earlier than I did</h3>
		<p>"When I get home I can click on two buttons and I'm basically sitting in my office. This means I can keep an eye on my stock at all times and I do a lot of bidding for products from home, for example on eBay. I can also complete purchase orders from home."</p>
