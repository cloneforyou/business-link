		<h1>Stock control and inventory</h1>
		<h2>Types of stock</h2>
		<p>Everything you use to make your products, provide your services and to run your business is part of your stock.</p>
		<p>There are four main types of stock:</p>
		<ul>
			<li>raw materials and components - ready to use in production</li>
			<li>work in progress - stocks of unfinished goods in production</li>
			<li>finished goods ready for sale</li>
			<li>consumables - for example, fuel and stationery</li>
		</ul>
		<p>The type of stock can influence how much you should keep - see the page in this guide on <a href="/starting-up/find-and-manage-the-money/stock-control-and-inventory/how-much-stock-should-you-keep.php">how much stock should you keep?</a></p>
		<h3>Stock value</h3>
		<p>You can categorise stock further, according to its value. For example, you could put items into low, medium and high value categories. If your stock levels are limited by capital, this will help you to plan expenditure on new and replacement stock.</p>
		<p>You may choose to concentrate resources on the areas of greatest value. However, low-cost items can be crucial to your production process and should not be overlooked.</p>
		
