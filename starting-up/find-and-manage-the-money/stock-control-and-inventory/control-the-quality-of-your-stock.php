		<h1>Stock control and inventory</h1>
		<h2>Control the quality of your stock</h2>
		<p>Quality control is a vital aspect of stock control - especially as it may affect the safety of customers or the quality of the finished product.</p>
		<p>Efficient stock control should incorporate <strong>stock tracking</strong> and <strong>batch tracking</strong>. This means being able to trace a particular item backwards or forwards from source to finished product, and identifying the other items in the batch.</p>
		<p>Goods should be checked systematically for quality, faults identified and the affected batch weeded out. This will allow you to raise any problems with your supplier and at the same time demonstrate the safety and quality of your product.</p>
		<p>With a good computerised stock control system, this kind of tracking is relatively straightforward. Manual stock control methods can also use <strong>codes</strong> to systematise tracking and make it easier to trace particular batches.</p>
		<p>Radio Frequency Identification (RFID) can be used to store information about a product or component's manufacturing date, to ensure that it is sold or processed in time. The system can also be used to trace faulty products quickly and efficiently. See the page in this guide on <a href="/starting-up/find-and-manage-the-money/stock-control-and-inventory/using-rfid-for-inventory-control-stock-security-and-quality-management.php">using RFID for inventory control, stock security and quality management</a>.</p>
		<p>The British Standards Institution (BSI) has a scheme to certify businesses that have achieved a certain standard of quality management. Achieving the standard is one way of showing customers and regulators that you take quality control seriously.</p>
		<p>The details vary according to the particular business sector. For some industries, it is a legal requirement to be certified - <a href="https://www.bsigroup.com/en-GB/iso-9001-quality-management/" target="_blank" rel="noopener noreferrer">view the guide to quality control on the BSI website</a>.</p>
		
