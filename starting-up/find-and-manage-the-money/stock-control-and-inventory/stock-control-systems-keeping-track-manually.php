		<h1>Stock control and inventory</h1>
		<h2>Stock control systems - keeping track manually</h2>
		<p>Stocktaking involves making an <strong>inventory</strong>, or list, of stock, and noting its location and value. It's often an annual exercise - a kind of audit to work out the value of the stock as part of the accounting process.</p>
		<p><strong>Codes</strong>, including barcodes, can make the whole process much easier but it can still be quite time consuming. Checking stock more frequently - a rolling stocktake - avoids a massive annual exercise, but demands constant attention throughout the year. Radio Frequency Identification (RFID) tagging using hand-held readers can offer a simple and efficient way to maintain a continuous check on inventory. See the page in this guide on <a href="/starting-up/find-and-manage-the-money/stock-control-and-inventory/using-rfid-for-inventory-control-stock-security-and-quality-management.php">using RFID for inventory control, stock security and quality management</a>.</p>
		<p>Any stock control system must enable you to:</p>
		<ul>
			<li>track stock levels</li>
			<li>make orders</li>
			<li>issue stock</li>
		</ul>

		<p>The simplest manual system is the <strong>stock book</strong>, which suits small businesses with few stock items. It enables you to keep a log of stock received and stock issued.</p>
		<p>It can be used alongside a simple <strong>reorder system</strong>. For example, the two-bin system works by having two containers of stock items. When one is empty, it's time to start using the second bin and order more stock to fill up the empty one.</p>
		<p><strong>Stock cards</strong> are used for more complex systems. Each type of stock has an associated card, with information such as:</p>
		<ul>
			<li>description</li>
			<li>value</li>
			<li>location</li>
			<li>reorder levels, quantities and lead times (if this method is used)</li>
			<li>supplier details</li>
			<li>information about past stock history</li>
		</ul>

		<p>More sophisticated manual systems incorporate <strong>coding</strong> to classify items. Codes might indicate the value of the stock, its location and which batch it is from, which is useful for quality control.</p>
		
