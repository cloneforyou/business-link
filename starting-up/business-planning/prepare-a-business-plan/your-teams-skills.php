		<h1>Prepare a business plan</h1>
		<h2>Your team's skills</h2>
		<p>Your business plan needs to set out your own background and skills and the structure and key skills of both your management team and your staff. It should identify the strengths in your team and your plans to deal with any obvious weaknesses.</p>
		<h3>The management team</h3>
		<p>If you're looking for external funding, your management team can be a decisive factor. Explain who is involved, their role and how it fits into the organisation. Include a CV or paragraph on each individual, outlining their background, relevant experience and qualifications. Include any advisers you might have such as accountants or lawyers.</p>
		<p>If you're looking to satisfy your bank manager or other investors, you need to demonstrate that your management team has the right balance of skills, drive and experience to enable your business to succeed. Key skills include sales, marketing and financial management as well as production, operational and market experience.</p>
		<p>Your investors will also want to be convinced that you and your team are fully committed. Therefore it's a good idea to set out how much time and money each person will contribute - or has already contributed - to the business and the salaries and benefits you plan to draw. You can find more practical tips in our guide on how to <a href="/finance-and-grants/prepare-to-raise-finance/how-to-attract-investment/use-your-business-plan-to-get-funding.php">use your business plan to get funding</a>.</p>
		<h3>Your people</h3>
		<p>Give details of your workforce in terms of total numbers and by department. Spell out what work you plan to do internally and if you plan to outsource any work. Other useful figures might be sales or profit per employee, average salaries, employee retention rates and productivity.</p>
		<p>Your plan should also outline any recruitment or training plans, including timescales and costs.</p>
		<p>It's vital to be realistic about the commitment and motivation of your people and spell out any plans to improve or maintain staff morale.</p>
		<p>See our videos and e-learning resources to <a href="https://www.gov.uk/write-business-plan" title="What you need to include in your business plan and how to create one step-by-step" target="_blank" rel="noopener noreferrer">create your business plan</a>.</p>
		
