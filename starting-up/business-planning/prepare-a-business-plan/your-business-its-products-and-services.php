		<h1>Prepare a business plan</h1>
		<h2>Your business, its products and services</h2>
		<p>You must be able to clearly describe what your business does, whether you are writing the business plan for your own purposes or if you want other people to provide funds through investments or loans.</p>
		<p>This part of the plan sets out your <strong>vision</strong> for your new business and includes who you are, what you do, what you have to offer and the market you want to address.</p>
		<p>Start with an overview of your business:</p>
		<ul>
			<li>when you started or intend to start trading and the progress and investment you have made to date</li>
			<li>the type of business and the sector it is in</li>
			<li>any relevant history - for example, if you acquired the business, who owned it originally and what they achieved with it</li>
			<li>the current legal structure</li>
			<li>your vision for the future</li>
		</ul>
		<p>Then describe your products or services as simply as possible, defining:</p>
		<ul>
			<li>what makes it different</li>
			<li>what benefits it offers</li>
			<li>why customers would buy it from you instead of your competitors</li>
			<li>how you plan to develop your products or services</li>
			<li>whether you hold any patents, trade marks or design registration</li>
			<li>the key features and success factors of your industry or sector</li>
		</ul>
		<p>Remember that the person reading the plan may not understand your business and its products, services or processes as well as you do, so try to avoid jargon. It's a good idea to get someone who isn't involved in the business - a friend or family member perhaps - to read this section of your plan and make sure they can understand it.</p>
