		<h1>Prepare a business plan</h1>
		<h2>Your markets and competitors</h2>
		<p>In this section you should define your market, your position in it and outline who your competitors are. In order to do this you should refer to any market research you have carried out. You need to demonstrate that you're fully aware of the marketplace you're planning to operate in and that you understand any important trends and drivers.</p>
		<p>You should also be able to show that your business will be able to attract customers in a growing market despite the competition.</p>
		<h3>Key areas to cover include:</h3>
		<ul>
			<li><strong>your market</strong> - its size, historical data about its development and key current issues</li>
			<li><strong>your target customer base</strong> - who they are and how you know they will be interested in your products or services</li>
			<li><strong>your competitors</strong> - who they are, how they work and the share of the market they hold</li>
			<li><strong>the future</strong> - anticipated changes in the market and how you expect your business and your competitors to react to them</li>
		</ul>
		<p>For further information, see our guides on <a href="/sales-and-marketing/marketing/market-research-and-market-reports.php">market research and market reports</a> and how to <a href="/sales-and-marketing/marketing/understand-your-competitors.php">understand your competitors</a>.</p>
		<p>It is important to know your competitors' strengths and weaknesses as compared to your own - and it is good practice to do a competitor analysis of each one. Remember that the market is not static - your customers' needs and your competitors can change. So, as well as showing the competitor analyses you have undertaken, you should also demonstrate that you have considered and drawn up contingency plans to cover alternative scenarios.</p>
