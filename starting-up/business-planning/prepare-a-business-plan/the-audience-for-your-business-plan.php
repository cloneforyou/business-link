		<h1>Prepare a business plan</h1>
		<h2>The audience for your business plan</h2>
		<p>There are many benefits to creating and managing a realistic business plan. Even if you just use it in-house, it can:</p>
		<ul>
			<li>help you spot potential pitfalls before they happen</li>
			<li>structure the financial side of your business efficiently</li>
			<li>focus your efforts on developing your business</li>
			<li>work as a measure of your success</li>
		</ul>
		<p>Many people think of a business plan as a document used to secure external funding. This is important because potential investors, including banks, may invest in your idea, work with you or lend you money as a result of the strength of your plan.</p>
		<p>The following people or institutions may request to see your business plan at some stage:</p>
		<ul>
			<li>banks</li>
			<li>external investors - whether this is a friend, a venture capitalist firm or a business angel</li>
			<li>grant providers</li>
			<li>anyone interested in buying your business</li>
			<li>potential partners</li>
		</ul>
		<p>You should also bear in mind that a business plan is a living document that will help you monitor your performance and stay on track and will therefore need updating and changing as your business grows. Regardless of whether you intend to use your plan internally, or as a document for external people, it should still take an objective and honest look at your business. Failing to do this could mean that you and others have unrealistic expectations of what can be achieved and when.</p>
		<p>See our videos and e-learning resources to <a href="https://www.gov.uk/write-business-plan" target="_blank" rel="noopener noreferrer">create your business plan</a>.</p>
		
