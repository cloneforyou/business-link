		<h1>Prepare a business plan</h1>
		<h2>The executive summary</h2>
		<p>The executive summary is often the most important part of your business plan. Positioned at the front of the document, it is the first part to be read. However, as a summary it makes sense to write it last.</p>
		<p>It may be the only part that will be read. Faced with a large pile of funding requests, venture capitalists and banks have been known to separate business plans into 'worth considering' and 'discard' piles based on this section alone.</p>
		<h3>What is it?</h3>
		<p>The executive summary is a synopsis of the <strong>key points</strong> of your entire plan. It should include highlights from each section of the rest of the document - from the key features of the business opportunity through to the elements of the financial forecasts.</p>
		<p>Its purpose is to explain the basics of your business in a way that both informs and interests the reader. If, after reading the executive summary, an investor or manager understands what the business is about and is keen to know more, it has done its job.</p>
		<p>It should be concise - no longer than two pages at most - and interesting. It's advisable to write this section of your plan after you have completed the rest.</p>
		<h3>What is it not?</h3>
		<ul>
			<li>A brief description of the business and its products. It's a synopsis of the entire plan.</li>
			<li>An extended table of contents. This makes for very dull reading. You should ensure it shows the highlights of the plan, rather than restating the details the plan contains.</li>
			<li>Hype. While the executive summary should excite the reader enough to read the entire plan, an experienced investor or business person will recognise hype and this will undermine the plan's credibility.</li>
		</ul>
