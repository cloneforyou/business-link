		<h1>Prepare a business plan</h1>
		<h2>What a business plan should include</h2>
		<p>Your business plan should provide details of how you are going to develop your business, when you are going to do it, who's going to play a part and how you will manage the finances.</p>
		<p>Clarity on these issues is particularly important if you're looking for finance or investment. The process of building your plan will also focus your mind on how your new business will need to operate to give it the best chance of success.</p>
		<p>Your plan should include:</p>
		<ul>
			<li><strong>An executive summary</strong> - this is an overview of the business you want to start. It's vital. Many lenders and investors make judgments about your business based on this section of the plan alone. See the page in this guide on <a href="/starting-up/business-planning/prepare-a-business-plan/the-executive-summary.php" title="The initial and potentially most important part of your plan, outlining the highlights of your business idea">the executive summary</a>.</li>
			<li><strong>A short description of the business opportunity</strong> - who you are, what you plan to sell or offer, why and to whom. See the page in this guide on <a href="/starting-up/business-planning/prepare-a-business-plan/your-business-its-products-and-services.php" title="Outlining what your company does, what you can offer, the market needs you address and why your idea is viable">your business, its products and services</a>.</li>
			<li><strong>Your marketing and sales strategy</strong> - why you think people will buy what you want to sell and how you plan to sell to them. See the pages in this guide on <a href="/starting-up/business-planning/prepare-a-business-plan/your-markets-and-competitors.php" title="Define your market, identify your target customers and main competitors and look to the future to anticipate change">your markets and competitors</a> and <a href="/starting-up/business-planning/prepare-a-business-plan/marketing-and-sales.php" title="Sectors covering price, customers, your sales channels, distribution methods, promotional strategy and sales plan">marketing and sales</a>.</li>
			<li><strong>Your management team and personnel</strong> - your credentials and the people you plan to recruit to work with you. See the page in this guide on <a href="/starting-up/business-planning/prepare-a-business-plan/your-teams-skills.php" title="An analysis of your workforce - the structure, key skills of your team, training and recruitment plans">your team's skills</a>.</li>
			<li><strong>Your operations</strong> - your premises, production facilities, your management information systems and IT. See the page in this guide on <a href="/starting-up/business-planning/prepare-a-business-plan/your-operations.php" title="Information to include about how you will source, procure, create and deliver the goods or services you will be selling">your operations</a>.</li>
			<li><strong>Financial forecasts</strong> - this section translates everything you have said in the previous sections into numbers. See the page in this guide on <a href="/starting-up/business-planning/prepare-a-business-plan/financial-forecasts.php" title="Putting your business into numbers with sales, cashflow and profit and loss forecasts and reviewing possible risks">financial forecasts</a>.</li>
		</ul>
