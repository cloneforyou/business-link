		<h1>Prepare a business plan</h1>
		<h2>Your operations</h2>
		<p>Your business plan also needs to outline your operational capabilities and any planned improvements. There are certain areas you should focus on.</p>
		<h3>Location</h3>
		<ul>
			<li>Do you have any business property?</li>
			<li>What are your long-term commitments to the property?</li>
			<li>Do you own or rent it?</li>
			<li>What are the advantages and disadvantages of your current location?</li>
		</ul>
		<h3>Producing your goods and services</h3>
		<ul>
			<li>Do you need your own production facilities or would it be cheaper to outsource any manufacturing processes?</li>
			<li>If you do have your own facilities, how modern are they?</li>
			<li>What is the capacity compared with existing and forecasted demand?</li>
			<li>Will any investment be needed?</li>
			<li>Who will be your suppliers?</li>
		</ul>
		<h3>Management-information systems</h3>
		<ul>
			<li>Have you got established procedures for stock control, management accounts and quality control?</li>
			<li>Can they cope with any proposed expansion?</li>
		</ul>
		<p>For more information, see our guides on <a href="/starting-up/find-and-manage-the-money/stock-control-and-inventory.php">stock control and inventory</a> and <a href="/finance-and-grants/financial-planning-and-accounts/financial-and-management-accounts-the-basics.php">financial and management accounts: the basics</a>.</p>
		<h3>Information technology</h3>
		<ul>
			<li>IT is a key factor in most businesses, so include your strengths and weaknesses in this area.</li>
			<li>Outline the reliability and the planned development of your systems.</li>
		</ul>
		<p>For more information, see our guide providing <a href="/it-and-ecommerce/introduction-to-it/an-overview-of-it-and-e-commerce.php">an overview of IT and e-commerce</a>.</p>
