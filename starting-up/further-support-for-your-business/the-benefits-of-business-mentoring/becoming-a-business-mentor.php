			<h1>The benefits of business mentoring</h1>
			<h2>Becoming a business mentor</h2>
			<p>If you already have plenty of business experience, you could consider offering your services as a mentor to other businesses. This can be a very rewarding way of passing on the benefits of your experience.</p>
			<p>As a mentor, your role is to support, develop, stimulate and challenge your mentee, whether they're just starting up in business or are already established and seeking guidance.</p>
			<p>You can mentor in a variety of ways - including online, face to face, by telephone, or as part of a discussion group. You can charge a fee for your services or offer free support.</p>
			
			<h3>The Get Mentoring initiative</h3>
			<p>Get Mentoring aims to increase the number of volunteer business mentors in the UK by recruiting and training 15,000 volunteer mentors from the small, medium and micro business community. The initiative is led by the Small Firms Enterprise Development Initiative.</p>
			<p>A free training course - Introduction to Enterprise Mentoring - will equip you with the skills and knowledge you need to become an effective mentor.</p>
			<p><a href="http://getmentoring.org/" target="_blank" rel="noopener noreferrer">Register for free business mentor training on the Get Mentoring website</a>. </p>
			
			<h3>Mentorsme.co.uk</h3>
			<p>Mentorsme.co.uk is the national web portal that allows businesses to access mentoring services. Once you're registered as a mentor, mentees will be able to find your details on the Mentorsme.co.uk website.</p>
			<p><a href="http://www.mentorsme.co.uk/" target="_blank" rel="noopener noreferrer">Find out about becoming a business mentor on the Mentorsme.co.uk website</a>.</p>
			
			<h3>What you'll need to become a business mentor</h3>
			<p>To mentor other business people effectively you need the right skills and experience. You should have:</p>
			<ul>
				<li>the ability to listen and empathise</li>
				<li>respect for other people's views</li>
				<li>experience of the problems faced by small businesses</li>
				<li>the ability to offer encouragement and sympathy</li>
				<li>a supportive, hands-off approach that allows others to make their own decisions</li>
				<li>a willingness to volunteer your time and skills</li>
			</ul>
			
			<p>You should also have a good, broad understanding of business. Many small businesses look for mentors who have expertise in particular areas or functions, such as:</p>
			<ul>
				<li>finance</li>
				<li>sales and marketing</li>
				<li>public relations</li>
				<li>strategy and planning</li>
				<li>negotiation</li>
				<li>operations</li>
			</ul>
			
