			<h1>The benefits of business mentoring</h1>
			<h2>Face-to-face business mentoring</h2>
			<p>If you choose to get involved in face-to-face business mentoring, mentor and mentee need to agree on:</p>
			<ul>
				<li>the degree of involvement you want</li>
				<li>the level of structure you want</li>
			</ul>

			<h3>Degrees of involvement</h3>
			<p>You will need to agree on the degree of involvement that suits you both. Some mentors and mentees work extremely closely with each other, speaking or meeting most weeks or even most days. In most cases, however, a mentor will not have day-to-day involvement with the business. Instead, they'll provide help every few weeks or months which could take the form of phone calls, emails, meetings, etc.</p>
			
			<h3>Level of structure</h3>
			<p>You should also agree on a level of structure to suit you both. Mentoring can be a very formal and structured process with regular meetings which follow a specific agenda and set of goals. It can also be quite a casual arrangement, where the mentee calls on the mentor as and when problems or questions arise.</p>
			
			<h3>Finding a face-to-face business mentor</h3>
			<p>Mentorsme.co.uk - the national mentoring network - can help you find mentors offering face-to-face support. This new government-backed service is funded by the British Bankers' Association and supported by the Better Business Finance Roundtable, which includes the key business groups in the UK.</p>
			<p><a href="http://www.mentorsme.co.uk/" target="_blank" rel="noopener noreferrer">Find business mentors on the Mentorsme.co.uk website</a>.</p>
			<p>For more about these and other mentoring services, see the page in this guide on <a href="/starting-up/further-support-for-your-business/the-benefits-of-business-mentoring/further-information-for-finding-or-becoming-a-business-mentor.php">further information for finding or becoming a business mentor</a>.</p>
			
