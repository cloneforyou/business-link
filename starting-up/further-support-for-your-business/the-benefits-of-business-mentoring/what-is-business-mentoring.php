			<h1>The benefits of business mentoring</h1>
			<h2>What is business mentoring?</h2>
			<p>Business mentoring is a relationship between you - as an entrepreneur and someone with business experience who is willing to act as a guide. The business mentor offers advice, guidance and support to help you run and improve your business.</p>
			<p>It can involve face-to-face meetings or online discussions - or a combination of both - depending on which arrangement is best for both parties.</p>
			
			<h3>Formal and informal mentoring</h3>
			<p>It's likely that your mentor will stay in regular contact with you through meetings, telephone calls and emails. The nature of the relationship may vary from being fairly casual - eg a monthly telephone call or informal visit - to being more formal and structured - eg setting meeting agendas and business objectives. Some mentoring services are free and others are paid for or available by subscription to an organisation.</p>
			<p>Not all mentors have to be formally established as such. Friends, family and business contacts can all become involved in unofficial mentoring.</p>
			
			<h3>How to find a mentor</h3>
			<p>There are several organisations that make mentors available to businesses. Many of these are represented on the national business mentoring network, Mentorsme.co.uk, which provides access to around 10,000 mentors - offering both free and paid for mentoring. This government-backed service is funded by the British Bankers' Association and supported by the Better Business Finance Roundtable, which includes the key business groups in the UK.</p>
			<p><a href="http://www.mentorsme.co.uk/" target="_blank" rel="noopener noreferrer">Find business mentors on the Mentorsme.co.uk website</a>.</p>
			<p>There are many other sources of business mentors, including some geared specifically towards supporting local businesses, young entrepreneurs or specific business sectors. See the page in this guide on <a href="/starting-up/further-support-for-your-business/the-benefits-of-business-mentoring/further-information-for-finding-or-becoming-a-business-mentor.php">further information for finding or becoming a business mentor</a>.</p>
			
