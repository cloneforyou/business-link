			<h1>The benefits of business mentoring</h1>
			<h2>Further information for finding or becoming a business mentor</h2>
			<p>There are several organisations that make mentors available to businesses. Many of these are represented on the national business mentoring network, Mentorsme.co.uk.</p>
			<p>Mentorsme.co.uk lets you:</p>
			<ul>
				<li>search for business mentors, according to their location and/or area of expertise</li>
				<li>offer your services as a business mentor, through an existing mentoring organisation  </li>
			</ul>

			<p><a href="http://www.mentorsme.co.uk/" target="_blank" rel="noopener noreferrer">Business mentors on the Mentorsme.co.uk website</a>.</p>
			<p>There are more than 10,000 mentors available on the network. Both face-to-face and online support is offered, and many mentors have particular expertise in areas such as finance, leadership and innovation.</p>
			<p>Mentorsme.co.uk is funded by the British Bankers' Association and supported by the Better Business Finance Roundtable, which includes the key business groups in the UK. It is backed by the Department for Business, Energy &amp; Industrial Strategy.</p>
			
			<h3>Other main providers of business mentoring services</h3>
			<p>You can also find business mentors through the networks listed below.</p>
			<p><a href="https://www.crunch.co.uk/blog/freelancer-advice/" target="_blank" rel="noopener noreferrer">Read information about online mentoring on the Freelance Advisor website</a>.</p>
			<p><a href="http://www.horsesmouth.co.uk/publisha.content/Work/topics.publisha?topic=Starting+a+business" target="_blank" rel="noopener noreferrer">Read information about online business mentoring on the horsesmouth website</a>.</p>
			<p><a href="https://www.enterprisenation.com/" target="_blank" rel="noopener noreferrer">Read information about online mentoring on the Enterprise Nation website</a>.</p>
			<p><a href="https://www.shell-livewire.org/" target="_blank" rel="noopener noreferrer">Read information about e-mentoring for young entrepreneurs on the Shell LiveWIRE website</a>.</p>
			
			<h3>Further sources of advice</h3>
			<p>The British Chambers of Commerce (BCC) network can point you towards business mentors with the skills and experience you need.</p>
			<p><a href="http://www.britishchambers.org.uk/contact-the-bcc/" target="_blank" rel="noopener noreferrer">Find contact information for the BCC on the BCC website</a>.</p>
			<p>You can call the British Chambers of Commerce on Tel 024 7669 4484.</p>
			<p>If your business operates in the creative or technology sectors, the National Endowment for Science Technology and the Arts (NESTA) may be able to recommend suitable mentors from its network.</p>
			<p></p>
			<p><a href="http://www.nesta.org.uk/contact_us" target="_blank" rel="noopener noreferrer">Find contact information for the creative business mentoring network on the NESTA website</a>.</p>
			<p>You can call the NESTA Helpline on Tel 020 7438 2500.</p>
			<p>Businesses looking for mentoring support with leadership and management issues can contact the Management and Leadership Network (MLN).</p>
			<p><a href="https://web.archive.org/web/20120121072633/http://www.mln.org.uk/online_mentoring.asp" target="_blank" rel="noopener noreferrer">Find contact information for the MLN e-mentoring scheme on the MLN website</a>.</p>
			<p>You can call the Management and Leadership Network InfoPoint on Tel 0845 602 6634.</p>
			
