			<h1>Business Advice Open Days</h1>
			<h2>How to contact the event organisers</h2>
			<p>If you have a query concerning any event, you can email the Business Liaison Team at <a href="#">open.days@hmrc.gsi.gov.uk</a>. You can also contact the HMRC Business Advice Open Day Booking Line on Tel 0845 603 2691 to discuss your booking.</p>
			<p>However, please be aware that the team cannot deal directly with any queries concerning tax (eg VAT, Corporation Tax, National Insurance) or benefits (eg tax credits, child benefit) enquiries.</p>
			<p>If your queries are about any of these subjects, you can contact specific HM Revenue &amp; Customs (HMRC) helplines. <a href="https://www.gov.uk/government/organisations/hm-revenue-customs/contact" target="_blank" rel="noopener noreferrer">Find a list of HMRC contact telephone numbers on the HMRC website</a>.</p>
			
			<h3>How the Business Liaison Team acts on the feedback</h3>
			<p>All feedback received on Business Advice Open Days is valued and acted upon by the organisers. You can either complete a feedback sheet at the venue at the end of your visit, send your comments by email to <a href="#">open.days@hmrc.gsi.gov.uk</a>.</p>
			<p>There have been a number of changes made to Business Advice Open Days because of feedback. For example, there is now a dedicated help desk situated at the entrance to all events and seminar programmes have been improved to reflect the topics of greatest interest to visitors.</p>
			
