		<h1>Who is liable for Corporation Tax</h1>
		<h2>What you must do if your company or organisation is liable for Corporation Tax</h2>
		<p>If your company or organisation is liable for Corporation Tax, it is subject to Corporation Tax deadlines and requirements. This means you have to do more than just pay the correct amount of Corporation Tax each year.</p>
		<p>You must also tell HM Revenue &amp; Customs (HMRC) you're liable for Corporation Tax and file a Company Tax Return on time.</p>
		<p>There are different requirements depending on whether or not your company or organisation is active - eg carrying on business, trading or receiving income - or dormant - eg not active, not carrying on business or not trading.</p>
		
		<h3>What active companies or organisations must do</h3>
		<p>If your company or organisation is liable for Corporation Tax, and is active - eg trading or receiving income - you must:</p>
		<ul>
			<li>tell HMRC that they are active</li>
			<li>pay Corporation Tax on any profits that are taxable within nine months and one day&nbsp;of the end of your Corporation Tax accounting period - usually the end of your financial year</li>
			<li>file a Company Tax Return within 12 months of the end of your company or organisation's Corporation Tax 'accounting period' - usually the end of your financial year</li>
		</ul>
		<p>If your company or organisation is liable for Corporation Tax, but is dormant or is not active, you must still submit a Company Tax Return within 12 months of the end of your Corporation Tax accounting period (usually the end of your company or organisation's financial year) if HMRC sends your company or organisation a 'Notice to deliver a Company Tax Return'. You don't need to file a return if HMRC does not send you this notice.</p>
		
		<p>Find out how to tell HMRC your company or organisation is active in our guide on <a href="/tax-payroll-and-company-information/corporation-tax/corporation-tax-for-new-companies-and-organisations/starting-a-company-or-organisation-and-corporation-tax.php">starting a company or organisation and Corporation Tax</a>.</p>
		
