		<h3>Submitting your Company Tax Return and how to pay</h3>
		<p>Virtually all companies and organisations must submit their Company Tax Returns <strong>online</strong>. You must also pay any Corporation Tax that's due <strong>electronically</strong>.</p>
		<p><strong>Additionally</strong> your tax computations and, with very few exceptions, the accounts that form part of your Company Tax Return, must be submitted in Inline eXtensible Business Reporting Language (iXBRL) format.</p>
