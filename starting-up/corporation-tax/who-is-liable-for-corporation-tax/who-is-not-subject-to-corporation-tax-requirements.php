		<h2>Who is not subject to Corporation Tax requirements?</h2>
		<p>Businesses and organisations that are not subject to Corporation Tax requirements include:</p>
		<ul>
			<li>sole traders - one-person businesses that are not operating through a limited company</li>
			<li>traditional partnerships</li>
			<li>limited liability partnerships</li>
			<li>local authorities</li>
			<li>local authority associations</li>
			<li>investment clubs</li>
			<li>allotment and garden societies</li>
			<li>health service bodies</li>
			<li>London Organising Committee of the Olympic Games Ltd and the International Olympic Committee</li>
		</ul>
		<p>If your business or organisation is not subject to Corporation Tax, you don't need to meet Corporation Tax deadlines and requirements.</p>
		<p>This doesn't mean your business is exempt from all taxes as you may have to complete a Self Assessment return.</p>
