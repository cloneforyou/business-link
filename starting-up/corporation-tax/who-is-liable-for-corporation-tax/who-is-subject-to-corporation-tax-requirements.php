		<h1>Who is liable for Corporation Tax</h1>
		<h2>Who is subject to Corporation Tax requirements?</h2>
		<p>The following limited companies and unincorporated organisations are subject to Corporation Tax requirements:</p>
		<ul>
			<li>limited companies incorporated in the UK</li>
			<li>foreign-based companies with a permanent place of business in the UK</li>
			<li>members' clubs, such as social clubs, sports clubs and holiday clubs</li>
			<li>societies, such as friendly societies and provident societies</li>
			<li>associations, such as housing associations and trade associations</li>
			<li>co-operatives</li>
			<li>other unincorporated associations</li>
			<li>groups of individuals carrying on a business that is not a partnership</li>
			<li>charities, or companies that are subsidiaries of - or wholly owned by - a charity</li>
			<li>NHS foundation trusts if they are carrying out significant commercial activities that are not part of core health care delivery - such as running a commercial laundry</li>
		</ul>
		<p>The taxable profits or surpluses of these businesses and organisations are subject to Corporation Tax requirements.</p>
		<p>A company or organisation subject to Corporation Tax requirements is known to HM Revenue &amp; Customs for Corporation Tax purposes as being 'within the charge to Corporation Tax', 'chargeable to tax' or in 'the charge to tax'.</p>
