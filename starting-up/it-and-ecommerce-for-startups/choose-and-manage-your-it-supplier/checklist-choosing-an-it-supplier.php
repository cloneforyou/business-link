		<h1>Choose and manage your IT supplier</h1>
		<h2>Checklist: choosing an IT supplier</h2>
		<p>As a new IT system can be a significant investment, it's important to choose the right system and supplier. Before you choose an IT supplier you should:</p>
		<ul>
			<li>Find out whether the supplier can provide all the hardware, software, licences, services, support and maintenance you need.</li>
			<li>Check whether the supplier intends to use subcontractors for any part of the supply (eg, installation, cabling or manning a helpdesk). If they do, make sure the subcontractors have the right kind of expertise for your system.</li>
			<li>Check whether they will install and configure your system so it's fully operational.</li>
			<li>Ask whether they will train your staff.</li>
			<li>Confirm what frontline support they can provide - eg a telephone helpdesk.</li>
			<li>Ask whether they will take responsibility for system components purchased elsewhere.</li>
			<li>Investigate what sort of maintenance contracts they provide.</li>
			<li>Get details of what exactly is included in their supply contract.</li>
			<li>Find out what sort of warranty they provide.</li>
			<li>Confirm whether they will continue to provide support if you take responsibility for replacing faulty hardware yourself.</li>
			<li>Find out if they will accept payment for the system only when it is installed and working to your satisfaction.</li>
			<li>Check what is and isn't included in the price - ask whether upgrades and fixes to software are included.</li>
			<li>Ask them to provide references from other, similar companies they've helped in the past.</li>
			<li>Ask whether they will provide you with written documentation, such as training manuals, that will help you understand your system.</li>
			<li>Decide whether you feel they understand your business needs.</li>
			<li>Ask about any experience they may have in your industry.</li>
			<li>Ask whether they are happy for you to test the proposed system, perhaps under a non-disclosure agreement.</li>
			<li>Find out whether they are financially viable and able to meet your requirements in the foreseeable future.</li>
		</ul>
		
