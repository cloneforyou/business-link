		<h1>Choose and manage your IT supplier</h1>
		<h2>Selecting the right IT supplier</h2>
		<p>The actual process of selecting a supplier will normally follow on from a request for a number of suppliers to submit proposals.</p>
		<p><strong>Review the proposals</strong> you have received in detail and come up with a shortlist.</p>
		<p>Compare proposals on a like-for-like basis. Take into account:</p>
		<ul>
			<li>financial viability</li>
			<li>implementation track record</li>
			<li>quality and relevance of reference sites</li>
			<li>project management capability</li>
			<li>ability to understand your business at both the strategic and process levels</li>
			<li>technical knowledge and expertise</li>
			<li>confidence that the supplier will be able to provide the solution into the foreseeable future</li>
			<li>the cost of the proposed solution</li>
		</ul>

		<p>Throughout the evaluation process, <strong>rank the suppliers</strong> and eliminate those that do not meet your technical requirements or fall outside your agreed budget.</p>
		<p>This process should usually include a <strong>demonstration</strong> of their proposed offering by each of the suppliers. This gives you the opportunity to evaluate the system being proposed and the supplier.</p>
		<p>Invite the potential suppliers to <strong>visit</strong> your company. Do they understand your business needs and have experience of your industry? Do you feel comfortable dealing with them?</p>
		<p>Ensure that the shortlisted suppliers provide <strong>references</strong> and contact them as part of the evaluation process. Try to obtain references from businesses similar to your own as this is a useful way to assess the system and the supplier.</p>
		<p>You should then be in a position to come up with a single preferred supplier. Consider carrying out further <strong>testing of the proposed solution</strong>, particularly if the system is going to be critical to your business.</p>
		<p>If you are happy with the proposed solution of the preferred supplier, you can move to the contract stage. You will need to formally agree on factors such as the levels of system maintenance and support, as well as the overall cost of the proposed solution.</p>

