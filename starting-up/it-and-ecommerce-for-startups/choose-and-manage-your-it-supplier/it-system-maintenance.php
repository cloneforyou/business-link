		<h1>Choose and manage your IT supplier</h1>
		<h2>IT system maintenance</h2>
		<p>Your IT supplier will probably offer you a range of <strong>hardware-maintenance contracts</strong> - perhaps as part of an overall support contract.</p>
		<p>Hardware failure is just one of many problems that could occur and you need to consider a range of potential risks carefully before making a decision. These include:</p>
		<ul>
			<li>theft of hardware</li>
			<li>physical damage</li>
			<li>intellectual property loss from hacking or human error</li>
			<li>system failure</li>
		</ul>

		<p>You can also <a href="/it-and-ecommerce/it-security-and-risks/assess-and-minimise-your-it-risks.php">assess and minimise the IT risks facing your business with our IT risk assessment tool</a>.</p>
		<p><strong>System maintenance and management</strong> are essential parts of managing these risks. Many IT suppliers can monitor problems remotely and take action to prevent a system failure.</p>
		<p>Many different types of maintenance contract are available. Most are on-site contracts where an engineer comes to your premises to fix the problem. However, some contracts still operate on a return-to-depot basis where you are required to return the equipment for repair. Costs vary considerably depending on the minimum response time and working hours you need.</p>
		<p>Contracts may seem expensive, particularly for standard PCs and peripherals with extended on-site warranties. After assessing the risks, you may decide not to get a maintenance contract and simply hold spares or purchase new parts when necessary.</p>
		<p>However, the value of a contract is that someone else takes the responsibility for system maintenance but some suppliers may be reluctant to offer helpdesk, software support and other services unless they're also maintaining the hardware.</p>
		
		<h3>Software maintenance contracts</h3>
		<p>You should ensure any new software purchases are compatible with your existing equipment and operating system.</p>
		<p>Many IT software suppliers offer maintenance contracts when you purchase their products. These can include recurring licensing fees, product upgrades, new versions, and patches and bug fixes. Some vendors charge for these, while others will include them for free with your purchase. You should also look for a supplier who gives you the original disks and licences if your software is coming pre-installed.</p>
		<p>If you don't have in-house experience, then look for a supplier that offers maintenance. Costs will vary depending on the type of software you're using, but the general rule of thumb is to have costs below 15 per cent of the purchase price. If using <strong>cloud computing</strong>, maintenance and other upgrades will be managed 'off site' by the service provider. For more information see our guide on <a href="/it-and-ecommerce/software-and-business-applications/cloud-computing.php">cloud computing</a>.</p>
		
