		<h1>Choose and manage your IT supplier</h1>
		<h2>Identifying and contacting potential IT suppliers</h2>
		<p>At the outset of any IT system procurement exercise you should <strong>establish and document your requirements</strong>. You can provide potential suppliers with these details. It will also provide a framework to compare the different quotations you may receive.</p>
		<p>The next stage is to <strong>research the potential solutions and suppliers</strong>. Try to identify three to five suppliers who are able to propose a solution for the budget you have set. Sources of information include:</p>
		<ul>
			<li>the internet, including the suppliers' own websites and sites offering independent guidance</li>
			<li>recommendations from trusted colleagues</li>
			<li>recommendations from trading and business partners</li>
			<li>IT seminars and conferences</li>
			<li>computer trade magazines</li>
			<li>advice from relevant trade associations and professional bodies</li>
			<li>IT exhibitions</li>
		</ul>

		<p>This research may highlight further options which should be reflected in your statement of requirements.</p>
		<p>Having identified the potential suppliers, you can contact them. Two commonly used approaches are a request for proposal (RFP) or a request for quotation (RFQ).</p>
		<p>The RFP asks for suppliers to submit formal proposals. This is usually followed by a shortlisting of candidates who are required to provide a series of presentations and/or demonstrations. The RFP should include:</p>
		<ul>
			<li>the agreed statement of requirements</li>
			<li>an indication of what format the proposal should follow</li>
			<li>an indication of the level of detail required in the proposal</li>
			<li>the timescales within which the proposal should be submitted</li>
			<li>the procedure for obtaining any further information from you should the suppliers have any queries</li>
		</ul>

		<p>The RFQ asks for potential suppliers to competitively provide a cost for the product or service in question. A customer sends solicitations to possible suppliers containing a list of requirements. These can include:</p>
		<ul>
			<li>part descriptions, specifications or numbers</li>
			<li>quantities or volumes</li>
			<li>a description or drawings of the product needed</li>
			<li>personnel skills or competencies for the product or service</li>
			<li>terms and conditions</li>
			<li>term of the contract</li>
			<li>any other value-added requirements or terms</li>
			<li>delivery requirements</li>
			<li>a draft contract</li>
		</ul>

