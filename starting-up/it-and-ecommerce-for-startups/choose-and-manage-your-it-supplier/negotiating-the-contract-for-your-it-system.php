		<h1>Choose and manage your IT supplier</h1>
		<h2>Negotiating the contract for your IT system</h2>
		<p>Make sure you're completely clear about exactly what the contract with your supplier includes. Hidden extras can be expensive.</p>
		<p>Check whether you'll receive:</p>
		<ul>
			<li><strong>on-site service calls</strong> - a certain number may be included or charged at a reduced rate</li>
			<li><strong>preventative maintenance</strong>, eg the use and regular updating of anti-virus software and firewalls to protect your system</li>
			<li><strong>remote monitoring</strong> and diagnosis of system problems - if included, check whether you will have to purchase any extra equipment or communication links</li>
			<li><strong>telephone support</strong> - there may be a limit on the number or length of telephone calls</li>
			<li><strong>warranties</strong> - check what warranties, if any, are included</li>
			<li><strong>software upgrades</strong> - check if upgrades are free, or whether you have to purchase new versions as they are released or pay an annual software subscription</li>
			<li><strong>software support</strong> - the ability for you to report problems and receive help, patches and bug fixes</li>
			<li><strong>user training</strong> - some suppliers include a limited amount of training in the system's purchase price, but others will charge extra</li>
			<li><strong>manuals</strong> - you may need them if you end the relationship with your supplier in future</li>
		</ul>

		<p>When purchasing IT services, you should draw up a <strong>service-level agreement (SLA)</strong> with the supplier, which specifies precisely what you expect from the supplier. A typical SLA should cover:</p>
		<ul>
			<li><strong>scope</strong> - details of the hardware and software that is to be covered</li>
			<li><strong>range of services</strong> - the services that the supplier will be required to provide</li>
			<li><strong>service availability</strong> - when the service should be available</li>
			<li><strong>response times</strong> - an important aspect of the SLA - usually the faster the response times, the greater the cost</li>
			<li><strong>escalation procedures</strong> - an agreed series of actions used to ensure problems are dealt with quickly: responses are intensified until the problem is resolved - eg moving from off-site diagnosis through to on-site support</li>
			<li><strong>record keeping</strong> - problems and solutions must be properly documented, and records maintained for dispute resolution</li>
			<li><strong>performance review</strong> - to maintain acceptable levels of service over time</li>
			<li><strong>supplier obligations</strong> - such as the provision of spare parts, the qualifications of support staff and the need to meet response times</li>
			<li><strong>customer obligations</strong> - your responsibilities, eg providing information about changes that affect the scope of the contract, and the cooperation of staff with the supplier</li>
			<li><strong>termination of agreement</strong> - a formal process which defines the specific terms and conditions under which the contract may be terminated</li>
		</ul>

