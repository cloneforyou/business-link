		<h1>Computer software: the basics</h1>
		<h2>Software installation, activation and upgrades</h2>
		<p>Traditionally, most off-the-shelf software has been delivered on CD-Rom. Its installation is usually simple - insert the CD and answer questions about how and where the software is to be installed. Many types of software are now available to buy and download online, with equally simple installation procedures.</p>
		<p>Software that will be shared between several employees and applications will typically be installed on a server - usually a bigger and more powerful computer that is often located in a secure office space. This may be a complicated process and will require the correct password and access rights.</p>
		<p>For businesses with more than ten desktop PCs, it can be better to set them up so that they all have a common set of software. This will make it easier to keep them all up to date and to check that software is properly licensed.</p>
		<p>Some software will need to be <strong>activated</strong> once it has been installed. You may be able to use the software for a short time without activation but it will eventually stop working.</p>
		<p>Activation can be carried out through the internet or by telephone. The software supplier will obtain some details about the hardware of the computer you are using and will then issue an authentication code. This will allow the software to be used. If the hardware details of the PC are changed it may be necessary to re-activate the software.</p>
		<p><strong>Software upgrades</strong> are offered by the supplier because:</p>
		<ul>
			<li>The software is improved or new functions are added.</li>
			<li>'Bugs' are discovered in the software and must be corrected, perhaps for security reasons. For example, bugs in web browsers have allowed hackers to gain access to company systems. The software suppliers provide 'fixes' or 'patches' for their software to prevent this.</li>
			<li>It has become incompatible with other, newer software.</li>
		</ul>

		<p>Upgrades are usually distributed via the internet. It is important to install upgrades and keep systems software fully up to date. If not done regularly, your systems may be subject to attack through newly discovered security flaws.</p>
		<p>If using cloud computing you will no longer need to install and set up software across the business. Instead your business applications are provided over the internet, with any future maintenance and upgrades being managed 'off site' by your cloud computing service provider. See our guide on <a href="/it-and-ecommerce/software-and-business-applications/cloud-computing.php">cloud computing</a>.</p>
		
