		<h1>Computer software: the basics</h1>
		<h2>Licences for proprietary software</h2>

		<p>Proprietary software is made available under a number of different types of licence.</p>
		<p><strong>Shrink-wrap licences</strong> are usually for one installation of the software. This is the most common type of licence for software that you will find in retail stores, including online outlets.</p>
		<p>Some versions of this type of licence may require you to activate the software. This allows the supplier to check that the software is installed on just one PC. Such software may require re-activation if you change the hardware in the PC.</p>
		<p><strong>Per-user licences</strong> are common where software may be used by more than one person simultaneously. The licence is usually for a specified maximum number of users.</p>
		<p><strong>Site licences</strong> are much less restrictive. They typically allow as many users as you wish at one location.</p>
		
		<h3>Software piracy</h3>
		<p>Software licences are often expensive, and it may be tempting to buy just one licence for a product and copy it for several users. The software industry is protective of its licensing revenue and, if you are discovered, you and your business could face prosecution.</p>
		<p>Be wary of buying very cheap software as it may well have been pirated by criminals. There have been cases of firms who supply complete licensed systems using pirated versions of networking software or operating systems.</p>
		<p>The Business Software Alliance (BSA) gives advice on how to manage your business software. You can <a href="https://web.archive.org/web/20120905154529/http://www.bsa.org/country/Anti-Piracy/What-is-Software-Piracy.aspx" title="Examples of unlicensed software use which are illegal and free tools to carry out a software audit" target="_blank" rel="noopener noreferrer">find guidance on protecting your business from software piracy on the BSA website</a>.</p>
		<p>Software is available that will investigate the software and hardware running on your computers and build up an inventory for you. This is a good investment, partly for your own resource management and partly because it helps you counter allegations of running unlicensed software.</p>
		
