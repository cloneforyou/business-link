			<h1>Buy a franchise</h1>
			<h2>Advantages and disadvantages of franchising</h2>
			<p>Buying a franchise can be a quick way to set up your own business without starting from scratch. But there are also a number of drawbacks.</p>
			
			<h3>Advantages</h3>
			<ul>
				<li>Your business is based on a <strong>proven idea</strong>. You can check how successful other franchises are before committing yourself.</li>
				<li>You can use a recognised <strong>brand name</strong> and <strong>trade marks</strong>. You benefit from any advertising or promotion by the owner of the franchise - the 'franchisor'.</li>
				<li>The franchisor gives you <strong>support</strong> - usually including training, help setting up the business, a manual telling you how to run the business and ongoing advice.</li>
				<li>You usually have <strong>exclusive rights</strong> in your territory. The franchisor won't sell any other franchises in the same territory.</li>
				<li><strong>Financing</strong> the business may be easier. Banks are sometimes more likely to lend money to buy a franchise with a good reputation.</li>
				<li>You can benefit from communicating and sharing ideas with, and receiving support from, other franchisees in the network.</li>
				<li>Relationships with <strong>suppliers</strong> have already been established.</li>
			</ul>
			
			<h3>Disadvantages</h3>
			<ul>
				<li><strong>Costs</strong> may be higher than you expect. As well as the initial costs of buying the franchise, you pay continuing management service fees and you may have to agree to buy products from the franchisor.</li>
				<li>The franchise agreement usually includes <strong>restrictions</strong> on how you can run the business. You might not be able to make changes to suit your local market.</li>
				<li>The franchisor might go <strong>out of business</strong>.</li>
				<li>Other franchisees could give the brand a <strong>bad reputation</strong>, so the recruitment process needs to be thorough</li>
				<li>You may find it difficult to <strong>sell</strong> your franchise - you can only sell it to someone approved by the franchisor.</li>
				<li>All profits (a percentage of sales) are usually shared with the franchisor.</li>
			</ul>
			
