			<h1>Buy a franchise</h1>
			<h2>Assess a franchise opportunity</h2>
			<p>To assess if a franchise represents a sound business opportunity, you'll need to consider:</p>
			<ul>
				<li>what the business is and how it <strong>operates</strong></li>
				<li>the <strong>location</strong> of the franchise</li>
				<li>the <strong>success</strong> of the franchise concept - the number of franchises in the UK, the length of time in business and how financially successful they are</li>
				<li>the amount and strength of <strong>competition</strong> from other businesses in the same market sector - at both a local and national level</li>
				<li>any <strong>market research</strong> that has analysed the public perception of the franchisor's (the business offering the franchise) brand</li>
				<li>levels of initial and ongoing <strong>costs</strong> - see the page in this guide on <a href="/starting-up/buy-a-business/buy-a-franchise/the-costs-of-a-franchise.php">the costs of a franchise</a> </li>
				<li>how much <strong>training</strong> and <strong>support</strong> you'll get in setting up and running the business</li>
				<li>conditions and restrictions in the <strong>franchise agreement</strong>, including how long it will run and whether you'll have the option to renew</li>
			</ul>

			<p>The franchisor will probably give you an <strong>information pack</strong> but you shouldn't just rely on this. Ask questions and look for evidence of their claims.</p>
			<p>One of the most helpful things you can do before deciding on a franchise is to visit <strong>other franchisees</strong> and talk to them. Ask the franchisor for a full list of past and present franchisees, not just the two most successful ones. It is important to visit new and established franchises - of differing levels of success - in as many different locations as possible. This should give you a good idea of the challenges you yourself will face should you decide to go ahead with a specific franchise.</p>
			<p>Take advantage of other sources of information and advice. The <strong>British Franchise Association</strong> (BFA) and <strong>whichfranchise.com</strong> websites have comprehensive lists of solicitors specialising in franchising at national and local levels. Ask your bank - many have franchising specialists. And make the most of other advisers such as Business Link, your solicitor or your accountant. <a href="http://solicitors.lawsociety.org.uk/" target="_blank" rel="noopener noreferrer">Find a solicitor on the Law Society website</a>, and <a href="http://www.accaglobal.com/gb/en/member/find-an-accountant.html?isocountry=GB" target="_blank" rel="noopener noreferrer">find an accountant on the Association of Chartered Certified Accountants (ACCA) website</a>.</p>
			
			<h3>How a business plan can help</h3>
			<p>Just as you would for any other business, you need to draw up a business plan when buying a franchise. This will help you assess the prospects for the business and identify potential weaknesses. A business plan is also essential for raising finance. For more information, see our guide on how to <a href="/starting-up/business-planning/prepare-a-business-plan.php">prepare a business plan</a>. You should be able to get assistance with your plan from the franchisor - and banks with specialist franchise units can check how realistic your projections are.</p>
			
