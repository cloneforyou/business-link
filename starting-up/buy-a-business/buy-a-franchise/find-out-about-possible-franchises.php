			<h1>Buy a franchise</h1>
			<h2>Find out about possible franchises</h2>
			<p>You can find out about possible franchise opportunities from a range of sources.</p>
			<p>A useful starting point is the British Franchise Association (BFA) website. As well as offering guidance and seminars on franchising, it provides contact information on all BFA members' franchise opportunities - both new franchises and existing franchises for resale.</p>
			<p><a href="http://www.thebfa.org/members" target="_blank" rel="noopener noreferrer">Find members who may be offering new franchises on the BFA website</a>.</p>
			<p>Franchises are advertised and written about in various national <strong>newspapers</strong> and in trade publications such as:</p>
			<ul>
				<li>Daltons Weekly - <a href="https://www.daltonsbusiness.com/" target="_blank" rel="noopener noreferrer">find out about Daltons Weekly on the Daltons website</a> </li>
				<li>Franchise World - <a href="http://www.franchiseworld.co.uk/" target="_blank" rel="noopener noreferrer">read franchise opportunities, news and views on the Franchise World website</a> </li>
				<li>Business Franchise - <a href="http://www.businessfranchise.com/" target="_blank" rel="noopener noreferrer">search for franchise opportunities on the Business Franchise website</a> </li>
				<li>The Franchise Magazine - <a href="http://www.thefranchisemagazine.net/" target="_blank" rel="noopener noreferrer">find business franchise opportunities on The Francise Magazine website</a></li>
			</ul>
			<p>Websites such as <strong>whichfranchise.com</strong> and <strong>Franchiseinfo</strong> can be a useful source of information on franchises. You can find other listings using a <strong>search engine</strong> and searching for terms like'franchise', 'franchise opportunity' or 'franchise directory'.</p>
			<p>Attending a franchise <strong>exhibition</strong> can also be a good way of finding out what's on offer. You can <a href="https://www.franchiseinfo.co.uk/exhibitions-across-the-uk" target="_blank" rel="noopener noreferrer">find details of some major franchise exhibitions on the Franchiseinfo website</a>.</p>
			<p>But tread carefully. Advertised franchise opportunities - particularly multi-level marketing schemes - can be untried, dishonest or even illegal. Assess the franchise opportunity carefully and check if the business offering the franchise is a member of the BFA.</p>
			
