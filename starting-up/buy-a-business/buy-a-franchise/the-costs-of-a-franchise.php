			<h1>Buy a franchise</h1>
			<h2>The costs of a franchise</h2>
			<p>When calculating the likely cost of a franchise, you need to take both initial and ongoing fees into account.</p>
			
			<h3>Initial costs</h3>
			<p>The franchisor - the business that sells you the franchise - usually charges an <strong>up-front fee</strong>. If the franchisor relies mainly on taking a percentage of your sales revenue, rather than on a high initial fee, it is usually a good indication that they have confidence in the value of their product or service.</p>
			<p>Your largest initial costs are usually your <strong>investment</strong> in:</p>
			<ul>
				<li>premises</li>
				<li>equipment</li>
				<li>initial stock</li>
			</ul>
			
			<p>You will need to establish <strong>a business entity</strong>. Although a franchisee holds a contractual agreement with the franchisor, each franchisee is an independent business - and it is this business entity that will enter into the franchise agreement. Your chosen business structure could be a limited company, partnership or sole trader - each of which will involve different costs - or your franchisor might have specific requirements. See our guide on <a href="/starting-up/business-structures/legal-structures-the-basics.php">legal structures: the basics</a>.</p>
			
			<h3>Continuing costs</h3>
			<p>You usually pay a percentage of the sales revenue to the franchisor by way of a <strong>management service fee</strong>. Alternatively, you may pay a fixed management fee of some kind.</p>
			<p>Under the terms of the franchise agreement, you may have to buy <strong>stock</strong> from the franchisor. Check what they charge. They may mark up the prices - or they may be able to offer them to you at a discount because of theirbuying power.</p>
			<p>You also have to pay the usual business costs - for example, rent for premises, utility bills or the costs of any employees you take on. Again, check if the things that you pay for through the franchisor have a realistic cost.</p>
			<p>Check too if the agreement includes additional charges. For example, you may be required to pay for <strong>training</strong>, or to contribute to the cost of national <strong>advertising campaigns</strong>.</p>
			
