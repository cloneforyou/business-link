			<h1>Buy a franchise</h1>
			<h2>How to purchase a franchise</h2>
			<p>There are a number of key things you need to consider when planning to buy a franchise. It is worth thinking about the following:</p>
			<ul>
				<li>Assess yourself to see what kind of franchise, if any, will suit you.</li>
				<li>Find out what franchises are available and draw up a shortlist.</li>
				<li>Assess franchise opportunities carefully, ask questions and talk to other franchisees.</li>
				<li>When you find a business, investigate its financial prospects. Base this on thorough research of performance figures. Include an analysis of three years' accounts - if they have been trading for that period - and management figures.</li>
				<li>If you'll need to raise finance, ask your bank if it will consider a loan for the type of franchise you're considering.</li>
				<li>Do your own market research into the business and the competitors in your area.</li>
				<li>Draw up a business plan.</li>
				<li>Check the franchise agreement and get professional advice.</li>
			</ul>

			<p>However, it is advisable to make sure you don't:</p>
			<ul>
				<li>take up the first opportunity before investigating alternatives</li>
				<li>allow yourself to be hurried into making a decision</li>
				<li>pay any non-refundable deposit</li>
				<li>commit yourself before you're completely sure</li>
				<li>assume a business will work in your area just because it works elsewhere</li>
				<li>rely on the forecasts provided by the business selling you the franchise</li>
				<li>sign any agreement without legal advice</li>
			</ul>

