			<h1>Buy an existing business</h1>
			<h2>Looking after existing employees</h2>
			<p>There are regulations that govern what happens to employees when someone new takes over a business.</p>
			<p>These apply to all employees when a business is transferred as a going concern, meaning employees automatically start working for the new owner under the same terms and conditions. For more information, see our guide on <a href="/employment-and-skills/redundancy-restructures-and-change/responsibilities-to-employees-if-you-buy-or-sell-a-business.php">responsibilities to employees if you buy or sell a business</a>.</p>
			
			<h3>Employment tribunal awards</h3>
			<p>When you buy an existing business, you might decide you need to employ fewer staff. But be careful about making any changes, as an employee might take a case to an employment tribunal for unfair dismissal or unfair selection for redundancy. It's best to consult a solicitor before making any such changes. For more information see our guides on <a href="/employment-and-skills/dismissals-and-staff-leaving/dismissal.php">dismissal</a> and <a href="/employment-and-skills/redundancy-restructures-and-change/redundancy-the-options.php">redundancy: the options</a>.</p>
			
			<h3>Inform and consult employees</h3>
			<p>If you do want to discuss reducing employee numbers or reorganising staff, it's a good idea to do this once you have completed the due diligence period, but before you take over the business. As the new employer you should inform and consult all employees - including employee representatives - who may be affected. For more information see our guide on how to <a href="/employment-and-skills/redundancy-restructures-and-change/inform-and-consult-your-employees.php">inform and consult your employees</a>.</p>
			<h3>Pensions</h3>
			<p>As their new employer, you do not have to take over rights and obligations relating to employees' occupational pension schemes put in place by the previous employer. However, if you don't provide comparable pensions arrangements, you could theoretically face a claim for unfair dismissal.</p>
			
