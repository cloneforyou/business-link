			<h1>Buy an existing business</h1>
			<h2>Deciding on the right type of business to buy</h2>
			<p>Ideally any business you buy needs to fit your own skills, lifestyle and aspirations. Before you start looking, think about what you can bring to a business and what you'd like to get back.</p>
			<p>List what is important to you. Look at your motivations and what you ultimately want to achieve. It is useful to consider:</p>
			<ul type="disc">
				<li>Your <strong>abilities</strong> - can you achieve what you want to achieve?</li>
				<li>Your <strong>capital</strong> - how much money do you have to invest?</li>
				<li>Your <strong>expectations</strong> in terms of earning - what level of profit do you need to be looking for to accommodate your needs?</li>
				<li>Your <strong>commitment</strong> - are you prepared for all the hard work and money that you will need to put into the business to get it to succeed?</li>
				<li>Your <strong>strengths</strong> - what kind of business opportunity will give you the chance to put your skills and experience to good use?</li>
				<li>The <strong>business sector</strong> you're interested in - learn as much as you can about your chosen industry so you can compare different businesses. It's important to take the time to talk to people already in similar businesses. The internet and your local library will also be good sources of information. Find out how to comply with all the regulations and licences that apply to <a href="/your-business-sector.php">Your business sector</a>.</li>
				<li><strong>Location</strong> - don't restrict your search to your local area. Some businesses can be easily relocated.</li>
			</ul>

