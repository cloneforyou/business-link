		<h1>Comply with the law in the provision of goods, facilities and services</h1>
		
		<h2>Supplying satisfactory and safe goods and services</h2>
		<p>The Sale of Goods Act and the Supply of Goods and Services Act make it a legal requirement for every business to ensure that the products or services it sells meet minimum standards.</p>
		<p><b>All goods</b> you sell must:</p>
		<ul>
			<li>correspond with any description given - verbally, in writing or in an illustration</li>
			<li>be of satisfactory quality</li>
			<li>be fit for their purpose</li>
		</ul>

		<p><b>All services</b> you supply must be carried out:</p>
		<ul>
			<li>with reasonable care and skill</li>
			<li>within a reasonable time and for a reasonable charge, unless these matters have previously been agreed with the customer</li>
		</ul>

		<p>Service providers must also provide specific information such as their business name and address and details of the service to be provided. See our guide on <a href="/sales-and-marketing/selling-and-the-law/customer-protection.php" title="An introduction to the rights customers have when buying goods or services from you">customer protection</a>.</p>
		<p>Customers who are unhappy with goods may be able to claim compensation, while in other circumstances they may be able to ask for repair or replacement of the goods.</p>
		<p>If any services you provide don't meet the requirements specified above you will be in breach of contract and customers may well be entitled to compensation.</p>
		<p>Your responsibility to your customers can last for a maximum of six years, dependent on the nature of the goods or services (five years from discovery of the problem in Scotland).</p>
		<p>For more information, see our guide on <a href="/sales-and-marketing/selling-and-the-law/the-sale-of-goods-act.php" title="Keep on the right side of the law when selling to your customers">the Sale of Goods Act</a>.</p>
		<p>The Consumer Protection from Unfair Trading Regulations give consumers the right not to be misled by the actions or omissions of traders. They also protect consumers from aggressive or high-pressure selling techniques.</p>
		<p>Business customers are protected by rules that prohibit misleading advertising.</p>
		<p><a href="../../../_files/berr_the-consumer-protection-from-unfair-trading-regulations-a-basic-guide-for-business_mar-2008.pdf" title="Practical advice on how the Consumer Protection from Unfair Trading Regulations apply to your business" target="_blank" rel="noopener noreferrer">Download a basic guide to the Consumer Protection from Unfair Trading Regulations from the Department for Business, Energy &amp; Industrial Strategy (BIS) website (PDF, 574K)</a>.</p>
		<p>If anyone incurs damage to their private property, is injured or killed as a result of using goods you have supplied you can be sued under product liability law. See our guide on <a href="/workplace-health-and-safety/managing-the-welfare-of-people/product-liability.php" title="Your legal responsibility to make sure your products are sufficiently safe">product liability</a>.</p>
		<p>Your local Trading Standards service monitors and enforces product safety and day-to-day fair-trading practices. <a href="http://webarchive.nationalarchives.gov.uk/20111006195104/https://www.businesscompanion.info/advice/advice-business.cfm" target="_blank" rel="noopener noreferrer">Find your local Trading Standards office on the Trading Standards Institute (TSI) website</a>.</p>
