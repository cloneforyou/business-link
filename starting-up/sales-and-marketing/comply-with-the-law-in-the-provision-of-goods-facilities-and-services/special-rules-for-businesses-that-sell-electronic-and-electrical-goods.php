		<h1>Comply with the law in the provision of goods, facilities and services</h1>
		
		<h2>Special rules for businesses that sell electronic and electrical goods</h2>
		<p>The Waste Electrical and Electronic Equipment (WEEE) Regulations 2006 place responsibilities on users, producers and distributors of electrical and electronic equipment. Under the WEEE regulations you are a distributor if you sell electrical and electronic equipment to consumers.</p>
		<p>You'll need to comply with the WEEE Regulations if you sell:</p>
		<ul>
			<li>household appliances</li>
			<li>IT, telecommunications or audio-visual equipment</li>
			<li>lighting equipment</li>
			<li>electrical and electronic tools</li>
			<li>electronic and battery-operated toys</li>
		</ul>

		<p>This list is not exhaustive. For further information, see the page on <a href="/environment-and-efficiency/waste-legislation-and-responsibility/waste-electrical-and-electronic-equipment-weee/equipment-covered-by-the-weee-regulations.php">equipment covered by the WEEE Regulations</a> in our guide on <a href="/environment-and-efficiency/waste-legislation-and-responsibility/waste-electrical-and-electronic-equipment-weee.php">waste electrical and electronic equipment (WEEE)</a>.</p>
		<p>To fulfil your obligations under the WEEE Regulations you must:</p>
		<ul>
			<li>provide information to consumers on the environmental impacts of electrical and electronic equipment and the reasons for separating WEEE from other waste</li>
			<li>explain the meaning of the crossed-out wheeled bin symbol</li>
			<li>explain how consumers can safely dispose of WEEE free of charge</li>
			<li>display information on the benefits of take-back schemes</li>
			<li>establish an in-store take-back scheme or join the distributor take-back scheme to enable consumers to dispose of their WEEE</li>
		</ul>

		<p>You must keep records of this information for four years.</p>
		<p>If you decide to set up an in-store take-back scheme, you are required to take back the old item from your customer when you sell them an equivalent new item. You must keep records of the amounts and category of items that you receive and keep these records for four years. You must then arrange the removal of the WEEE you collect through a licensed waste carrier or producer compliance scheme.</p>
		<p>Alternatively you can join the distributor take-back scheme. This scheme works through a network of centres where consumers can dispose of their WEEE free of charge. You must inform customers about how and where they can do this.</p>
		<p>See the page on <a href="/environment-and-efficiency/waste-legislation-and-responsibility/waste-electrical-and-electronic-equipment-weee/obligations-of-electrical-and-electronic-equipment-distributors.php">obligations of electrical and electronic equipment distributors</a> in our guide on <a href="/environment-and-efficiency/waste-legislation-and-responsibility/waste-electrical-and-electronic-equipment-weee.php">waste electrical and electronic equipment (WEEE)</a>.</p>
		<p>The distributor take-back scheme is operated by Valpak Retail WEEE Services.</p>
		
