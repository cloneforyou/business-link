		<h1>Comply with the law in the provision of goods, facilities and services</h1>
		
		<h2>Getting paid on time</h2>
		<p>Late payment of bills can be frustrating and can cause serious cashflow problems for businesses of any size.</p>
		<p>The law aims to discourage late payment by allowing businesses to charge each other interest on overdue invoices under the Late Payment of Commercial Debts (Interest) Act 1998.</p>
		<p>Unless your terms and conditions state otherwise, an invoice becomes due within 30 days of the invoice date. If it is not paid within this time you are entitled to charge interest.</p>
		<p>It is not compulsory to charge interest but if you do it should be calculated at the Bank of England base rate plus 8 per cent. You can <a href="http://www.bankofengland.co.uk/" target="_blank" rel="noopener noreferrer" title="Find out the Bank of England base rate">find the current base rate on the Bank of England website</a>.</p>
		<p>To calculate how much interest you are owed, <a href="/finance-and-grants/debt-recovery/calculate-the-interest-due-on-an-unpaid-debt.php">use our interactive tool to calculate statutory or contractual interest you may be able to charge on an unpaid debt</a>.</p>
		<p>The law also allows you to claim <b>compensation</b> for late payment at the following rates:</p>
		<ul>
			<li>&pound;40 for debts up to &pound;999</li>
			<li>&pound;70 for debts between &pound;1,000 and &pound;9,999.99</li>
			<li>&pound;100 for debts more than &pound;10,000</li>
		</ul>
		
		<p>Some firms choose not to charge interest because they are concerned it may alienate important customers. Developing strong credit management procedures can help avoid this conflict of interests.</p>
		<p>And remember - your purchase invoices may also attract interest if you fail to pay them on time.</p>
