		<h1>Comply with the law in the provision of goods, facilities and services</h1>
		
		<h2>Customers and data protection</h2>
		<p>All businesses that hold the details of customers, potential customers, suppliers, staff or any other business contacts are required to comply with the Data Protection Act. The Act applies to any records held electronically - for example on computer - or manually such as in a paper file.</p>
		<p>You may be required to register with the Information Commissioner. <a href="/it-and-ecommerce/data-protection-and-legal-issues/notifying-the-information-commissioner.php">Use our interactive tool to find out whether you need to notify the Information Commissioner about the data your business holds</a>.</p>
		<p>You can <a href="https://ico.org.uk/for-organisations/register/" title="A step-by-step guide on how to notify the ICO about personal information" target="_blank" rel="noopener noreferrer">register with the Information Commissioner on the Information Commissioner's Office (ICO) website</a> or by calling the ICO Helpline on Tel 0303 123 1113.</p>
		<p>The Data Protection Act requires businesses to comply with a number of principles. These include:</p>
		<ul>
			<li>information must only be used for specified and lawful purposes</li>
			<li>businesses should only hold on to information they actually need</li>
			<li>information that is no longer required should be deleted or destroyed as soon as possible</li>
			<li>the information must be accurate and up to date</li>
			<li>the information must be held securely</li>
			<li>businesses must observe the subject's rights</li>
		</ul>

		<p>You can <a href="https://ico.org.uk/for-organisations/guide-to-data-protection/data-protection-principles/" target="_blank" rel="noopener noreferrer">read information on the principles of the Data Protection Act on the ICO website</a>.</p>
		<p>For further information on the implications of the Data Protection Act, read our guide on <a href="/sales-and-marketing/selling-and-the-law/privacy-and-data-protection-in-direct-marketing.php" title="What steps you must take to protect your customers' privacy and data">privacy and data protection in marketing</a>.</p>
		
