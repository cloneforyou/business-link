		<h1>Comply with the law in the provision of goods, facilities and services</h1>
		
		<h2>The Proof of Age Standards Scheme</h2>
		<p>The Proof of Age Standards Scheme (PASS) is the UK's national scheme for proof-of-age cards.</p>
		<p>PASS brings in a common standard and accreditation process to help protect you and your employees when making age-related sales.</p>
		<p>The PASS hologram indicates that the card issuer has passed the rigorous Trading Standards audit process and that you can rely on the card's authenticity.</p>
		
		<h3>How PASS makes life easier for retailers</h3>
		<p>If you are a retailer, PASS has several important benefits for you and your employees. It can help to:</p>
		<ul>
			<li>protect your business from receiving fines for selling age-restricted goods</li>
			<li>protect you from the loss of your premises licence</li>
			<li>clarify which cards are genuine</li>
			<li>give a single recognisable logo you can trust - the PASS hologram is a registered trademark, which means that to forge the hologram is a criminal offence</li>
			<li>make it easier to check the identity of the carrier against the photo and date of birth on the card with the PASS hologram</li>
		</ul>
		
		<p>Almost two million young people hold proof-of-age cards bearing the PASS hologram, and numbers are increasing all the time.</p>
		<p>The following cards are all part of PASS and are available nationally:</p>
		<ul>
			<li>CitizenCard</li>
			<li>VALIDATE UK</li>
			<li>Young Scot</li>
			<li>Proof GB</li>
		</ul>
		
		<p>There are a number of local schemes which produce PASS-accredited cards. <a href="http://www.pass-scheme.org.uk/" title="PASS card issuers, including national and regional schemes" target="_blank" rel="noopener noreferrer">Find a list of local and national schemes issuing PASS-accredited cards on the PASS website</a>.</p>
		
