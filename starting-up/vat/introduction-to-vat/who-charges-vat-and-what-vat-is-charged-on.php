		<h1>Introduction to VAT</h1>
		<h2>Who charges VAT and what VAT is charged on</h2>
		<p>VAT-registered businesses add VAT to the sale price of most goods and services they provide.</p>
		
		<h3>When you must register for VAT</h3>
		<p>If you're a business and the goods or services you provide count as what's known as 'taxable supplies' (see 'What is VAT charged on?' below) you'll have to register for VAT if either:</p>
		<ul>
			<li>your turnover for the previous 12 months has gone over a specific limit - called the 'VAT threshold' (currently &pound;77,000)</li>
			<li>you think your turnover will soon go over this limit</li>
		</ul>
		
		<p>You can choose to register for VAT if you want, even if you don't have to.</p>
		<p><a href="/tax-payroll-and-company-information/vat/how-and-when-to-register-for-vat/how-to-register-for-vat.php">How and when to register for VAT</a></p>
		
		<h3>What is VAT charged on?</h3>
		<p>If you're VAT-registered, you'll have to charge VAT on any goods and services that you provide in the UK that are VAT taxable. You charge VAT on the full sale price, even if you accept goods in part exchange or through barter instead of money. You can find more information about goods and services on which you have to charge VAT in the guide below.</p>
		<p><a href="/tax-payroll-and-company-information/vat/charging-vat-on-your-sales-and-other-supplies/goods-and-services-where-you-have-to-charge-vat.php">Goods and services where you have to charge VAT</a></p>
		
		<h3>How VAT is charged and accounted for</h3>
		<p>If you're VAT-registered, the VAT you add to the sale price of your goods or services is called your 'output tax'. The VAT you pay when you buy goods and services for your business is called your 'input tax'.</p>
		
		<h3>Filling in your VAT Return</h3>
		<p>If you're VAT-registered you'll have to submit a VAT Return at regular intervals - usually quarterly - and send it to HM Revenue &amp; Customs (HMRC). The return shows:</p>
		<ul>
			<li>the VAT you have charged on your sales to your customers in the period - known as output tax</li>
			<li>the VAT you have paid on your purchases - known as input tax</li>
		</ul>
		
		<p>If the amount of output tax is more than the input tax, then you send the difference to HMRC with your return.</p>
		<p>If the input tax is more than your output tax, you claim the difference back from HMRC.</p>
		<p>There are special schemes that some businesses can use to help them work out and pay their VAT.</p>
		<p>You can read about VAT Returns, payments and repayments, VAT accounting schemes and VAT online services in our guides below.</p>
