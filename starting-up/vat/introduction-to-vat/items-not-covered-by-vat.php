		<h1>Introduction to VAT</h1>
		<h2>Items not covered by VAT</h2>
		<p>There are some items that aren't covered by VAT. These items are either:</p>
		<ul>
			<li>exempt</li>
			<li>outside the scope of VAT</li>
		</ul>

		<h3>Exempt items</h3>
		<p>Some items are exempt from VAT because the law says they mustn't have any VAT charged on them. Items that are exempt include the following:</p>
		<ul>
			<li>insurance</li>
			<li>providing credit</li>
			<li>education and training, if certain conditions are met</li>
			<li>fundraising events by charities, if certain conditions are met</li>
			<li>membership subscriptions, if certain conditions are met</li>
			<li>most services provided by doctors and dentists</li>
		</ul>

		<p>Selling, leasing and letting of commercial land and buildings are also exempt from VAT.</p>
		<p>But you can choose - or 'opt' - to give up the right to the exemption and to charge VAT at the standard rate instead. This allows you to reclaim input tax when otherwise you wouldn't be able to. For more information, see the page in this guide on <a href="/starting-up/vat/introduction-to-vat/the-difference-between-exempt-and-zero-rated.php">the difference between exempt and zero-rated</a>.</p>
		<p><a href="https://www.gov.uk/government/publications/vat-notice-70130-education-and-vocational-training" target="_blank" rel="noopener noreferrer" title="Notice 701/30 - VAT on education and vocational training">Find out about VAT exemptions for education in VAT Notice 701/30 on the HM Revenue &amp; Customs (HMRC) website</a></p>
		<p><a href="/tax-payroll-and-company-information/vat/charities-and-other-not-for-profit-organisations-and-vat/how-vat-applies-to-fundraising-events.php">How VAT applies to fundraising events</a>&nbsp;</p>
		<p><a href="https://www.gov.uk/government/publications/vat-notice-742a-opting-to-tax-land-and-buildings" target="_blank" rel="noopener noreferrer" title="Opting to tax land and buildings">Find out about opting to tax land and buildings in VAT Notice 742A on the HMRC website</a></p>
		
		<h3>Outside the scope of VAT</h3>
		<p>There are some things that aren't in the UK VAT system at all - they're outside the scope of VAT. They are not taxable supplies and no VAT is charged on them. Items that are outside the scope of VAT include:</p>
		<ul>
			<li>non-business activities like a hobby - for example, you might sell some stamps from your collection</li>
			<li>fees that are fixed by law - known as 'statutory fees' - for example the congestion charge or vehicle MOT tests</li>
		</ul>
		
