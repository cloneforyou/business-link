		<h1>Introduction to VAT</h1>
		<h2>Rates of VAT</h2>
		<p>There are different VAT rates, depending on the goods or services that are being provided. Currently there are three rates:</p>
		<ul>
			<li>standard rate - 20 per cent</li>
			<li>reduced rate - 5 per cent</li>
			<li>zero rate - 0 per cent</li>
		</ul>

		<p>The standard rate of VAT is the default rate - this is the rate that's charged on most goods and services in the UK unless they're specifically identified as being reduced or zero-rated.</p>
		
		<h3>Examples of reduced-rated items</h3>
		<p>These are some examples of goods and services that may be reduced-rated, depending on the product itself and the circumstances of the sale:</p>
		<ul>
			<li>domestic fuel and power</li>
			<li>installing energy-saving materials</li>
			<li>sanitary hygiene products</li>
			<li>children's car seats</li>
		</ul>

		<p>This isn't a complete list of reduced-rated items and services.</p>
		
		<h3>Examples of zero-rated items</h3>
		<p>These are examples of goods and services that may be zero-rated, depending on the product itself and the circumstances of the sale:</p>
		<ul>
			<li>food - but not meals in restaurants or hot takeaways</li>
			<li>books and newspapers</li>
			<li>children's clothes and shoes</li>
			<li>public transport</li>
		</ul>

		<p>This isn't a full list of zero-rated items.</p>
