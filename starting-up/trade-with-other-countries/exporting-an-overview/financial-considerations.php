		<h1>Exporting - an overview</h1>
		<h2>Financial considerations</h2>
		<p>The delay between the shipping of goods and your receiving payment for them will affect your cashflow.</p>
		<p>It's worth discussing your cash position with your accountant and bank manager before committing to exporting.</p>
		<p>It's also important to insure your business against not being paid in case one of your overseas customers goes out of business. UK Export Finance, run by the government, may be able to offer insurance and advice, depending on the type of export. <a href="https://www.gov.uk/government/organisations/uk-export-finance" target="_blank" rel="noopener noreferrer">Find out about export credits on the UK Export Finance website</a>.</p>
		<p>Other commercial providers can also give information on credit insurance and offer cover. You can <a href="https://www.biba.org.uk/find-insurance/" target="_blank" rel="noopener noreferrer">find a listing through the British Insurance Brokers Association (BIBA) website</a>.</p>
		<p>There are also currency issues you need to consider. Some of your customers could face problems obtaining foreign currency to pay for your exports. In this case, it's worth insisting on a (confirmed) irrevocable letter of credit that secures payments according to the terms of the credit and often at an agreed rate. See our guide on <a href="/international-trade/importing-and-exporting-basics/getting-paid-when-trading-internationally/letters-of-credit.php">letters of credit</a>.</p>
		<p>Businesses that sell on credit to foreign customers can use factoring or invoice discounting to free up cashflow. Export factors specialise in the collection of money from overseas. The factoring company pays you a percentage of the invoice value up-front and the balance (less their percentage) once they have collected payment.</p>
		<p>See our guide on <a href="/international-trade/importing-and-exporting-basics/getting-paid-when-trading-internationally/getting-paid-when-selling-overseas.php">getting paid when selling overseas</a>.</p>
		
		<h3>Trading abroad</h3>
		<p>When arranging your sales contract you should ensure that delivery responsibilities are clearly defined using the Incoterms 2000 terminology and contracts. Read our guide on <a href="/international-trade/importing-and-exporting-basics/getting-paid-when-trading-internationally/international-commercial-contracts-incoterms.php">International Commercial Contracts - Incoterms</a>.</p>
		<p>UK Trade &amp; Investment offers several services to new and experienced exporters which are part of the government's <a href="/solutions-for-business.php">Solutions for Business portfolio</a>.</p>
		<p>
			<p>You can get advice from a member of UK Trade &amp; Investment international trade team. <a href="https://www.contactus.trade.gov.uk/office-finder/" target="_blank" rel="noopener noreferrer">Find your local international trade team on the UK Trade &amp; Investment website</a>.</p>
		</p>
