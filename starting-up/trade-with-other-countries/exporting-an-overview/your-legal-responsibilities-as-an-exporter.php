		<h1>Exporting - an overview</h1>
		<h2>Your legal responsibilities as an exporter</h2>
		<p>You'll need to familiarise yourself with the VAT rules administered by HM Revenue &amp; Customs (HMRC).</p>
		<p>In most cases, exports will be zero-rated for VAT although there are exceptions. Check with the HMRC VAT Helpline on Tel 0845 010 9000 to find out what applies to you.</p>
		<p>Details of any exports you make must be entered on your VAT return. If you have a high level of exports to European Union (EU) member states, you may have to submit more detailed declarations in an Intrastat return. Intrastat thresholds are reviewed annually. The 2011 thresholds are &pound;600,000 for Arrivals and &pound;250,000 for Dispatches.</p>
		<p>Learn more about the Intrastat return in our guide on an <a href="/international-trade/import-and-export-procedures/customs-declarations/intrastat-returns/introduction-to-intrastat.php">introduction to Intrastat</a>.</p>
		<p>For exports outside the EU you must report sales to HMRC electronically using the National Export System (NES) or using the single administrative document (SAD).</p>
		<p>For information on VAT rules, see our section on <a href="/tax-payroll-and-company-information/vat/international-trade-international-visits-and-vat.php">international trade, international visits and VAT</a>.</p>
		<p>HMRC has introduced a new status for businesses - Authorised Economic Operator. While the scheme is not compulsory, companies that meet the requirements can take advantage of simplified customs procedures. See our guide on <a href="/international-trade/authorised-economic-operators-aeos/authorised-economic-operators.php">Authorised Economic Operators</a>.</p>
		<p>New security laws mean that you must declare your goods leaving the EU. If you supply normal export declarations such as the SAD, you will be covered, but if you're not declaring them in the usual way, you must complete an Exit Summary Declaration.</p>
		<p><a href="/_files/eu_guidelines-on-export-and-exit_jan-2011.pdf" target="_blank" rel="noopener noreferrer">Download guidelines on security declarations on export and exit provisions from the Europa website (PDF, 272K)</a>.</p>
		
		<h3>Export controls</h3>
		<p>Certain goods may only be exported following the issue of an export licence. Examples of goods subject to licensing control include fine art, firearms and chemicals. See our <a href="/international-trade/export-control-organisation/beginners-guide-to-export-controls.php">beginners' guide to export controls</a>.</p>
		<p><a href="http://webarchive.nationalarchives.gov.uk/20120729133119/http://webarchive.nationalarchives.gov.uk/20120729133119/http://www.bis.gov.uk/policies/export-control-organisation/eco-training-skills-academy/eco-films" target="_blank" rel="noopener noreferrer">Watch a video about your export control responsibilities and how you can ensure you have the correct licences on the Department for Business, Innovation &amp; Skills (BIS) website</a>.</p>
		
		<h3>Understanding the law</h3>
		<p>As soon as your goods enter another country they become subject to that country's laws.</p>
		<p>You can <a href="http://webarchive.nationalarchives.gov.uk/20120729124841/http://www.ukti.gov.uk/es_es/export/exporting.html" target="_blank" rel="noopener noreferrer">get reports and data on specific countries from the UK Trade &amp; Investment website</a>.</p>
		<p>You can also <a href="https://www.contactus.trade.gov.uk/office-finder/" target="_blank" rel="noopener noreferrer">find your local international trade team on the UK Trade &amp; Investment website</a>.</p>
