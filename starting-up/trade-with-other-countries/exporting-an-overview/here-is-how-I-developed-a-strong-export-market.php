		<h1>Here's how I developed a strong export market</h1>
		<h2>Here's how I developed a strong export market</h2>
		<p>Established in 1965, Halifax Fan Ltd designs and manufactures customised industrial fans. Based in Brighouse, Yorkshire, the company employs 53 people. Managing director Malcolm Staff describes how a joint venture with an Indian manufacturer enabled the company to export to the growing Asian market.</p>
		<p><strong>What I did</strong></p>
		
		<h3>Assess risks and benefits</h3>
		<p>"About two years ago, we identified new sales opportunities in the expanding Asian markets. At the same time, we needed to reduce our cost base to existing customers.</p>
		<p>"We soon established that opening a factory overseas would be difficult and expensive. Our preferred solution was to establish a joint venture with a company who would manufacture the product in their country, driven by UK design and technical know-how. We also wanted the chosen partner to have its own sales force to establish an immediate local market presence.</p>
		<p>"We had many issues to address, most fundamentally how we would retain control and protect our investment. We got advice on planning, plus grant assistance, from Business Link."</p>
		
		<h3>Research the market</h3>
		<p>"We concentrated initially on the Indian market because we saw it as a growth area and English is commonly spoken. Following desktop research we identified 40 potential partners and decided to visit six.</p>
		<p>"Our first visit proved quite difficult, because we hadn't prepared for the different ways in which business is conducted. However, we learnt from the experience and during subsequent visits we became more relaxed while remaining focused on what we wanted. For example, it's easy to turn your nose up at a factory that looks more like a barn, but the quality of workmanship can soon change your view!</p>
		<p>"Having met all six during the initial trip we made our final selection during the second."</p>
		
		<h3>Choose the right partner</h3>
		<p>"While cost was an important factor in choosing a partner, we looked at all aspects of the shortlisted companies, including financial history and quality assurance. Once we'd selected a preferred partner we arranged for key Indian employees to visit our UK operation. It's important to build trust and mutual understanding.</p>
		<p>"Having a local presence in India has really given us an edge over competitors because we have representation on the ground. We have won a number of export contracts because of it."</p>
		<p><strong>What I'd do differently</strong></p>
		
		<h3>Talk to other exporters</h3>
		<p>"Looking back, I wish we'd contacted businesses who'd already been through the process, perhaps via Business Link or our local Chamber of Commerce. We could have learnt a lot from their experience."</p>
		
		<h3>Find a local lawyer</h3>
		<p>"It would have saved time and hassle at the agreement stage if we'd sourced a local lawyer during our trade visits. We left it until we were entering final negotiations, which wasn't ideal."</p>
		
