		<h1>Exporting - an overview</h1>
		<h2>Working effectively with different cultures</h2>
		<p>You'll need to understand the culture of your target markets to establish a successful relationship with your potential customers.</p>
		<p>Being able to speak the language of your potential customers can help to establish mutual confidence. If you don't speak the local language, you could consider investing in foreign language training for your staff. Alternatively you could employ a translator or interpreter.</p>
		<p>It can also help to have your promotional material translated. However, it's a good idea to avoid colloquialisms and metaphors in promotional material - they could be embarrassing in the local language.</p>
		<p><a href="http://webarchive.nationalarchives.gov.uk/20120418184810/http://www.ukti.gov.uk/export/howwehelp.html" target="_blank" rel="noopener noreferrer">Get help with cultural and language issues on the UK Trade &amp; Investment website</a>.</p>
		<p>For more advice on conducting business with another language, see our guide on <a href="/international-trade/doing-business-in-the-eu/doing-business-in-another-language.php">doing business in another language</a>.</p>
		
		<h3>Research</h3>
		<p>You should conduct research into your target market to establish <b>local considerations</b>. These may include product or packaging modifications to enable your product to conform to local cultural demands. Or it could be that local sales and marketing channels for your particular product are different from those in the UK - mail order in one country, shops in the other, for example.</p>
		<p>See our guide on <a href="/international-trade/importing-and-exporting-basics/overseas-supplier-relationships/entering-overseas-markets.php">researching and entering overseas markets</a>. You can also <a href="https://www.gov.uk/world" target="_blank" rel="noopener noreferrer">read reports and data on specific countries on the UK Trade &amp; Investment website</a>.</p>
		<p>You can also <a href="https://www.contactus.trade.gov.uk/office-finder/" target="_blank" rel="noopener noreferrer">find your local international trade team on the UK Trade &amp; Investment website</a>.</p>
		<p><a href="/international-trade/doing-business-in-the-eu/getting-ready-to-export-to-individual-countries.php">Use our interactive tool to identify what you need to know to export to your target country</a>.</p>
		
