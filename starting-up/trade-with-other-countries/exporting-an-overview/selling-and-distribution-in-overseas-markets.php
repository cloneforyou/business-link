		<h1>Exporting - an overview</h1>
		<h2>Selling and distribution in overseas markets</h2>
		<p>There are a number of elements you need to consider to sell successfully overseas. How you organise your <b>sales presence</b> in export markets is one of the key decisions.</p>
		<p>Depending on your product, you may be able to sell directly. For example, you might be able to sell over the internet or by exhibiting at local trade shows.</p>
		<p>Many businesses look for a <b>partner</b> who already understands the local market. For example:</p>
		<ul>
			<li>You can sell to a <b>distributor</b> who then sells your products locally.</li>
			<li>You can use a <b>sales agent</b> who sells products on your behalf, or puts you into contact with potential customers on a commission basis.</li>
			<li>You can enter into a <b>joint venture</b> with a local business. This gives you a share of the management and profits of the joint venture, but is a more complicated and expensive option.</li>
			<li>If you want complete control over sales, you can set up your own <b>local office</b>. This is the most expensive option.</li>
		</ul>

		<p>The choice you make can have important financial and legal consequences. See our guide on selling and promotion overseas.</p>
		<p>You can <a href="https://www.contactus.trade.gov.uk/office-finder/" target="_blank" rel="noopener noreferrer">find your local International Trade Team on the UK Trade &amp; Investment website</a>.</p>
		<p>The UK Trade &amp; Investment Accessing International Markets programme can provide support and help in planning your entry into new overseas markets. <a href="http://webarchive.nationalarchives.gov.uk/20120418184810/http://www.ukti.gov.uk/export/howwehelp.html" target="_blank" rel="noopener noreferrer">Find out about growing your business internationally on the UK Trade &amp; investment website</a>.</p>
		<p>When arranging a sales contract with an agent or distributor, you need to ensure that responsibility for delivery and payment is clearly defined.</p>
		<p>It's also important to remember that intellectual property (IP) protection becomes more complicated if you sell goods overseas. Patents and trade marks are only recognised and protected in the country of origin, so you will need to secure IP protection in each country you intend to sell into. For more information, read our guide to <a href="/sales-and-marketing/selling-overseas.php">intellectual property protection overseas</a>.</p>
		<p><a href="/international-trade/doing-business-in-the-eu/getting-ready-to-export-to-individual-countries.php">Use our interactive tool to identify what you need to know to export to your target country</a>.</p>
		
