		<h1>Exporting - an overview</h1>
		<h2>Planning to export?</h2>
		<p>Before you commit to exporting you need to honestly assess your <b>export potential</b> - both in terms of the readiness of your business and of your product or service.</p>
		<p>It's essential to carry out detailed <b>market research</b> to identify and evaluate the target market. Examine:</p>
		<ul>
			<li>the industry structure</li>
			<li>the predicted demand for your product or service</li>
			<li>competition and how you plan to fit into that marketplace</li>
			<li>any modifications required to make your product or service saleable</li>
		</ul>

		<p>The UK Trade &amp; Investment Accessing International Markets programme can provide support and help in planning your entry into new overseas markets, including market research. <a href="http://webarchive.nationalarchives.gov.uk/20120418184810/http://www.ukti.gov.uk/export/howwehelp.html" target="_blank" rel="noopener noreferrer">Find out about growing your business internationally on the UK Trade &amp; investment website</a>.</p>
		<p>After completing your market research, you can draw up an <b>export plan</b> defining how you will enter the new market.</p>
		<p>Consider whether you have:</p>
		<ul>
			<li>a marketing strategy that incorporates international trade development</li>
			<li>the necessary financial resources</li>
			<li>the right people to develop the new export markets</li>
			<li>adequate knowledge of the requirements of your chosen market - eg modifying packaging to meet local regulations and standards</li>
			<li>an understanding of export payment mechanisms and export finance</li>
		</ul>

		<p>Then assess whether your product is <b>suitable</b> for export. Consider:</p>
		<ul>
			<li>product standards and regulations in the overseas market - the British Standards Institution offers help for exporters</li>
			<li>the costs of adapting your product or service</li>
		</ul>

		<p>UK Trade &amp; Investment offers practical help with your ability to export. See the page in this guide on <a href="/starting-up/trade-with-other-countries/exporting-an-overview/helping-your-business-grow-internationally.php">Developing Your International Trade Potential</a>.</p>
