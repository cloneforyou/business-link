		<h1>Preparing to export</h1>
		<h2>Use the right export support services</h2>
		<p>UK Trade &amp; Investment offers a range of services for UK exporters, including market information and help breaking into new markets. Many of these services are free. <a href="http://webarchive.nationalarchives.gov.uk/20120418184810/http://www.ukti.gov.uk/export/howwehelp.html" target="_blank" rel="noopener noreferrer">Find out about UK Trade &amp; Investment services on the UK Trade &amp; Investment website</a>.</p>
		<p>The British Chambers of Commerce (BCC) also offer export services such as export training and help with export documentation. <a href="http://www.britishchambers.org.uk/business/trading-internationally/" target="_blank" rel="noopener noreferrer">Find export information and advice on the BCC website</a>.</p>
		<p>You may want help with foreign languages, eg translating marketing brochures or using an interpreter to help at meetings. <a href="https://web.archive.org/web/20141026164609/http://www.cilt.org.uk/home.aspx" target="_blank" rel="noopener noreferrer">Find help with language and cultural issues on the National Centre for Languages website</a>.</p>
		<p>The UK Trade &amp; Investment Accessing International Markets programme can provide support and help in planning your entry into new overseas markets. <a href="http://webarchive.nationalarchives.gov.uk/20120418184810/http://www.ukti.gov.uk/export/howwehelp.html" target="_blank" rel="noopener noreferrer">Find out about growing your business internationally on the UK Trade &amp; investment website</a>.</p>
		<p>You may also want help in several other areas:</p>
		<ul>
			<li>An experienced lawyer can help with contract negotiation. See our guide on how to <a href="/finance-and-grants/expert-financial-advice/choose-and-work-with-a-solicitor.php">choose and work with a solicitor</a>.</li>
			<li>Your bank can advise you on payment methods, financing and handling foreign exchange.</li>
			<li>A freight forwarder can handle transport. See our guide to <a href="/international-trade/preparing-your-goods-for-transport/international-transport-and-distribution.php">international transport and distribution</a>.</li>
			<li>You may want to use a broker to find the right insurance. See our guide on <a href="/international-trade/importing-and-exporting-basics/managing-risks-of-importing-and-exporting/insurance-for-international-trade.php">insurance for international trade</a>.</li>
		</ul>
		<p>For more information on government support services, see our guide on <a href="/international-trade/importing-and-exporting-basics/licensing-and-paperwork/government-help-for-exporters.php">government help for exporters</a>.</p>
		
