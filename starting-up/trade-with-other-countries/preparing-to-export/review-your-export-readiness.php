		<h1>Preparing to export</h1>
		<h2>Review your export readiness</h2>
		<p>You can use these points to help you decide whether you are ready to start exporting or what else you need to do to prepare.</p>
		
		<h3>Planning and strategy</h3>
		<ul>
			<li>Do you have a well-defined export strategy and a thorough plan? How will you start exporting, and how will you develop your export business?</li>
			<li>Do you have the resources you need to carry out your plan, eg finances, personnel, production?</li>
			<li>How will you build your export skills?</li>
			<li>How much do you expect to invest, and what return do you expect?</li>
			<li>Are you committed to exporting? Have all the key people in your business agreed?</li>
		</ul>
		
		<h3>Marketing</h3>
		<ul>
			<li>Do you know what markets you will target?</li>
			<li>How much research have you done into your target market? Do you understand the business culture and legal environment? Read our guide on <a href="/international-trade/importing-and-exporting-basics/overseas-supplier-relationships/entering-overseas-markets.php">researching and entering overseas markets</a>.</li>
			<li>What is the market for your product? Who are you competing against and what do they offer?</li>
			<li>Will your product need to be modified to meet local regulations or customer requirements?</li>
			<li>How will you position your product? What price will you charge? What currency will you price and sell your products in?</li>
			<li>How will you market and promote your product? What distribution channels will you use? Read our guide on selling and promotion overseas.</li>
		</ul>
		
		<h3>Export procedures</h3>
		<ul>
			<li>What contracts will you aim to negotiate? What are your preferred Incoterms and will these suit your customers? Read our guide on <a href="/international-trade/importing-and-exporting-basics/getting-paid-when-trading-internationally/international-commercial-contracts-incoterms.php">International Commercial Contracts - Incoterms</a>.</li>
			<li>What payment method and terms will you offer? Do you have the skills to handle any specialist methods such as letters of credit? Will you require a foreign currency bank account or accounting software that can handle foreign currency transactions?</li>
			<li>How will you transport your products? Will you use a freight forwarder? See our section on <a href="/international-trade/transport-options-for-moving-your-goods.php">transporting your goods</a>.</li>
			<li>Have you identified all the costs, such as transport, insurance, duties and taxes? Will your export business be profitable after taking these into account?</li>
		</ul>
		
