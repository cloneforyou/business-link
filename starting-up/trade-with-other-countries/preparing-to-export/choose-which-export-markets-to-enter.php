		<h1>Preparing to export</h1>
		<h2>Choose which export markets to enter</h2>
		<p>Different markets often have different requirements. For example, product modifications that suit customers in China might be the opposite of what customers in Canada want.</p>
		<p>Trying to export to several different countries can be very expensive. Unless you tailor what you offer to suit each individual market, you may fail to offer what customers really want.</p>
		<p>Instead, it's usually best to focus on selling to one or two individual markets. In large countries like the US, you might even concentrate on a particular region, and roll out gradually across the country as you build your market presence.</p>
		<p>
			<p>Your nearest International Trade adviser help you choose the most appropriate market for your product. <a href="https://www.contactus.trade.gov.uk/office-finder/" target="_blank" rel="noopener noreferrer">Find your local international trade team on the UK Trade &amp; Investment website</a>.</p>
		</p>
		<p>Many new exporters choose to start by exporting to markets that are relatively easy to deal with. For example, the practicalities of dispatching goods to countries within the European Union (EU) are relatively straightforward. Many trading practices, regulations and standards apply throughout the EU, and key tasks such as accounting for VAT have been simplified. This can be a good way of building your export skills.</p>
		<p>Or you might choose to target a market where you have some links - eg through your family, personal contacts or employee knowledge.</p>
		<p>Developing countries can be riskier to deal with. The risks are generally lower if the country has an investment protection and promotion agreement with the UK. <a href="https://www.gov.uk/government/policies/free-trade" target="_blank" rel="noopener noreferrer">Find information on investment promotion and protection agreements on the Gov website</a>.</p>
		<p>Ultimately, it's up to you to decide which markets offer the best opportunities and suit your own export strategy. For more information on identifying potentially promising markets, see the page in this guide on how to <a href="/starting-up/trade-with-other-countries/preparing-to-export/assess-export-market-opportunities.php"">assess export market opportunities</a>.</p>
		
