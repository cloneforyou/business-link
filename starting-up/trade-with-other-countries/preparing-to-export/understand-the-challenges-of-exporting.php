		<h1>Preparing to export</h1>
		<h2>Understand the challenges of exporting</h2>
		<p>Exporting isn't simply an add-on to your existing business. It should be part of an overall strategy to develop the business.</p>
		<p>Before you start exporting, it's worth making sure you have developed a complete export plan looking at all the costs and risks involved:</p>
		<ul>
			<li>Exporting presents all the normal challenges of <strong>marketing</strong> in the UK - it's up to you to find customers and convince them to buy from you. Understanding what customers want and how the market operates is vital. See our guide on <a href="/international-trade/importing-and-exporting-basics/overseas-supplier-relationships/entering-overseas-markets.php">researching and entering overseas markets</a>.</li>
			<li>You need to cope with extra logistical problems,<strong> contractual issues and paperwork</strong>. You'll probably want a contract drawn up using internationally recognised terms and conditions and standard commercial practices to make it clear what your responsibilities are. There's also a range of paperwork for sorting out transport, customs clearance and payments. See our guide on <a href="/international-trade/importing-and-exporting-basics/licensing-and-paperwork/international-trade-paperwork-the-basics.php">international trade paperwork: the basics</a>.</li>
			<li>If you are VAT registered, you must provide details of all your transactions with other European Union (EU) member states on your VAT return. If the value of goods you buy from or sell to other EU countries reaches the sales or purchases Intrastat threshold during the course of a calendar year you must submit additional monthly supplementary declarations to HM Revenue &amp; Customs. Intrastat thresholds are reviewed annually. The 2011 thresholds are &pound;600,000 for Arrivals and &pound;250,000 for Dispatches. See our guide: <a href="/international-trade/import-and-export-procedures/customs-declarations/intrastat-returns/introduction-to-intrastat.php">introduction to Intrastat</a>.</li>
			<li>You need to <strong>comply with regulations</strong> in both the UK and overseas. For example, some goods that are allowed in the UK might not satisfy another country's standards or even be legal there.</li>
			<li>Exporting demands additional <strong>resources</strong>, both in terms of financing and skilled personnel. See the page in this guide on how to <a href="/starting-up/trade-with-other-countries/preparing-to-export/assess-your-skills-and-resources-for-exporting.php">assess your skills and resources for exporting</a>. It can also present extra risks. See our guide on how to <a href="/international-trade/importing-and-exporting-basics/managing-risks-of-importing-and-exporting/manage-the-risks-of-exporting.php">manage the risks of exporting</a>.</li>
		</ul>

		<p>With the additional costs, such as international transport, you may find you simply can't compete with local suppliers. If the market only offers low margins, or you haven't got the resources you need, you may decide that exporting isn't for you.</p>
		<p>But if you have got a good product to offer and a well-run business, the chances are there are opportunities out there. If the rewards you expect justify the investment and the risks, you should commit to your export plan and make it happen.</p>
		
