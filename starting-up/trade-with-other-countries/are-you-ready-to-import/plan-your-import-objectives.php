		<h1>Are you ready to import?</h1>
		<h2>Plan your import objectives</h2>
		<p>Before you start importing, it's a good idea to be clear about what you are trying to achieve. You might want to find a cheaper source of supplies overseas, or to import products that aren't yet available in the UK to sell to your customers.</p>
		<p>At the same time, importing should fit in with your overall <strong>business strategy</strong>. For example, importing low cost goods might be part of a cost-control strategy, but you want to be sure that this does not compromise your reputation for quality.</p>
		<p>It's worth thinking about how much time, money and effort you want to devote to setting up and managing your importing activities. You are unlikely to want to invest heavily for a small, one-off import. But if importing is going to be an important part of your business, you might be prepared to spend more on finding the best suppliers, building relationships and so on. For more information, see the page in this guide on how to <a href="/starting-up/trade-with-other-countries/are-you-ready-to-import/understand-what-importing-involves.php">understand what importing involves</a>.</p>
		<p>You also need to decide how much <strong>involvement</strong> you want to have in the import process. Importing can be fairly straightforward if you only deal with experienced suppliers who handle most of the paperwork and logistics, and get help from an import agent. On the other hand, dealing with less experienced suppliers and handling more of the process yourself could be more time consuming but also more profitable. See the page in this guide on how to <a href="/starting-up/trade-with-other-countries/are-you-ready-to-import/decide-your-approach-to-importing.php">decide your approach to importing</a>.</p>
		<p>Whichever approach you choose, you should ensure that all the key people in your business agree and are committed to it.</p>

