		<h1>Are you ready to import?</h1>
		<h2>Assess your skills and resources for importing</h2>
		<p>Importing requires special skills and extra resources. Before you start importing, you should assess whether you have the right skills and resources and decide how to cope with any shortcomings.</p>
		<p>You need to understand UK customs procedures and ensure you have the right paperwork when deliveries arrive in the UK. International trade often uses payment methods, such as letters of credit, that require an understanding of payment documentation. If you agree to pay in a foreign currency, you also need to be able to handle foreign exchange. You will probably need new <strong>administrative systems</strong> to track deliveries and payments and manage the paperwork. </p>
		<p>If you do not have the skills in house, you may want to invest in <strong>training</strong>. Or you could use someone else - such as an import agent - to handle specialist areas. You may also need extra personnel to handle the additional workload.</p>
		<p>Alternatively, you might decide to only deal with suppliers who handle most of the procedures. Many first time or small scale importers prefer to keep things simple. See the page in this guide on how to <a href="/starting-up/trade-with-other-countries/are-you-ready-to-import/decide-your-approach-to-importing.php">decide your approach to importing</a>.</p>
		
		<h3>Financial resources</h3>
		<p>As well as skills, you need to think about what resources you can devote to importing. Researching overseas markets and managing overseas suppliers can be time-consuming and expensive, particularly if you need to visit them. See our guide on how to <a href="/international-trade/importing-and-exporting-basics/overseas-supplier-relationships/manage-overseas-suppliers.php">manage overseas suppliers</a>.</p>
		<p>If your finances are limited, you may prefer to deal with suppliers who offer credit. On the other hand, agreeing to pay promptly could help you negotiate a competitive price.</p>
		<p>Make sure you have planned how you are going to use the imports, and have the skills and resources you need in place. For example, you might need production capacity for processing imported components, or a marketing plan and sales resources for selling imports on to your customers.</p>
		
