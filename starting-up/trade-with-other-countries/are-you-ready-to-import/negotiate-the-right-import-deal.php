		<h1>Are you ready to import?</h1>
		<h2>Negotiate the right import deal</h2>
		<p>Understanding your own strengths and weaknesses, and what your supplier's priorities are, helps you negotiate the best deal. For example, if you have a healthy cash position, you could offer to pay more promptly in return for a good price.</p>
		<p>The best way to split responsibility for delivery and customs clearance depends on your skills and what third parties you use. It can be more cost-effective for you (or your agent) to handle UK customs clearance and onwards delivery to your premises. Before you finalise the contract, make sure you can handle everything involved.</p>
		<p>Try to plan ahead, anticipating what could go wrong and working out how you could deal with it. Even if the supplier is responsible for something, or you use an agent, you could still have problems if things go wrong, eg if a delivery doesn't arrive and you can't supply your own customers. See our guide on how to <a href="/international-trade/importing-and-exporting-basics/managing-risks-of-importing-and-exporting/manage-the-risks-of-importing.php">manage the risks of importing</a>.</p>
		<p>Whatever you negotiate, it's important to have a <strong>clear contract</strong> setting out exactly what payment and delivery terms you have agreed. Using internationally agreed <strong>Incoterms</strong> (International Commercial Terms) helps reduce the risk of delivery problems or misunderstandings. The contract should also cover what payment is required, when and in what currency, and what payment method will be used. See our guide to <a href="/international-trade/importing-and-exporting-basics/licensing-and-paperwork/international-trade-paperwork-the-basics.php">international trade paperwork: the basics</a>. You may want to take advice from a lawyer with experience of international trade.</p>
		<p>Imports tend to run most smoothly if you build a good relationship with your suppliers. See our guide on how to <a href="/international-trade/importing-and-exporting-basics/overseas-supplier-relationships/manage-overseas-suppliers.php">manage overseas suppliers</a>.</p>
		
