		<h1>Starting a business from home</h1>
		<h2>Popular home business ideas</h2>
		<p>If you do not yet have a home business idea, ask yourself three questions:</p>
		<p><strong>Is there a gap in the market?</strong> Have you tried to buy something that you just can't find? In which case, others may be looking for that product too.</p>
		<p><strong>What is my passion/skill/hobby?</strong> Can you find a way of making a living from it?</p>
		<p><strong>Can I do something better?</strong> Have you seen someone offer a product or service that you think you can offer better yourself?</p>
		<h3>Popular home business ideas</h3>
		<p>Consider opportunities in your local area for services such as child care, tutoring, translation, editorial services, dog walking and alteration services.</p>
		<p>Other common home-based businesses include:</p>
		<ul>
			<li>consultants</li>
			<li>website designers</li>
			<li>arts and craft makers</li>
			<li>publishers</li>
			<li>caterers</li>
			<li>virtual assistants who provide professional assistance to clients from a home office</li>
		</ul>
		<p>You could also consider investing in a franchise. The benefits of being a franchisee are that you are your own boss but also have the benefit of working with a central team. There are a growing number of home-based franchise opportunities that you can explore.</p>
		<p>If you need more help developing your home business idea, see our guide <a href="/starting-up/considering-starting-up/research-and-develop-your-business-ideas-new-products-and-services.php">research and develop your business ideas</a>.</p>
		<h3>Working 'five to nine'</h3>
		<p>You may want to consider starting a business whilst in full or part-time employment, using the evenings and weekends to build your business. This means that you will have money coming in whilst your business finds its feet. However, you might find it difficult to manage the extra hours and the extra work.</p>
		<h3>Your business plan</h3>
		<p>Once you have worked out a business idea, you need to create a written business plan. For more information, see our videos and e-learning resources on how to <a href="https://www.gov.uk/write-business-plan" target="_blank" rel="noopener noreferrer">create your business plan</a>.</p>
		
