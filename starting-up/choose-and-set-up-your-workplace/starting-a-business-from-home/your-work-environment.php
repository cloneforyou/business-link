		<h1>Starting a business from home </h1>
		<h2>Your work environment</h2>
		<p>It is important to create dedicated work space - it allows you to work without distraction and close the door on work at the end of the day.</p>
		<h3>Workspace priorities</h3>
		<p>The area that you use for work should ideally be clearly marked as such. A spare room with a lockable door or an outbuilding are popular choices enabling home business owners to:</p>
		<ul>
			<li>deal with clients in a professional manner</li>
			<li>resist demands from other members of the household</li>
			<li>keep work equipment separate from home equipment and therefore protected</li>
		</ul>
		<p>Allocating part of your house as a workplace can have tax and insurance implications. For more information, see the page on <a href="/premises-and-property/choosing-the-right-premises/use-your-home-as-a-workplace/tax-considerations-of-working-from-home.php">tax considerations of working from home</a> in our guide on how to <a href="/premises-and-property/choosing-the-right-premises/use-your-home-as-a-workplace.php">use your home as a workplace</a>.</p>
		<p>You can create a space to call your own by:</p>
		<ul>
			<li>using office equipment in a general area of the house and putting it away when not in use</li>
			<li>housing your workstation in a cupboard, wardrobe or under-stair space with lockable shutters or doors</li>
		</ul>
		<h3>Work equipment and workstation setup</h3>
		<p>When setting up your work area:</p>
		<ul>
			<li>your chair and desk should be fully adjustable and suited for computer use</li>
			<li>your desk should be big enough for your computer, keyboard and monitor</li>
			<li>use storage to keep office clutter free</li>
			<li>when sitting at your desk, keep your feet flat on the floor and your back straight - the top of your monitor should be at eye level</li>
		</ul>
		<p>For more information, see the page on <a href="/premises-and-property/choosing-the-right-premises/use-your-home-as-a-workplace/work-equipment-and-workstation-setup.php">work equipment and workstation setup</a> in our guide on how to <a href="/premises-and-property/choosing-the-right-premises/use-your-home-as-a-workplace.php">use your home as a workplace</a>.</p>
		<p>You must carry out a health and safety risk assessment on your work space. For more information, see our guides on how to <a href="/workplace-health-and-safety/managing-the-risks-in-your-business.php">manage the risks in your business</a> and <a href="/workplace-health-and-safety/what-workplace-facilities-do-you-need-to-provide.php">what workplace facilities do you need to provide?</a></p>
		<h3>Home business and planning permission</h3>
		<p>If you are making substantial changes to your home to accommodate your business you should contact your local authority regarding planning permission. For more information, see the page on <a href="/premises-and-property/choosing-the-right-premises/use-your-home-as-a-workplace/building-regulations-and-planning-permission-for-home-businesses.php">building regulations and planning permission for home businesses</a> in our guide on how to <a href="/premises-and-property/choosing-the-right-premises/use-your-home-as-a-workplace.php">use your home as a workplace</a>.</p>
		
