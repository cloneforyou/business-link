		<h1>Starting a business from home </h1>
		<h2>Record keeping</h2>
		<p>It is vital to keep full and accurate records of your income and expenses from the start. Keeping records makes sound business sense and is a legal requirement. So it is important to get a proper system in place from the outset, and update the information regularly.</p>
		<p>Keeping good records helps you:</p>
		<ul>
			<li>complete your tax returns easier and quicker</li>
			<li>pay the right tax at the right time</li>
			<li>avoid paying unnecessary interest and penalties</li>
		</ul>
		<p>You should keep invoices and receipts to show what you have bought or sold relating to your business.</p>
		<p>If you are employing others, you must keep records of their wages and tax and National Insurance you have deducted and paid to HM Revenue &amp; Customs (HMRC).</p>
		<p>Keeping bank statements and building society books is vital, especially if you don't have a separate business account. You should be able to show clearly what you have spent personally and what is spent on business. If you use cash, you will need till receipts and a record book to keep track of it all.</p>
		<p>If you are using part of your home for business then you should keep copies of the utility bills so that you can work out the amount spent in relation to your business.</p>
		<p>If you have an accountant you might want to get their advice on what system suits your business and on how to keep your records up to date.</p>
		<p>Keeping records is important as penalties were introduced in April 2009 for not taking reasonable care with records and tax returns.</p>
		<p>For more information about taking reasonable care and the new penalties, <a href="../../../_files/hmrc_take-care-to-avoid-a-penalty.pdf" target="_blank" rel="noopener noreferrer">download guidance on taking care to avoid tax penalties from the HMRC website (PDF, 265K)</a>.</p>
		
