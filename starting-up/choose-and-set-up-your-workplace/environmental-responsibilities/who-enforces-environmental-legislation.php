			<h1>Environmental responsibilities</h1>
			<h2>Who enforces environmental legislation?</h2>
			<p>There are three main environmental regulators in the UK:</p>
			<ul>
				<li>the Environment Agency in England and Wales</li>
				<li>the Northern Ireland Environment Agency</li>
				<li>the Scottish Environment Protection Agency</li>
			</ul>

			<p>The environmental regulators have produced the NetRegs website, which provides free environmental guidance. You can <a href="http://webarchive.nationalarchives.gov.uk/20140328084622/http://www.environment-agency.gov.uk/netregs/" target="_blank" rel="noopener noreferrer">find guidance on environmental legislation and good practice on the NetRegs website</a>.</p>
			<p>You have the right to appeal against environmental regulators' decisions.</p>
			<p>Your local authority is also responsible for regulating some environmental issues such as:</p>
			<ul>
				<li>air quality</li>
				<li>statutory nuisance problems such as air pollution, noise, odour and light pollution</li>
				<li>land contamination</li>
				<li>environmental health</li>
			</ul>

			<p>See our guide on <a href="/environment-and-efficiency/sources-of-environmental-advice/local-authority-services-an-overview.php">local authority services: an overview</a>.</p>
			<p>Your local authority can also help you achieve compliance with various aspects of environmental legislation. <a href="http://webarchive.nationalarchives.gov.uk/20120823131012/http://online.businesslink.gov.uk/bdotg/action/findcontactbrowse?topicId=1074537118" target="_blank" rel="noopener noreferrer">Find your local authority using our Contacts Directory</a>.</p>
			
