			<h1>Environmental responsibilities</h1>
			<h2>Hazardous substances covered by environmental legislation</h2>
			<p>Specific environmental rules cover potentially dangerous substances. Every business needs to think about the risks to people or the environment posed by chemicals or substances classified as hazardous to health under the Chemicals (Hazard Information and Packaging for Supply) Regulations (CHIP).</p>
			<p>Hazardous substances are tightly regulated. They include:</p>
			<ul>
				<li>animal by-products</li>
				<li>chemicals</li>
				<li>oil</li>
				<li>ozone-depleting substances (ODS)</li>
				<li>pesticides and biocides</li>
				<li>radioactive materials</li>
				<li>hazardous substances in electrical and electronic equipment</li>
				<li>solvents</li>
			</ul>
			
			<p>You must consider the environmental risks for every hazardous substance you store, use, produce or dispose of at work. You must ensure you control any potential risks and comply with legislation when storing goods and materials. See our guide on <a href="/environment-and-efficiency/managing-hazardous-substances/hazardous-substances-and-the-environment-the-basics.php">hazardous substances and the environment - the basics</a>.</p>
			<p>The CHIP Regulations have been amended to bring them into line with regulations in force in the European Union. The aim is to eventually introduce a globally recognised system for the classification and labelling of hazardous chemicals - at which point the CHIP Regulations will be repealed. For more information see our guide on <a href="/international-trade/manufactured-goods/chemicals.php">chemicals</a>.</p>
			<p>If you manufacture, use, import or recover from waste materials any chemicals, you may have to comply with the Registration, Evaluation and Authorisation of Chemicals (REACH) Regulations. Businesses manufacturing, <strong>recovering</strong> or importing any chemicals over 1 tonne per year must ensure those chemicals have been pre-registered or registered <strong>in full</strong>. Read our guide on <a href="/environment-and-efficiency/chemical-industry/registration-evaluation-authorisation-and-restriction-of-chemicals-reach.php">registration, evaluation, authorisation and restriction of chemicals (REACH)</a>.</p>
			<p>If you manufacture electrical and electronic equipment you must comply with the Restriction of the Use of Certain Hazardous Substances in Electrical and Electronic Equipment Regulations 2008 (RoHS). Under RoHS, manufacturers are prevented from placing products on the market with more than a certain amount of hazardous substances such as lead, mercury and cadmium. See our guide on <a href="/environment-and-efficiency/electrical-and-electronic-equipment-manufacturing/restriction-of-the-use-of-certain-hazardous-substances-in-electrical-and-electronic-equipment-rohs.php">restriction of the use of certain hazardous substances in electrical and electronic equipment (RoHS)</a>.</p>
			<p>If you work with equipment containing ODS or fluorinated gases - including air conditioning and refrigeration equipment - there are requirements that you must meet regarding:</p>
			<ul>
				<li>containment including prevention and repair of leaks, checking for leakages and record keeping</li>
				<li>recovery for the purpose of recycling, reclamation or destruction</li>
				<li>training and certification</li>
			</ul>

			<p>Regulated work includes the disposal, maintenance, servicing and dismantling of refrigerators, freezers, air-conditioning equipment and heat pumps and the decommissioning of fire protection equipment and extinguishers.</p>
			<p>Read our guide on how to <a href="/environment-and-efficiency/managing-hazardous-substances/managing-fluorinated-gases-and-ozone-depleting-substances.php">manage fluorinated gases and ozone-depleting substances</a>.</p>
			
