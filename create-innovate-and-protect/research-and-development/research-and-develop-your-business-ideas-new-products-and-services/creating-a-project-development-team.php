		<h1>Research and develop your business ideas, new products and services</h1>
		<h2>Creating a project development team</h2>
		<p>Every potential new product or service needs a dedicated development team.</p>
		<p>In creating your team you need to include people with a variety of skills. For example, you may need a creative ideas person, a technical expert, a marketing specialist, someone in charge of market research who understands customer needs, someone who can source components and someone who understands the supply-chain difficulties you could encounter.</p>
		<p>All team members should understand your business' objectives and be committed to them.</p>
		<p>There are many forms of effective teamworking and the right one for you will depend on your business' needs. For example, team members might:</p>
		<ul>
			<li>work exclusively on one project in a single department, reporting to a project manager</li>
			<li>work exclusively on one project but remain in separate departments reporting to department heads who are under the project manager</li>
			<li>work on several projects at once with both a department head and project manager to monitor progress</li>
		</ul>
		<p>Teams need someone in a project management role to lead, co-ordinate and motivate the team - see the page in this guide on how to <a href="/create-innovate-and-protect/research-and-development/research-and-develop-your-business-ideas-new-products-and-services/manage-a-development-project.php">manage a development project</a>.</p>
		<p>For more information on working with formal project management systems for the first time, see our guide on <a href="/grow-your-business/planning-how-to-grow/project-management-the-basics.php">project management - the basics</a>.<br />
		</p>

