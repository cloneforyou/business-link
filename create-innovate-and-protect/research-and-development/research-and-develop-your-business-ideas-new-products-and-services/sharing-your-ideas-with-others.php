		<h1>Research and develop your business ideas, new products and services</h1>
		<h2>Sharing your ideas with others</h2>
		<p>At some stage you will probably wish to discuss your idea with a third party. But once you put an idea into the public domain it can no longer be considered confidential or a trade secret. So you need to take steps to protect your intellectual property.</p>
		<p>Before talking to third parties, it is a good idea to ask them to sign a non-disclosure or confidentiality agreement - to prevent them from sharing details of your idea with others. See our guide on <a href="/create-innovate-and-protect/protecting-your-intellectual-property/patent-protection-and-your-business/non-disclosure-agreements.php">non-disclosure agreements</a>.</p>
		<h3>Intellectual property (IP)</h3>
		<p>All businesses have IP. Your business' IP is what sets it apart from its competitors. For example, it could be your business name, logos, inventions, product designs or other creative work.</p>
		<p>As your IP is likely to be a valuable asset, it's wise to help secure the future of your business by protecting it legally. For information on the different ways you can prevent others from stealing your IP, and advice on carrying out an IP audit, see our guide on <a href="/create-innovate-and-protect/protecting-your-intellectual-property/find-value-you-didnt-know-existed-in-your-business/protecting-intellectual-property.php">protecting intellectual property</a>.</p>

