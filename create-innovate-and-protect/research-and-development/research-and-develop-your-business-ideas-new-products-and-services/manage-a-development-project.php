		<h1>Research and develop your business ideas, new products and services</h1>
		<h2>Manage a development project</h2>
		<p>Project managers are essential to ensure the successful development of new products or services. The project manager is responsible for:</p>
		<ul>
			<li>controlling costs and allocating resources</li>
			<li>drawing up the product or service's specification</li>
			<li>co-ordinating the product development team - see the page in this guide on <a href="/create-innovate-and-protect/research-and-development/research-and-develop-your-business-ideas-new-products-and-services/creating-a-project-development-team.php">creating a project development team</a></li>
			<li>timetabling the development process</li>
			<li>troubleshooting</li>
		</ul>
		<h3>Timetabling the development process</h3>
		<p>Your project manager should draw up a critical path for the completion of key tasks and stages in the product development process. SMART (specific, measurable, agreed, realistic and time-bound) objectives should be introduced to help measure and monitor progress.</p>
		<p>However, your plans should be flexible as well so that you can deal with any unknown event that occurs.  For example, you may have to deal with a change in the project's specifications or expected completion date.</p>
		<p>For more information on managing your development project, see our guide on how to <a href="/create-innovate-and-protect/research-and-development/manage-your-research-design-and-development.php">manage your research, design and development</a>.</p>
		<p>For more information on working with formal project management systems for the first time, see our guide on <a href="/grow-your-business/planning-how-to-grow/project-management-the-basics.php">project management - the basics</a>.</p>

