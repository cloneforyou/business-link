		<h1>Research and develop your business ideas, new products and services</h1>
		<h2>Checklist: developing a product or service</h2>
		<p>The following checklist will help you understand everything you should take into account when developing a new product or service:</p>
		<ul>
			<li>Have you consulted members of your team about your development plans? They may contribute insights and have useful experience.</li>
			<li>Have you spoken to suppliers, customers and other business associates? Their specialist expertise could be invaluable.</li>
			<li>Have you thought about your potential market and competition? For more information, see the page in this guide on <a href="/create-innovate-and-protect/research-and-development/research-and-develop-your-business-ideas-new-products-and-services/is-there-a-market-for-my-idea.php">is there a market for my idea?</a></li>
			<li>Have you thought about how you're going to price your product or service and how to cover your costs? See the page in this guide on <a href="/create-innovate-and-protect/research-and-development/research-and-develop-your-business-ideas-new-products-and-services/pricing-your-proposed-service-or-product.php">pricing your proposed service or product</a>.</li>
			<li>Have you considered how to phase in your new product development and control how much you are spending? Make sure you stop work on ideas that don't meet your criteria before committing a lot of time and resources. For further information, see the page in this guide on <a href="/create-innovate-and-protect/research-and-development/research-and-develop-your-business-ideas-new-products-and-services/financing-your-idea-and-cost-control.php">financing your idea and cost control</a>.</li>
			<li>Have you considered who needs to be on your product development team and how they will work together? For more information, see the page in this guide on <a href="/create-innovate-and-protect/research-and-development/research-and-develop-your-business-ideas-new-products-and-services/creating-a-project-development-team.php">creating a project development team</a>.</li>
			<li>Have you considered who's going to manage the team? See the page in this guide on how to <a href="/create-innovate-and-protect/research-and-development/research-and-develop-your-business-ideas-new-products-and-services/manage-a-development-project.php">manage a development project</a>.</li>
			<li>Have you considered any regulations (including environmental regulations) that will affect your new product or service?</li>
			<li>Have you looked beyond the new product or service's immediate potential and considered the longer term?</li>
		</ul>

