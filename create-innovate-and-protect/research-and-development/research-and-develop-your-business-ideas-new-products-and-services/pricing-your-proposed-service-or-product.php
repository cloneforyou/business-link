		<h1>Research and develop your business ideas, new products and services</h1>
		<h2>Pricing your proposed service or product</h2>
		<p>Establishing a pricing strategy for a new product or service is an important part of the development process. You should consider pricing the moment you decide to take an idea forward as it will determine how much you can afford to invest in the project.</p>
		<p>You will need to take the following factors into account:</p>
		<ul>
			<li>The benefits - or value - to the customer of your product or service compared with what the competition has to offer - will customers be prepared to pay the price you want to charge?</li>
			<li>Whether or not you're first to market - is your product or service innovative or are you following a market trend?</li>
			<li>The selling channels you want to use as this will affect your promotional spend and distribution costs.</li>
			<li>How quickly you want to establish your product or service.</li>
			<li>The expected lifecycle of your product or service.</li>
			<li>How much you will need to charge to cover your costs.</li>
		</ul>
		<p>Strategic pricing can be used to drive sales and regulate demand. See our guide on how to <a href="/sales-and-marketing/pricing/price-your-product-or-service.php">price your product or service.</a></p>

