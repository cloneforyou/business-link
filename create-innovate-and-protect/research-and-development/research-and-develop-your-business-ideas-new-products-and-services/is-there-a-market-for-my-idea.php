		<h1>Research and develop your business ideas, new products and services</h1>
		<h2>Is there a market for my idea?</h2>
		<p>There are certain criteria you can use to establish whether there is a market or demand for your product or service:</p>
		<ul>
			<li>Does it satisfy or create a market need?</li>
			<li>Can you identify potential customers?</li>
			<li>Will it outlive passing trends or capitalise on the trend before it dies away?</li>
			<li>Is it unique, distinct or superior to those offered by competitors?</li>
			<li>What competition will it face - direct or indirect, local, national or global?</li>
			<li>Is the product safe for public use and does it comply with relevant regulations and legislation? Seek legal advice before proceeding.</li>
			<li>Will the market want your product or service at a realistic price?</li>
		</ul>

		<p><strong>Market research</strong> can play an important role in answering many of these questions and increasing your chances of success. It is much better to invest time researching before you invest your money. See our guide on <a href="/sales-and-marketing/marketing/market-research-and-market-reports.php">market research and market reports</a>.</p>
		<p>Remember that although the end user of your new product or service might be your most important customer, you may have to take the needs of other parties into account, such as retailers or distributors.</p>
		<p>It is very important that you are as thorough in your market research as possible, as mistakes made at this stage of development could prove costly later on. Remember, the more information you have, the better you will be able to understand your potential customers, the marketplace and how your product fits in. For more information, see our guide on <a href="/starting-up/considering-starting-up/common-mistakes-when-starting-up-and-how-to-avoid-them.php">common mistakes when starting up - and how to avoid them</a>.</p>

		<h3>Your competition</h3>
		<p>Not only must you meet your customers' needs, you should do so in a way that is better than the alternatives offered by your competitors.</p>
		<p>Your new product or service should have a unique selling proposition - a feature or property that makes it stand out in the marketplace. Before entering the market you need to gather 'competitive intelligence' by determining:</p>
		<ul>
			<li>how customer needs are currently met</li>
			<li>why customers would choose your product or service rather than your competitors', both now and in the future</li>
			<li>what risks you are prepared to take to launch your product or service into this market</li>
		</ul>
		<p>For more information, see our guides on how to <a href="/sales-and-marketing/keeping-your-customers/know-your-customers-needs.php">know your customers' needs</a> and <a href="/sales-and-marketing/marketing/understand-your-competitors.php">understand your competitors</a>.</p>

