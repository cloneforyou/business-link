		<h1>Here's how I got my great new product in front of customers</h1>
		<h2>Here's how I got my great new product in front of customers</h2>
		<p>Sam Tate co-founded Sprue Aegis plc in 1998 when he and his business partner came up with an innovative smoke detector. The FireAngel fits between a light fitting and the bulb, taking its energy from the mains. FireAngel charges up its rechargeable battery when the light is on - and runs off the battery when the light is off.</p>
		<p>The company hasn't looked back since convincing one high street retailer to sell the detector. It's now stocked in around 6,000 outlets.</p>
		<p><strong>What I did</strong></p>
		<h3>Research the target consumer</h3>
		<p>"We spent months digging out market research reports, talking to the Fire Brigade and the Office of the Deputy Prime Minister - the government department responsible for fire safety (now the responsibility of Communities and Local Government).</p>
		<p>"Once we confirmed the FireAngel was a valuable idea, I went out on to the street to see if there was a market for it. We basically just stopped people, showed them the prototype and asked a set list of questions. We also looked closely at our competitors' products and who their market was.</p>
		<p>"We initially worked out a sales strategy involving the Internet, direct marketing and advertising. However, we soon knew on a cost-per-sale basis this wouldn't be feasible - we would have sold less and had to charge twice as much. We then realised an ideal way to reach consumers who wanted a quick solution was through supermarkets."</p>
		<h3>Get the right price and sales channel</h3>
		<p>"Most smoke detectors retail at between &pound;5 and &pound;10. We knew we could charge a premium because fitting the alarm is as simple as changing a bulb. It doesn't need a battery and it lasts for up to ten years.</p>
		<p>"We set the price at around &pound;20 but the retailer obviously takes a margin. Remember - it's a lot harder to put the price up than drop it down, so don't go in too cheaply."</p>
		<h3>Nail the sales pitch early</h3>
		<p>"It's really important to be able to get your message across in 30 seconds flat. If you get the chance to speak to a buyer, you need to be able to sock it to them - they're extremely busy people. It's definitely down to trial and error so I would recommend rehearsing your sales pitch in front of anyone who will listen, and not delivering it for the first time to the top buyer at a potentially major client.</p>
		<p>"It took three years to get our product to market. A lot of this time was spent on design and safety testing, but it took 18 months of talking to one well-known high street retailer before they agreed to stock the alarm. Once you're in there with one retail chain, it gets much easier to approach others. But you definitely have to be persistent to succeed."</p>
		<p><strong>What I'd do differently</strong></p>
		<h3>Don't take 'no' for an answer - right from the start</h3>
		<p>"It's easy to say, but be prepared for knockbacks. We got them - and would have started to get demoralised by them without a total belief in our product.</p>
		<p>"After a short time we realised that persistence was the key to making it all happen. Buyers are busy people and you've got to make yourself stand out from the crowd. For example, we waited in the reception of one high street retailer's head office until the buyer agreed to see us. Sometimes you have to go to that level."</p>
