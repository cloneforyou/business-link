		<h1>Support networks and facilities for innovation and R&amp;D</h1>
		<h2>The Enterprise Europe Network</h2>
		<p>The Enterprise Europe Network (EEN) provides support and advice to businesses across Europe and helps them make the most of the opportunities in the European Union (EU) and globally. The EEN:</p>
		<ul>
			<li>provides information on EU legislation</li>
			<li>offers help in finding a business partner</li>
			<li>provides access to innovation networks or information on funding opportunities</li>
			<li>brings together sellers and buyers of innovative ideas and products</li>
			<li>aims to help businesses <strong>promote new technologies</strong> or find new ways to meet a <strong>technology need</strong></li>
		</ul>

		<p>EEN partner organisations in the UK operate across the country. The partner organisations consist of Chambers of Commerce, universities and other qualified bodies.</p>
		<p>EEN partners work by:</p>
		<ul>
			<li>visiting companies to discuss and assess their needs</li>
			<li>identifying technologies that could help businesses develop</li>
			<li>matching companies with suitable European partners or technologies and arranging visits to them</li>
			<li>helping companies promote their innovative technologies throughout Europe</li>
			<li>helping companies and research departments make innovative technologies available to each other - technology transfer</li>
			<li>advising on all stages of technology transfer, including valuing technologies, negotiating contracts, intellectual property and financing</li>
			<li>developing the research and innovation capacities of small and medium-size enterprises (SMEs) by encouraging co-operation with other research bodies, fostering technological co-operation and holding brokerage events</li>
			<li>helping SMEs to share research results, participate in research programmes and apply for funding - particularly from the EU's Seventh Framework Programme for Research and Technological Development (FP7)</li>
		</ul>

