		<p>The government offers a range of Solutions for Business products to help business benefit from the knowledge and expertise of other businesses and academic institutions:</p>
		<ul>
			<li>Networking for Innovation.</li>
			<li>Knowledge Transfer Partnerships (KTPs) - see the page on <a href="/create-innovate-and-protect/research-and-development/work-with-uk-universities-and-colleges/knowledge-transfer-partnerships.php">Knowledge Transfer Partnerships</a> in our guide on how to <a href="/create-innovate-and-protect/research-and-development/work-with-uk-universities-and-colleges.php">work with UK universities and colleges</a>. Also, see the page on <a href="/create-innovate-and-protect/research-and-development/technology-strategy-board/how-the-technology-strategy-board-can-help-your-business.php">how the Technology Strategy Board can help your business</a> in our guide on the <a href="/create-innovate-and-protect/research-and-development/technology-strategy-board.php">Technology Strategy Board</a>.</li>
		</ul>

		<p>Solutions for Business is a Government Business Support Scheme that is only available in England. Support for businesses is different in Scotland, Wales and Northern Ireland.</p>
		<p>Read more about Solutions for Business in our guide on <a href="/finance-and-grants/finance-options/grants-and-government-support/government-support-for-businesses.php">government support for businesses</a>.</p>
		
		<h3>KTPs</h3>
		<p>KTPs enable businesses to benefit from the expertise of organisations like further education colleges, universities or research institutes.</p>
		<p>Recently qualified people at NVQ level 4 or above are recruited to work in the business as a Knowledge Transfer Partnership Associate. Projects last between ten weeks and three years, and address product design, manufacturing, product or management processes, computing or management information.</p>
		<p>You can <a href="https://www.gov.uk/guidance/knowledge-transfer-partnerships-what-they-are-and-how-to-apply" target="_blank" rel="noopener noreferrer">find information on KTPs on the Technology Strategy Board website</a>.</p>
		<p>Find out about the range of support for business included in the government's <a href="/solutions-for-business-portfolio.php">Solutions for Business portfolio</a>.</p>
		
