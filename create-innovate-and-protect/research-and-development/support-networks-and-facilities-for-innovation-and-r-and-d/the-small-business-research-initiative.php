		<h1>Support networks and facilities for innovation and R&amp;D</h1>
		<h2>The Small Business Research Initiative</h2>
		<p>The Small Business Research Initiative (SBRI) is run by the Technology Strategy Board and aims to stimulate the creation of innovative new products and services. See the page on <a href="/create-innovate-and-protect/research-and-development/technology-strategy-board/the-small-business-research-initiative.php">the Small Business Research Initiative</a> in our guide on the <a href="/create-innovate-and-protect/research-and-development/technology-strategy-board.php">Technology Strategy Board</a>.</p>
		<p>Through the SBRI, government departments run competitions to seek solutions to specific issues. For example, could you look into ways of reducing the weight that soldiers have to carry or could you create new energy-efficient technologies?</p>
		<p>Successful applicants to these competitions receive fully funded development contracts with the relevant government department rather than a grant. The business retains intellectual property rights.</p>
		
		<h3>The SBRI process</h3>
		<p>You can <a href="https://apply-for-innovation-funding.service.gov.uk/competition/search" target="_blank" rel="noopener noreferrer">find details of and apply for government competitions on the Technology Strategy Board website</a>.</p>
		<p>Once applications have been processed, the most interesting ideas are selected. There are then two main phases.</p>
		<p><strong>Phase 1 - demonstration of scientific, technical and commercial feasibility</strong>. This lasts six months and is worth about &pound;50,000-&pound;100,000. Projects are then assessed for Phase 2 funding. Not all projects will go through to the second phase.</p>
		<p><strong>Phase 2 - prototype development</strong>. This lasts about two years and is worth &pound;250,000-&pound;1 million. Businesses can then commercialise their product and offer it to government departments and others under normal procurement processes. See our guide for an <a href="/sales-and-marketing/selling/overview-on-selling-to-government.php">overview on selling to government</a>.</p>
		<p>You can <a href="https://www.gov.uk/government/collections/innovate-uk-case-studies" target="_blank" rel="noopener noreferrer">find case studies about previous competitions on the Technology Strategy Board website</a>.</p>
		
