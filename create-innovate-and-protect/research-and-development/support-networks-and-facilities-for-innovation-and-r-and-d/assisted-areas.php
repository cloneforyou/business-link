		<h1>Support networks and facilities for innovation and R&amp;D</h1>
		<h2>Assisted areas</h2>
		<p>One of the ways in which the government's development agencies help particular areas to develop is by creating <strong>assisted areas</strong>.</p>
		<p>Assisted areas are those locations where regional aid may be granted under European Union legislation. Businesses based in regions classified as assisted areas may benefit from a grant if they stimulate regional development, urban regeneration or an improvement in employment prospects.</p>
		<p>There are two main assisted area grant schemes in Britain:</p>
