		<h3>Current Assisted Areas Map</h3>
		<p>The current Assisted Areas Map came into effect on 13 February 2007, and remains in force until 31 December 2013. You can <a href="/_img/file38642.png" target="_blank" rel="noopener noreferrer">view the Current Assisted Areas map</a>.</p>
		
		<h3>Regional Growth Fund</h3>
		<p>The Regional Growth Fund (RGF) operates across England from 2011 to 2014. It supports projects that create economic growth and long-term employment. RGF aims particularly to help areas that are currently dependent on the public sector.</p>
		<p><a href="https://www.gov.uk/government/policies/regional-growth-fund" target="_blank" rel="noopener noreferrer">Read about the RGF on the BIS website</a>.</p>
		
