		<h1>Support networks and facilities for innovation and R&amp;D</h1>
		<h2>Business clusters</h2>
		<p>The success of an industry in a particular geographical area might not just be related to the cost of production. For example, businesses can benefit from the presence of local expertise, a pool of skilled labour, or research and development facilities. The cluster concept aims to build on this idea.</p>
		<p>Clusters are geographically close, interconnected companies and specialised suppliers within certain fields that work and co-operate with each other, as well as being competitors.</p>
		<p>Support for these clusters includes helping companies access a skilled workforce, quality universities, good sites and investment capital. Growth in these clusters also attracts suitable suppliers and even customers to the area. <a href="http://webarchive.nationalarchives.gov.uk/20120903211434/http://webarchive.nationalarchives.gov.uk/20120903211434/http://www.bis.gov.uk/policies/economic-development/clusters-in-the-uk" target="_blank" rel="noopener noreferrer">Read about business clusters on the Department for Business, Innovation &amp; Skills (BIS) website</a>.</p>
		
