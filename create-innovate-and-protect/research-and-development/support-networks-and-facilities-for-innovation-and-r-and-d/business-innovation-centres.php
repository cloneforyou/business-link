		<h1>Support networks and facilities for innovation and R&amp;D</h1>
		<h2>Business innovation centres</h2>
		<p>Business innovation centres (BICs) give <strong>support</strong> to innovative businesses and entrepreneurs. They aim to help young companies survive and grow during the start-up period when they are most vulnerable. This is known as <strong>incubation</strong>.</p>
		<p>Services include:</p>
		<ul>
			<li>information about sharing premises with similar companies, such as in a science park - for more information see the page in this guide on <a href="/create-innovate-and-protect/research-and-development/support-networks-and-facilities-for-innovation-and-r-and-d/science-parks.php">science parks</a></li>
			<li>advice about management, strategy and planning</li>
			<li>technological help</li>
			<li>help with finding manufacturers and markets for innovative products and services</li>
			<li>expert partnership and mentoring</li>
			<li>access to training</li>
			<li>access to other sources of help</li>
		</ul>

		<p>The European Community runs a scheme to certify BICs, of which more than 150 are gathered under the umbrella of the European Business Network (EBN).</p>
		<p><a href="https://ebn.eu/index.php?p=members" target="_blank" rel="noopener noreferrer">Search a list of EBN members on the EBN website</a>.</p>
		<p>You can also <a href="#" title="Visit the UKBI website to get up to the minute news on the incubation community in the UK">keep up to date with all the latest news about incubators on the UK Business Incubation (UKBI) website</a>.</p>
