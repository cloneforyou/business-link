		<h1>Support networks and facilities for innovation and R&amp;D</h1>
		<h2>Virtual support networks</h2>
		<p>Virtual support networks exist online to enable businesses to interact and to share ideas and tips for progressing. They can help you to contact other businesses or organisations within your own specialism or area of expertise that you might otherwise have been unaware of or have been unable to connect with.</p>
		<p>There are a number of virtual business support websites available, which enable people involved in research and development to access other businesses for support and mutual advice.</p>
		<p>Some businesses may prefer to create their own business network for their specialism or industry. There are organisations online that can help you to set up your own e-community, using their contacts and technical expertise. 
