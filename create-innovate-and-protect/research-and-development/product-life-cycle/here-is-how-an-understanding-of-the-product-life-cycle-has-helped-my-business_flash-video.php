			<h1>Case study video transcript</h1>
			<h2>"Here's how winning the Queen's Award has helped my business"</h2>

			<p><em>(Opening animation, title and jingle. Samantha Fernando sits in an office in front of several awards on a desk and a rack of Keela jackets)</em></p>

			<p>Samantha Fernando: "My name's Samantha Fernando, I work for Keela International. Keela manufactures outdoor technical performance gear and active wear, so waterproof garments, fleeces, t-shirts. We're based in the Kingdom of Fife, it's in the lowlands of Scotland.</p>

			<p><em>(Footage inside an outdoor retailer, with several racks of insulated jackets and outdoors clothing)</em></p>

			<p>"Keela's sister company Ardmel manufactures bespoke, automated machinery dedicated towards the garment industry, where we have what we call AC technology which is advanced construction technology and this is looking at new ways of constructing garments, fabrics together.</p>

			<p><em>(Exterior shot of the Ardmel building, and shots of employees working on machinery inside. Close-ups of machines cutting, sewing and printing on fabrics)</em></p>

			<p>"We manufacture a lot for the tactical forces - police, mountain rescue teams, we do a lot with the emergency services. The high-end technical users know our kit and they tend to use our kit.</p>

			<p><em>(Close-ups of police and camouflage clothing)</em></p>

			<p>"Product lifecycle to Keela is very important. It's how we plan and strategise where the company is going to go both medium and long-term. (Picture of a product lifecycle graph with Sales Volume marked against Time. The line is mountain-shaped and five phases on the graph are labelled as Development, Introduction, Growth, Maturity and Decline.) We look to our users for their requirements so we're very consumer-led. We very much are a technical company in that we look to engineer solutions that will basically help solve our customer's requirements. (Footage of a man trying on a jacket in a shop) An example I could give for Keela is System Dual Protection. It's one of our most successful products that we do. It's now widely used throughout the industry.</p>

			<p>"We were receiving from a lot of our consumers and we saw this happening in the market, where they were complaining about condensation inside their jackets, and this was happening with every single fabric and every single brand and we realised that we needed to look at a solution that would solve this problem and one of our directors, who is also an engineer, had a eureka moment and thought about double-glazing and how double-glazing basically protects a house. (Footage of employees at work in the Ardmel factory) You don't get condensation forming inside a house and from that we looked at solutions of how could we use that kind of basic concept into a garment and that's where we introduced System Dual Protection.</p>

			<p><em>(Footage of employees sewing garments together using sewing machines)</em></p>

			<p>"The System Dual Protection when we first developed it, it very much was following the traditional mountain shape. (Picture of the Product Lifecycle graph from earlier in the video) The initial part, which was very heavily involved in research and development, there was a lot of user trials, a lot of investigation, a lot of input went into that initial stage. Then it obviously went into the stage of introducing it into the market - this involved marketing, very aggressive marketing. (Close-ups of Keela-branded garmets in the outdoor shop) Where we are at the moment is still probably gradually going up that mountain, it hasn't reached market saturation. There are still refinements that we can be doing to that particular product system and we see at this moment in time that's a very long-term product for us, that we've got another ten years' growth in that particular system.</p>

			<p>"The amount of investment that we divert into a particular product does depend on which specific product it is, what we perceive the product lifecycle was going to be and obviously the amount of revenue it will bring into the company.</p>

			<p>"It's very important that we know whereabouts a particular product is at any one time in the product lifecycle. We need to know one, when the product is coming to end of its lifecycle and also more importantly when we need to start to look at feeding in a new product. (Footage of machines stress-testing pieces of material) A product development for a new product can take anything from six months to two or three years so obviously there's quite a long time, lead-in period into that. So to really gauge exactly where you are on that product lifecycle mountain slope, so to speak, is extremely important.</p>

			<p><em>(Exterior shot of hills and a lake)</em></p>

			<p>"Changes that I would maybe look at for the future in developing new products or the product lifecycle - last year we ran a promotion asking users of our equipment and clothing to come forward with their ideas for new, innovative ways of making a new mountaineering jacket. The feedback that we got back from these individuals was tremendous. There was very innovative ideas - some of them were very simple, some of them very complex but it gave us a fantastic insight and new directions to look at and I would very much like to look at involving the users much more than we have in the past, really getting them involved right at the initial stages and getting that feedback and them firmly entrenched in that process."</p>

			<p><em>(End animation and jingle)</em></p>

