		<h1>Product life cycle</h1>
		<h2>Introduction stage of a product life cycle</h2>
		<p>The introduction stage of a product's life cycle is when you can build an awareness of your product or service in certain markets and develop a specific market.</p>
		<p>Once your product or service is introduced to the market, you should focus on an intense marketing effort to establish a clear identity and promote your product.</p>
		<p>You should concentrate on building a base for the product at the introduction stage, rather than making an immediate profit.</p>
		<p>At the introduction stage, you should focus on the following marketing factors:</p>
		<ul>
			<li>pricing</li>
			<li>distribution</li>
			<li>promotion</li>
		</ul>

		<h3>Pricing</h3>
		<p>You should initially start pricing at the highest point you believe it is possible to achieve.</p>
		<p>You can also consider a skimming price strategy: charging a relatively high price for a short time when a new, innovative, or much-improved product is launched onto a market. The objective with skimming is to skim off customers who are willing to pay more to be one of the first to have a new product. Prices can be lowered later when demand from the early adopters falls.</p>
		<p>A penetration pricing strategy may work best for businesses entering a new market or building on a relatively small market share. It involves the setting of lower, rather than higher prices in order to achieve a large, if not dominant market share.</p>
		
		<h3>Distribution</h3>
		<p>Your distribution should be selective and limited to a specific type of consumer, until your product is accepted. Also, different distribution models should be considered during different periods of the product life cycle, eg new products for different seasons in a clothes shop.</p>
		
		<h3>Promotion</h3>
		<p>You should try to build brand awareness at an early stage. It is worth working with a brand design or communications agency as you develop a product to establish a strong brand.</p>
		<p>You can use samples or trial incentives to capture early adopters of the product or service. Introductory promotions can also help convince potential resellers to carry your lines.</p>
		<p>It is likely that at the introduction stage, your sales will be low until customers become aware of your product or your service's benefits. Due to the high cost of advertising and low sales, you may find that the product is producing negative profits at this stage. However, you should make up for this with increasing revenue generated at the growth and maturity stage of a product life cycle. For more information, see the page in this guide on <a href="/create-innovate-and-protect/research-and-development/product-life-cycle/product-life-cycles-growing-and-maturing-a-product.php">product life cycles - growing and maturing a product</a>.</p>
		
