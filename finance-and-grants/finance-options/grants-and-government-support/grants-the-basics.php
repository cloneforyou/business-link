		<h1>Grants: the basics</h1>
		<h2>Introduction</h2>
		
		<img src="/_img/BES_012L.gif" width="78" height="118" alt="" />

		<p>A grant is a sum of money given to an individual or business for a specific project or purpose.</p>
		<p>A grant usually covers only part of the total costs involved. However, as long as you keep to any conditions attached to the grant, you will not have to repay it or give up shares in your business.</p>
		<p>Grants to help with business development are available from a variety of sources, such as the government, European Union, local authorities and some charitable organisations.</p>
		</p>
		<p>These grants may be linked to business activity or a specific industry sector. Some grants are linked to specific geographical areas, eg those in need of economic regeneration.</p>
		<p>This guide introduces you to some of the grants available to businesses in the UK and outlines the kinds of projects and organisations to which they are available. It also provides hints on improving your chances of being awarded a grant.</p>
		<p>However, it is important to remember that applying for a grant can be complex and time consuming. There is also a lot of competition and even if your business is eligible for a particular scheme or initiative, your application may not be successful.</p>
		
