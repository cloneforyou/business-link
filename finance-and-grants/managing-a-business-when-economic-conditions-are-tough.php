		<p>Managing a business is even more demanding when market conditions are tough or uncertain.</p>
		<p>Planning can be difficult when your business, its customers and suppliers are faced with rising prices, falling sales, shrinking margins and cashflow problems. Redundancies and business failures further diminish market confidence while reduced orders and late payment can indicate problems within your market.</p>
		<p>You cannot make your business completely bullet proof. However, taking appropriate action can help your business survive when economic conditions are tough. The first step is to carry out a 'health check', enabling you to identify your weaknesses and threats, as well as your strengths and opportunities. This will help you develop an effective survival strategy.</p>
		<p>This guide outlines what to look out for when economic conditions are tough and how to ensure your business runs effectively. It gives advice on how to assess performance, financial considerations, how to implement a risk strategy and where to get support.</p>
		
