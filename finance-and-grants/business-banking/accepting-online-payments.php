		<h1>Accepting online payments</h1>
		<h2>Introduction</h2>
		<img src="/_img/BS0430.gif" width="78" height="118" alt="" />
		<p>For many small businesses, accepting payments online offers major benefits. Customers increasingly expect this facility and it can improve your cashflow significantly.</p>
		<p>It's easy to accept cheques or invoices for your online sales and to process payments in the traditional way. However, because buyers often use the internet for a speedy service, most sales are paid for with credit and debit cards. To accept cards online, you will have to make special banking arrangements.</p>
		<p>Online payments using cards are 'card-not-present' transactions. There are higher risks of fraud with this type of payment and banks require you to operate within a well-defined set of rules and accept a higher level of commercial risk than a conventional swiped card transaction in a shop.</p>
		<p>This guide will help you to understand these requirements and assess the options available for taking advantage of online payments.</p>
		
