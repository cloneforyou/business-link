		<h1>Choose and work with an accountant</h1>
		<h2>How an accountant can help your business</h2>
		<p>If you run a business, you're legally required to:</p>
		<ul>
			<li>Keep certain records, including PAYE (Pay As You Earn) records if you have employees, and VAT records if you're VAT registered. This will involve regular bookkeeping work.</li>
			<li>Submit an annual tax return to HM Revenue &amp; Customs. This will involve the preparation of financial statements.</li>
			<li>Appoint an auditor to carry out an annual audit of your accounts, unless the company is exempt - see the page <a href="/tax-payroll-and-company-information/companies-house-annual-returns-and-accounts/accounting-and-audit-exemptions-for-small-companies/do-you-qualify-for-an-audit-exemption.php">do you qualify for an audit exemption?</a> in our guide on <a href="/tax-payroll-and-company-information/companies-house-annual-returns-and-accounts/accounting-and-audit-exemptions-for-small-companies.php">accounting and audit exemptions for small companies</a>.</li>
		</ul>

		<p>If you're a limited company, you also have to file accounts with the Registrar of Companies - see our guide on <a href="/tax-payroll-and-company-information/companies-house-annual-returns-and-accounts/filing-accounts-with-companies-house-dates-and-checklist.php">filing accounts with Companies House: dates and checklist</a>.</p>
		<p>You could decide to do some of this work yourself - but many businesses prefer to employ an accountant for at least some of it. For example, you might not have the time or necessary skills available in-house to do all the accounting work yourself. There is also a legal requirement to employ a qualified accountant for certain tasks.</p>
		<p>In addition to basic accounting functions, many accountants also offer other services, such as start-up business and management systems advice.</p>
		
