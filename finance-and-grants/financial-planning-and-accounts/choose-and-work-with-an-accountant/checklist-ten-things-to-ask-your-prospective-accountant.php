		<h1>Choose and work with an accountant</h1>
		<h2>Checklist: ten things to ask your prospective accountant</h2>
		<p>Different accountants can help your business in different ways, so you should always think about what you are looking for before choosing an accountant. For example, you may only need basic bookkeeping, or you may want someone who can give you specialist business advice.</p>
		<p>Once you have drawn up a shortlist of potential accountants who would be suitable, you should arrange to meet them to make sure they are the ideal choice for your business.</p>
		<p>You should take your business plan and other useful information about your business to the meeting. A good accountant should want to know as much about you as you do about them.</p>
		<p>They should also be happy to pass on names of clients for you to take up references.</p>
		<p>To make sure you get the best value for money, there are things you should find out about any prospective accountant. You should:</p>
		<ul>
			<li>ask about their qualifications</li>
			<li>find out how many partners there are in the practice, and who will look after your business on a day-to-day basis</li>
			<li>make sure they are experienced in dealing with businesses of a similar size and at a similar stage of growth to yours, and how they could help you develop your business</li>
			<li>assess whether they understand your business sector and its needs</li>
			<li>ask about their estimated response times</li>
			<li>check if their service will be proactive - eg whether they will remind you when you need to submit accounts or send you updates on changes in tax law</li>
			<li>find out if they offer any additional services - eg inheritance planning or advice on information systems</li>
			<li>ask whether they offer any specialist services - eg in start-ups or stock-market listing</li>
			<li>what services they will charge for and how and when you will pay for them - you may be able to pay a fixed fee for the first 12 months</li>
			<li>the level of access you will be given to the data held about your business - you might need this data to update your business plan or for tender documents and you will also want easy access to it if you ever decide to change accountants</li>
		</ul>

