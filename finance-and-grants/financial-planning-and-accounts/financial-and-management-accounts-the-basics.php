		<h1>Financial and management accounts: the basics</h1>
		<h2>Introduction</h2>
		<img src="../../_img/SU0320.gif" width="94" height="62" alt="" />
		<p>The accounting needs of your business will vary according to its size, type and sector. As the business owner, it's your responsibility to make sure your business keeps accurate records and accounts.</p>
		<p>There are two types of accounting information - financial accounts and management accounts.</p>
		<p>Financial accounts describe the performance of your business and have to be filed at Companies House. Management accounts are aimed at helping you to plan your business and make decisions about key areas such as sales, margins and stock.</p>
		<p>This guide explains the basics of both types of accounts and what they should include. It outlines your financial accounting obligations and details how management accounting can help you run your business more effectively.</p>
		
