		<h1>Financial and management accounts: the basics</h1>
		<h2>False accounting</h2>
		<p>False accounting is fraudulent and usually occurs when a business or employee:</p>
		<ul>
			<li>deliberately records false financial information - eg to hide losses or appear more successful</li>
			<li>changes, defaces or destroys records</li>
			<li>exaggerates the business' assets or understates its liabilities</li>
		</ul>

		<p>In this way, an employee who adds only a few pounds to an expenses claim could constitute false accounting, even though it might not seriously affect your finances. But more serious fraud could mean that your business suffers major financial losses, or that it has been trading while insolvent.</p>
		
		<h3>What to do if you think an employee has been falsifying accounts</h3>
		<p>You should immediately report false accounting to the National Fraud Reporting Centre (NFRC), who may then pass it to the police to investigate. Your business can also take action to recover any losses if an employee was involved.</p>
		<p>You can <a href="http://www.actionfraud.org.uk/" target="_blank" rel="noopener noreferrer">find information on fraud, scams and identity theft on the Action Fraud website</a> or contact the Action Fraud Helpline on Tel 0300 123 2040.</p>
		<p>To work out how much your business has lost and how the fraud occurred, you might need to use an accountant or auditor - possibly from outside the business. But you must still remember to report the fraud immediately.</p>
		<p>You may suspend an employee while the investigations are carried out, but only if their contract of employment allows it. For further guidance on this, see the page on <a href="/employment-and-skills/problems-at-work/disciplinary-procedures-hearings-and-appeals/investigating-disciplinary-issues.php">investigating disciplinary issues</a> in our guide on <a href="/employment-and-skills/problems-at-work/disciplinary-procedures-hearings-and-appeals.php">disciplinary procedures, hearings and appeals</a>.</p>
		
		<h3>Reduce the risk of false accounting</h3>
		<p>Common sense and sound business practices will help you to protect your business against the risks of theft and fraud. For example, you should:</p>
		<ul>
			<li>maintain thorough recruitment procedures and carry out pre-employment checks such as checking references - see our guide on <a href="/employment-and-skills/employment-checks/pre-employment-checks.php">pre-employment checks</a></li>
			<li>put in place a whistle-blowing policy and try to encourage a culture of fraud awareness across the business</li>
			<li>have a 'zero tolerance' approach to employee theft and fraud and clearly state this in employees' terms and conditions or your discipline and grievance policy - see the page on <a href="/employment-and-skills/staff-documents-and-employment-policies/set-up-employment-policies-for-your-business/policies-on-discipline-grievance-bullying-and-harassment.php">policies on discipline, grievance, bullying and harassment</a> in our guide on <a href="/employment-and-skills/staff-documents-and-employment-policies/set-up-employment-policies-for-your-business.php">how to set up employment policies for your business</a> </li>
			<li>restrict access to financial information and divide duties so that no single person is responsible for all accounts and have more than one person authorise payments</li>
			<li>check bank statements and other accounts - look into any unusual transactions or discrepancies, and audit processes and procedures regularly</li>
			<li>commission a registered auditor, who would usually be a qualified accountant, to conduct an external audit to examine the business' financial report to check that they show a true and fair view of the business' financial performance and its assets and liabilities</li>
			<li>undertake an assurance report - similar to an external audit but with more limited scope and objectives - possibly conducted by an external accountant, to review the entire business' accounts, but with specific aspects checked where necessary</li>
		</ul>

