		<h1>Financial and management accounts: the basics</h1>
		<h2>The importance of maintaining accurate accounts</h2>
		<p>It's important that your accounts are accurate and up to date so you can draw up 'true and fair' annual accounts. Your accounts should be backed up with full and detailed records of all business income and expenditure, such as receipts, invoices and purchase orders, payments in and out, etc. See the page on <a href="/finance-and-grants/financial-planning-and-accounts/set-up-a-simple-profit-and-loss-account-for-your-business/records-required-for-producing-a-profit-and-loss-account-or-completing-a-tax-return.php">records required for producing a profit and loss account or completing a tax return</a> in our guide on how to <a href="/finance-and-grants/financial-planning-and-accounts/set-up-a-simple-profit-and-loss-account-for-your-business.php">set up a simple profit and loss account for your business</a>.</p>
		
		<h3>Why you should keep records and documents</h3>
		<p>Following careful record keeping procedures can also help you with tax returns and prevent fraud or theft. Using a good record keeping system will keep you up to date and help you to:</p>
		
		<ul>
			<li>track expenses, debts and creditors</li>
			<li>apply for additional funding - eg a bank loan or overdraft facility</li>
			<li>save time and accountancy costs</li>
			<li>pay tax, accurately and on time, avoiding penalties</li>
			<li>apply for and receive the correct amount of benefits or credits</li>
		</ul>

		<p>If you are starting a new business it is essential that you get a proper record keeping system in place immediately.</p>
		<p>You can use various storage methods to keep records - such as a computer, hard disc drive or CD - as long as they:</p>
		<ul>
			<li>show all information contained within a document</li>
			<li>allow information to be presented in a readable format</li>
		</ul>

		<p>You should try to keep all original documents, and must keep any which show that tax has been deducted - eg your end of year certificate for PAYE (form P60).</p>
		<p>For more information, see our guide <a href="/tax-payroll-and-company-information/set-up-a-basic-record-keeping-system.php">set up a basic record-keeping system</a>.</p>
		<p>Detailed and up-to-date records will help you comply with tax legislation, deal with mistakes and avoid penalties. You can be penalised for:</p>
		<ul>
			<li>not keeping adequate records</li>
			<li>failing to keep records for required periods of time</li>
			<li>inaccurate tax returns</li>
		</ul>

		<p><a href="/tax-payroll-and-company-information/find-out-what-records-you-should-be-keeping.php">Use our interactive tool to find out the business records you must keep.</a></p>
		
		<h3>Analytical accounting tools</h3>
		<p>Analysing your financial accounts enables you to compare your performance against previous years and with its competitors.</p>
		<p>Ratios enable you to quickly compare relative values - eg two items on the balance sheet.</p>
		<p>Ratio analysis can also be applied to non-financial data. For ease of reference, ratios are often split into the following areas of common control:</p>
		<ul>
			<li>liquidity ratios - these are used to measure solvency and short-term survival prospects</li>
			<li>capital structure ratios - these measure the adequacy of owners' funding in relation to long-term debt</li>
			<li>activity and efficiency ratios - these measure the operating efficiency of the business in non-financial terms</li>
			<li>profitability ratios - these measure overall profitability and how well the business is using its assets and covering overhead costs</li>
		</ul>

		<p>You can <a href="https://web.archive.org/web/20111102190949/http://www.lloydstsbbusiness.com/support/businessguides/key_accounting_ratios.asp" target="_blank" rel="noopener noreferrer">find guidance on key accounting ratios and ratio analyses on the Lloyds TSB website</a> or see our guide on <a href="/finance-and-grants/financial-planning-and-accounts/balance-sheets-the-basics.php">balance sheets: the basics</a>.</p>
		
