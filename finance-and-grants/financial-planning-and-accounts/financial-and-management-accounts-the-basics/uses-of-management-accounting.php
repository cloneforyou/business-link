		<h1>Financial and management accounts: the basics</h1>
		<h2>Uses of management accounting</h2>
		<p>Management accounts will enable you to:</p>
		<ul>
			<li>compare your accounts with original budgets or forecasts</li>
			<li>manage your resources better</li>
			<li>identify trends in your business</li>
			<li>highlight variations in your income or spending which may require attention</li>
		</ul>

		<p>They should be used for the following:</p>
		<p><strong>Record keeping</strong></p>
		<ul>
			<li>recording business transactions</li>
			<li>measuring results of financial changes</li>
			<li>projecting financial effects of future transactions</li>
			<li>preparing internal reports in a user-friendly format</li>
		</ul>

		<p><strong>Planning and control</strong></p>
		<ul>
			<li>collecting cash</li>
			<li>controlling stocks</li>
			<li>controlling expenses</li>
			<li>co-ordination and monitoring of strategy/performance</li>
			<li>monitoring gross margins</li>
		</ul>

		<p><strong>Decision making</strong></p>
		<ul>
			<li>using cost information for pricing, capital investment and marketing</li>
			<li>evaluating market and product profitability</li>
			<li>evaluating the financial effect of strategies and plans</li>
		</ul>

