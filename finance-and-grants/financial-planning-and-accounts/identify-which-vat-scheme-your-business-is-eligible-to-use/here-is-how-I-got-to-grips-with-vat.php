		<h1>Here's how I got to grips with VAT</h1>
		<h2>Here's how I got to grips with VAT</h2>
		<p>Based in London, Harley Street Cosmetic is a range of innovative plant-based skincare products and natural perfumes, developed by the company's founder and owner, Dr Cuross Bakhtiar. From the start, Cuross was determined to keep a close eye on the business' finances. Here he talks about why he decided to register for VAT early, the importance of setting up an effective record-keeping system and what to consider when choosing a software package.</p>
		
		<h3>What I did</h3>
		<h4>Gather information</h4>
		<p>"While my background and core skills lie in medical research, plant chemistry and product development, I also wanted to understand and control the operational side of things, especially the finances. One area I knew I had to get to grips with was VAT.</p>
		<p>"I attended two of HMRC's VAT workshops, which were very beneficial, not least because I was able to talk to other business owners about how they approach VAT and pick up useful tips. The workshops also proved to be an unexpectedly good networking exercise!</p>
		<p>"There's plenty of guidance available online if you know where to look, such as the Business Link website."</p>
		
		<h4>Decide which VAT scheme to use</h4>
		<p>"I decided early on that the company should voluntarily register for VAT, rather than waiting until the turnover made it compulsory. One advantage of this was that we could reclaim some of the input tax (the VAT charged on purchases made by the company), which I had calculated could be a considerable amount. Another advantage of being VAT registered is that it helps project a professional image, which is important in the cosmetics industry because many people are wary of newcomers.</p>
		<p><a href="/starting-up/vat/when-to-register-for-vat.php">Use our interactive tool to find out if you need to register for VAT</a> or see our guide on <a href="/tax-payroll-and-company-information/vat/how-and-when-to-register-for-vat/when-to-register-for-uk-vat.php">when to register for UK VAT</a>.</p>
		<p>"We sell our products direct to the consumer, so I considered the various VAT retail schemes, which provide an alternative to traditional VAT accounting rules. However, I decided against using one.</p>
		<p><a href="/tax-payroll-and-company-information/vat/accounting-schemes-to-simplify-your-vat/identify-which-vat-scheme-your-business-is-eligible-to-use.php">Use our interactive tool to identify which VAT scheme your business is eligible to use</a>.</p>
		
		<h4>Set up a record-keeping system</h4>
		<p>"If you want to keep on top of the VAT, good record-keeping is essential. I bought in computer software, recommended by a former colleague, to streamline the process.</p>
		<p>"It certainly made filing my first VAT return easier, as it generates a VAT summary and all the other figures required by HMRC.</p>
		<p>"I try to enter things into the system as soon as possible to ensure that nothing's overlooked and that any management information generated is as accurate and up to date as possible. Having a good system also makes it easier to tackle the more complex areas of VAT. For example, the natural oils we use have different VAT treatments according to whether they are classified as a raw material or a food product by HMRC. That's a lot easier to manage when I can see all the figures at a glance."</p>
		
		<h3>What I'd do differently</h3>
		<h4>Use the same software package as the accountants</h4>
		<p>"Computerising the VAT accounts has been a huge benefit, but looking back, it would have been better to buy the same software that our accountants use. Even if you do the day-to-day bookkeeping yourself, you may have to send it to your accountant at some point, and using the same software makes it easier to transfer information."</p>

