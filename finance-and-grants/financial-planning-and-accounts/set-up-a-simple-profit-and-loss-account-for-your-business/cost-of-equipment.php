		<h1>Set up a simple profit and loss account for your business</h1>
		<h2>Cost of equipment</h2>
		<p>Any items of equipment you have bought or leased for long-term use are called 'capital items' or 'fixed assets'. These might include:</p>
		<ul>
			<li>furniture</li>
			<li>computer equipment</li>
			<li>cars or vans necessary for the business</li>
			<li>machinery</li>
			<li>premises</li>
		</ul>

		<p>Depending on the size of your business, you can record the cost of equipment you buy in a separate register of equipment, the 'fixed asset register', or you can include it in your general expenditure records and show it as a 'capital item'.</p>
		
		<h3>Depreciation and allowances</h3>
		<p>The cost of capital items is not deducted from your profits in the same way that ordinary business expenses are. Instead, you make a charge for depreciation each year, reflecting the fall in the value of the asset over time. This spreads the cost of the asset over several years.</p>
		<p>For your own profit and loss, you typically choose a depreciation charge that provides a realistic reflection of how long the asset will be useful. For example, you might expect a computer to be obsolete after three years, so charge a third of its cost against profit each year.</p>
		<p>Read about depreciation in our guide on how to <a href="/finance-and-grants/manage-your-cashflow/decide-whether-to-lease-or-buy-assets.php">decide whether to lease or buy assets</a>.</p>
		<p>For tax purposes, depreciation is not an allowable expense. Instead, there are set allowances that you can claim. For small businesses, the costs of most asset purchases can be fully claimed against tax using the annual investment allowance (up to an annual limit of &pound;100,000).</p>
		<p>See our guide on <a href="/tax-payroll-and-company-information/tax-reliefs-and-allowances/capital-allowances/capital-allowances-the-basics.php">capital allowances: the basics</a>.</p>
		
