		<h1>Set up a simple profit and loss account for your business</h1>
		<h2>Profit reporting: how, when and where?</h2>
		<p>Profit and loss reporting requirements vary according to how the business is set up.</p>
		<p>For example, if your business is a limited company, the company will report its profit (or loss) through annual accounts and a Corporation Tax return. You also need to report your own personal income from the business, for example as a company director, on your Self Assessment tax return.</p>
		<p>If you are a sole trader, you report your business income as part of your own Self Assessment tax return.</p>
		<p>If your business is a partnership, the nominated partner must complete a Self Assessment tax return for the partnership. In addition , you need to report your own personal income from the business on your own Self Assessment tax return.</p>
		<p>Read about Self Assessment in our section on <a href="/tax-payroll-and-company-information/self-assessment.php">Self Assessment</a>.</p>
		
