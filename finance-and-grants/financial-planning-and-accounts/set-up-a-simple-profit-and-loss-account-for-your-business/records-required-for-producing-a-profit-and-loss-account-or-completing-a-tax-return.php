		<h1>Set up a simple profit and loss account for your business</h1>
		<h2>Records required for producing a profit and loss account or completing a tax return</h2>
		<p>Whatever your business type, you must keep accurate records of your income and expenditure. You need to keep self-employment records for <strong>five</strong> years after the 31 January deadline - and you may need to keep them for longer if you file your return late or if HM Revenue &amp; Customs starts a check. You need to keep limited company or partnership records for <strong>six</strong> years after the latest date your tax return is due.</p>
		<p>Accurate record keeping has important benefits. It:</p>
		<ul>
			<li>gives you the information you need to manage and grow your business</li>
			<li>enables you to report on your profit or loss easily and quickly when required</li>
			<li>will improve your chances of getting a loan or mortgage</li>
			<li>makes filling in your tax return easier and quicker</li>
			<li>makes completing VAT Returns straightforward - if you are registered for VAT</li>
			<li>helps you or your business pay the right tax</li>
			<li>provides back-up for claims for certain allowances</li>
			<li>helps you plan and budget for tax payments</li>
			<li>reduces the risk of interest on late payments or late-filing penalties</li>
			<li>helps reduce fees if you use an accountant - your annual accounts will be far easier to produce</li>
		</ul>

		<p>The <strong>basic records</strong> you will need to keep are:</p>
		<ul>
			<li>a record of all your sales and takings</li>
			<li>a record of all your purchases and expenses</li>
		</ul>

		<p>You may also need to keep:</p>
		<ul>
			<li>a separate list for petty cash expenditure if relevant</li>
			<li>a record of goods taken for personal use and payments to the business for these</li>
			<li>a record of money taken out for personal use or paid in from personal funds - this applies to limited companies</li>
			<li>back-up documents for all of the above</li>
		</ul>

		<p>You will need the information above to create your profit and loss account and to complete your tax returns.</p>
