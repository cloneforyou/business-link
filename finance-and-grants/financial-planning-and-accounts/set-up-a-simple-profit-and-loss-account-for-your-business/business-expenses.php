		<h1>Set up a simple profit and loss account for your business</h1>
		<h2>Business expenses</h2>
		<p>These are all the ongoing expenses associated with running your business that you can deduct from your 'gross profit' figure on your profit and loss account to calculate a figure of 'profit before taxation'.</p>
		<p>Legitimate business expenses for accounting purposes are:</p>
		<ul>
			<li>employee costs</li>
			<li>premises costs</li>
			<li>repairs</li>
			<li>general administration</li>
			<li>motor expenses</li>
			<li>travel/subsistence</li>
			<li>advertising/promotion/entertainment</li>
			<li>interest</li>
			<li>bad debts</li>
			<li>legal/professional costs</li>
			<li>other finance charges</li>
			<li>depreciation or loss - profit - on sales of equipment</li>
			<li>any other expenses</li>
		</ul>

		<p>Note that some elements of these expenses are not allowed for tax purposes and are added back before your taxable profit is calculated.</p>
		
		<h3>Apportioning expenses - self-employment and partnerships</h3>
		<p>Where expenses apply partly to business and partly to non-business or personal use, you need to exclude any expenditure that relates to non-business use. For example, if you use your car for both business and private purposes, you normally work out the allowable business and non-allowable private proportions based on the mileage covered for each.</p>
		<p>When filing invoices, remember to note any apportionment on them.</p>
		
