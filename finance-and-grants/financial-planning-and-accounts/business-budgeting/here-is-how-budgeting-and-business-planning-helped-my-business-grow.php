		<h1>Here's how budgeting and business planning helped my business grow </h1>
		<h2>Here's how budgeting and business planning helped my business grow </h2>
		<p>Gideon Kasfiner launched First Luggage, a door-to-door luggage collection and delivery service, in 2004. The business now employs nine people and turns over &pound;2 million a year. Here Gideon explains how financial planning has helped him grow his business in a controlled way.</p>
		
		<h3>What I did</h3>
		<h4>Worked to an annual and monthly timetable</h4>
		<p>"I start doing my annual budgets three months before the year end. I look at the current year and see whether we are going to increase sales, and if we are how it's going to increase our costs, and so on. My accountant actually sets the budgets though, from the figures I give.</p>
		<p>"Then, every month, I sit down with my management accounts. I look at my actual income and expenditure, compare them with my projections and look at the variance. I can see under every budgetary heading where things have gone up or down and I can make appropriate decisions.</p>
		<p>"So, for example, we may have budgeted sales of &pound;200,000 for one month but only made &pound;170,000. I might, for example, look at the marketing budget and do some more advertising. I might take a service out where costs are high, or even put prices up. It's all about juggling."</p>
		
		<h4>Built a detailed picture of my business</h4>
		<p>"I work with as many budget headings as I can. On the sales side, I break the figures down into the different products we deliver &ndash; suitcases, prams, golf bags, and so on. Then I can see exactly where revenue is coming from. I'll also look at the top 15 countries I'm delivering to.</p>
		<p>"On the cost side there are all sorts of headings, from transportation to fuel surcharges, office rent, salaries, and PR and marketing.</p>
		<p>"It's not easy doing things this way, but it gives me a better picture of my business and I have more areas to juggle with. Flexibility is very important."</p>
		
		<h4>Budgeted for loss of business</h4>
		<p>"After doing the annual budgets, I do them again with a 20 per cent drop in sales to see what the impact would be on the cashflow if there are any changes in the market. I want to make sure I'm being as prudent as I can be with my cash.</p>
		<p>"My mind is always looking at the downside as well as the upside. If I have a month that hasn't come up to where it should, my comfort zone is that I can do something about it."</p>
		
		<h3>What I'd do differently</h3>
		<h4>Work with a colleague on monthly reviews from the start</h4>
		<p>"Initially, I did the monthly reviews on my own and then I started doing them with a commercial colleague. Ideas are better when you have two heads looking at the same thing."</p>
		
