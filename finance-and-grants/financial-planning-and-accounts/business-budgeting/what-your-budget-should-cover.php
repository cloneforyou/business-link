		<h1>Business budgeting</h1>
		<h2>What your budget should cover</h2>
		<p>You should first decide how many budgets you really need. Many small businesses have one overall operating budget which sets out how much money is needed to run the business over the coming period - usually a year. As your business grows, your total operating budget is likely to be made up of several individual budgets such as your marketing or sales budgets.</p>
		
		<h3>What your budget will need to include</h3>
		<p><strong>Projected cashflow</strong> - your cash budget projects your future cash position on a month-by-month basis. Budgeting in this way is vital for small businesses as it can pinpoint any difficulties you might be having. It should be reviewed at least monthly. See our guide on <a href="/finance-and-grants/manage-your-cashflow/cashflow-management-the-basics.php">cashflow management: the basics</a>.</p>
		<p><strong>Costs</strong> - typically, your business will have three kinds of costs:</p>
		<ul>
			<li>fixed costs - items such as rent, rates, salaries and financing costs</li>
			<li>variable costs - including raw materials, overtime and tax</li>
			<li>one-off capital costs - for example, purchases of computer equipment or premises</li>
		</ul>
		
		<p>To forecast your costs, it can help to look at last year's records and contact your suppliers for quotes.</p>
		<p><strong>Revenues</strong> - sales or revenue forecasts are typically based on a combination of your sales history and how effective you expect your future efforts to be.</p>
		<p>Using your sales and expenditure forecasts, you can prepare projected profits for the next 12 months. This will enable you to analyse your margins and other key ratios such as your return on investment. See our guide on how to <a href="/sales-and-marketing/selling/forecast-and-plan-your-sales.php">forecast and plan your sales</a>.</p>
		
