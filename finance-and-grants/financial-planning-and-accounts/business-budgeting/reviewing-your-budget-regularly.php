		<h1>Business budgeting</h1>
		<h2>Reviewing your budget regularly</h2>
		<p>To use your budgets effectively, you will need to review and revise them frequently. This is particularly true if your business is growing and you are planning to move into new areas.</p>
		<p>Using up-to-date budgets will help you manage your cashflow effectively and identify what needs to be achieved in the next budgeting period.</p>
		
		<h3>Income and expenditure</h3>
		<p>There are two main areas to consider when reviewing your budget - <strong>income</strong> and <strong>expenditure</strong>.</p>
		<p><strong>Your actual income</strong> - each month, you should compare your actual income with your sales budget. To do this, you should:</p>
		<ul>
			<li>
				analyse the reasons for any shortfall - for example, lower sales volumes, flat markets and underperforming products
			</li>
			<li>
				consider the reasons for a particularly high turnover - for example, whether your targets were too low
			</li>
			<li>compare the timing of your income with your projections and check that they fit</li>
		</ul>

		<p>Analysing these variations will help you to set future budgets more accurately and also allow you to take action where needed.</p>
		<p><strong>Your actual expenditure</strong> - regularly review your actual expenditure against your budget. This will help you to predict future costs with greater reliability. You should:</p>
		<ul>
			<li>look at how your fixed costs differed from your budget</li>
			<li>check that your variable costs were in line with your budget - normally variable costs adjust in line with your sales volume</li>
			<li>analyse any reasons for changes in the relationship between costs and turnover</li>
			<li>analyse any differences in the timing of your expenditure - for example, by checking suppliers' payment terms</li>
		</ul>

