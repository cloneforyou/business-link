		<h1>Market research and market reports</h1>
		<h2>Should I use a market research agency?</h2>
		<p>Though you may be able to do your own field research, it may be better and more cost-effective to outsource the job to professionals and use the services of a market research agency - see our guide on <a href="/grow-your-business/growth-through-strategic-sourcing/outsourcing.php">outsourcing</a>.</p>
		<p>The benefits to your business are:</p>
		<ul>
			<li>Market research professionals are likely to get <b>better results</b>. They have experience in designing survey questionnaires, running focus groups and asking the right questions.</li>
			<li>Customers may find it easier to be <b>honest</b> with an outsider, particularly if they have a complaint.</li>
			<li>Customers may worry you're trying to <b>sell</b> them something if you conduct the research yourself.</li>
			<li>You may find it difficult to be <b>impartial</b>, particularly if people criticise your business.</li>
		</ul>

		<p>For small-scale field research, your best option may be a <b>freelance researcher</b>. A market research agency won't usually take on projects with a budget below &pound;3,000.</p>
		<p>As well as recommendations from business contacts, you can <a href="https://www.mrs.org.uk/researchbuyersguide" target="_blank" rel="noopener noreferrer">search for a market research agency on the Research Buyers Guide website (registration required)</a>.</p>
		<p>Before taking on a market research agency or a freelance researcher, investigate their <b>reputation</b>. Ask for a list of previous clients and contact them for feedback. Check the agency or researcher has relevant experience and consider how comfortable you'd feel working with them. And get a clear idea of <b>fees</b> for the services you want.</p>
		<p>Check the agency's researchers or freelance researchers fit the image of your business. If they'll be carrying out <b>street interviews</b>, confirm they'll have the required local authority licence and identity card.</p>
		<p>Make sure you provide a thorough and clear <b>brief</b>. This needs to cover areas such as the business objectives behind the project, the information the research should uncover and how you intend to use the results.</p>
		
