		<h1>Create your marketing strategy</h1>
		<h2>Key elements of a successful marketing strategy</h2>
		<p>Your existing and potential customers fall into particular groups or segments, characterised by their 'needs'. Identifying these groups and their needs through market research, and then addressing those needs more successfully than your competitors, should be one of the key elements of your marketing strategy. See our guide on <a href="/sales-and-marketing/marketing/market-research-and-market-reports.php">market research and market reports</a>.</p>
		<p>You can then create a marketing strategy that makes the most of your strengths and matches them to the needs of the customers you want to target. For example, if a particular group of customers is looking for quality first and foremost, then any marketing activity aimed at them should draw attention to the high quality of your products or service.</p>
		<p>Once you have created your marketing strategy, you must then decide which marketing activity or activities will ensure your target market know about the products or services you offer, and why they meet their needs.</p>
		<p>There are many ways to achieve this - such as various forms of advertising, exhibitions, public relations initiatives, internet activity and an effective 'point of sale' strategy if you rely on others to actually sell your products. But try to limit your activities to those methods you think will work best, to avoid spreading your budget too thinly.</p>
		<p>Monitoring and evaluating how effective your strategy has been is a key element, yet often overlooked. This control element not only helps you see how your strategy is performing in practice, it can also help inform your future marketing strategy. A simple approach is to ask each new customer how they heard about your business.</p>
		<p>Once you have decided on your marketing strategy, draw up a marketing plan that sets out how you intend to execute that strategy and evaluate its success. The plan should be constantly reviewed and, if necessary, updated so you can respond quickly to changes in customer needs and attitudes in your industry and in the broader economic climate. See our guide on how to <a href="/sales-and-marketing/marketing/write-a-marketing-plan.php">write a marketing plan</a>.</p>
		
