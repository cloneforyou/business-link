		<h1>Create your marketing strategy</h1>
		<h2>SWOT and PEST analysis</h2>
		<p>An honest and rigorous <b>SWOT</b> analysis - looking at your strengths, weaknesses, opportunities and threats - is a good starting point for your marketing strategy document.</p>
		<p><b>Strengths</b> could include:</p>
		<ul>
			<li>good customer service</li>
			<li>special product features or benefits</li>
			<li>specialist knowledge or skills</li>
		</ul>
		<p><b>Weaknesses</b> could include:</p>
		<ul>
			<li>poor cashflow or a lack of capital</li>
			<li>lack of an established reputation</li>
			<li>inefficient accounting systems</li>
		</ul>
		<p><b>Opportunities</b> could include:</p>
		<ul>
			<li>increased demand</li>
			<li>using the internet to reach new markets</li>
			<li>new technologies that allow you to improve product quality</li>
		</ul>
		<p><b>Threats</b> could include:</p>
		<ul>
			<li>new competitors</li>
			<li>more attractive or cheaper versions of your product or service</li>
			<li>new legislation increasing your costs</li>
			<li>a downturn in the economy, reducing overall demand</li>
		</ul>
		<p>
			<p><a href="https://web.archive.org/web/20120825131553/http://www.bizhelp24.com/marketing/swot-analysis-understanding-your-business.html" target="_blank" rel="noopener noreferrer">Find out how to do a SWOT analysis on the bizhelp24 website<span> - Opens in a new window</a>.</p>
		</p>
		<p>Having done your analysis, you can then measure the potential effects each element may have on your marketing strategy.</p>
		
		<h3>Business environment</h3>
		<p>Understanding the environment your business operates in is a key part of market planning, and allows you to identify the threats and opportunities associated with your area of business. A 'PEST' analysis helps you identify the main opportunities and threats in your market:</p>
		<ul>
			<li><strong>political</strong> and legal changes - such as new regulations</li>
			<li><strong>economic</strong> factors - such as interest rates, exchange rates and consumer confidence</li>
			<li><strong>social</strong> factors - such as changing attitudes and lifestyles, and the ageing population</li>
			<li><strong>technological</strong> factors - such as new materials and growing use of the internet</li>
		</ul>
		
