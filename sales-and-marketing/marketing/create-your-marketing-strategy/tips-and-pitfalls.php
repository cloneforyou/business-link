		<h1>Create your marketing strategy</h1>
		<h2>Tips and pitfalls</h2>
		<p>Before looking at new markets, think about how you can get the most out of your existing customers - it's usually more economical and quicker than finding new customers.</p>
		<p>Perhaps you could sell more to your existing customers, or look at better ways to retain key customers.</p>
		
		<h3>Focus on the market</h3>
		<ul>
			<li>Analyse the different needs of different groups of customers.</li>
			<li>Focus on a market niche where you can be the best.</li>
			<li>Aim to put most of your efforts into the 20 per cent of customers who provide 80 per cent of profits.</li>
		</ul>
		
		<h3>Don't forget the follow-up</h3>
		<ul>
			<li>Approach a third party for feedback about your strategy - they may be able to spot any gaps or weaknesses that you can't see.</li>
			<li>Put your marketing strategy into effect with a marketing plan that sets out the aims, actions, dates, costs, resources and effective selling programmes.</li>
			<li>Measure the effectiveness of what you do and be prepared to change things that aren't working.</li>
		</ul>
		
		<h3>Pitfalls to avoid</h3>
		<ul>
			<li>Making assumptions about what customers want.</li>
			<li>Ignoring the competition.</li>
			<li>Trying to compete on price alone.</li>
			<li>Relying on too few customers.</li>
			<li>Trying to grow too quickly.</li>
			<li>Becoming complacent about what you offer and failing to innovate.</li>
		</ul>
		<p>Be sure to follow up all of your marketing and promotional strategies with a marketing plan. This sets out clear objectives and lists the actions you will take to achieve them. See our guide on how to <a href="/sales-and-marketing/marketing/write-a-marketing-plan.php">write a marketing plan</a>.</p>
		
