		<h1>Here's how I chose the right marketing mix</h1>
		<p>Human resources consultancy Wickland Westcott reviewed the marketing strategy for its assessment and development services, and a new focus and a more structured approach have rapidly paid dividends. Co-owner and director of assessment and development, Colin Mercer, describes the process.</p>
		
		<h2>What I did</h2>
		<h3>Identify strengths and weaknesses</h3>
		<p>"The company was doing well, but our approach to marketing was hit-and-miss. We went through the business from top to bottom and pinpointed our strengths and weaknesses. </p>
		<p>"On the plus side, customer research showed that we had a great reputation for quality service. On the minus side, brand awareness was low and some of our systems weren't working well. We also used published academic research to find out more about our target customers' mindset and why they buy. We used all the information to create a marketing strategy with clear objectives. These included developing our network of partnerships, raising brand awareness, positioning ourselves as strategic thinkers in our market, and getting more business through referrals."</p>
		
		<h3>Work to a plan</h3>
		<p>"With clear objectives in mind, writing and implementing a marketing plan was easier. We changed our marketing mix and our approach:</p>
		<ul>
			<li>
				Our printed newsletter, our main direct marketing method, was made more customer-focused by including market briefings. Feedback has improved and we've had several approaches from new clients who've received it.
			</li>
			<li>
				Instead of cold calling, we stepped up marketing via our network of partners. We provided them with clear information so that they could promote all our services to their clients. Now, about 20 per cent of what we bill is to partners rather than end users.
			</li>
			<li>
				To establish ourselves as strategic thinkers in our markets we began getting articles published in relevant trade journals. This raised brand awareness and enhanced our reputation. We also use re-prints of the articles as a direct marketing tool.
			</li>
			<li>
				We also re-designed our website and launched an Interview Guide on CD-Rom to show the flexibility of our services.
			</li>
		</ul>
		<p>"We now review our marketing strategy and update the plan every six months. As a result of one review, we decided to attend a big trade conference, which worked extremely well for us and generated a lot of new leads."</p>
		
		<h3>Clean up the database</h3>
		<p>"Marketing to existing customers is easier and cheaper than starting from scratch with new ones, because existing customers are aware of our quality. However, to market effectively to this group required a better customer database than we had. As a result we decided to update our customer database. It's just as well we did, because as we updated it we found that approximately 40 per cent of the entries contained errors, duplications or were completely irrelevant. If we'd marketed to our original database we'd have wasted a lot of time and money.</p>
		<p>"We now have a proper database management strategy. We nominated one person internally to take responsibility and now it's kept up to date meticulously. We also use bought-in contact databases to help us target new customers."</p>
		
		<h2>What I'd do differently</h2>
		<h3>Repackage content</h3>
		<p>"We were slow to realise that the content we had on our website and in other marketing literature was a gold mine. It can be repackaged in lots of different ways. For example, a case study written for the website can be used as a mail shot, as the basis for a trade magazine article and as a handout for meetings as well. We're much better at it now, and we use all our content, including survey results, across all our marketing channels. However, we could have made use of what we had much sooner."</p>
		
