		<h1>Create your marketing strategy</h1>
		<h2>Developing your promotional strategy</h2>
		<p>When you have determined your target audience you should decide what message you are trying to get across to them in your marketing. If you have more than one target market, this message could be different depending on the potential customer. It may also be necessary to focus your marketing in different ways - for more information, see the page in this guide on <a href="/sales-and-marketing/marketing/create-your-marketing-strategy/product-strategy-positioning-and-differentiation.php">product strategy - positioning and differentiation</a>.</p>
		
		<h3>Brand awareness</h3>
		<p>To create a successful promotional strategy, you need to create brand awareness amongst your customers. Brand awareness relates to how well your specific product is recognised by current and potential customers. For more information on the benefits of creating a brand, see our guide on <a href="/sales-and-marketing/advertising-branding-and-design/branding-the-basics.php">branding: the basics</a>.</p>
		<p>There are a number of forms of advertising which you can use to create brand awareness within your customer groups. Methods to consider include:</p>
		<ul>
			<li>mass media advertising - eg business directories, magazines, newspapers, billboards, radio or TV</li>
			<li>public relations - eg press releases, launches, events or tours</li>
			<li>sales-based methods - eg coupons, competitions, discounts, gift vouchers, loyalty incentives for existing customers</li>
			<li>direct marketing - eg sales letters, email, mail order catalogues, packaging designs or point of sales displays</li>
			<li>telemarketing - see the page on <a href="/sales-and-marketing/marketing/direct-marketing-the-basics/telemarketing.php">telemarketing</a> in our guide on <a href="/sales-and-marketing/marketing/direct-marketing-the-basics.php">direct marketing: the basics</a></li>
			<li>digital marketing through websites or mobile phone promotions - see our guide on <a href="/it-and-ecommerce/create-and-manage-a-website/maintaining-your-web-content-and-technology.php">maintaining your web content and technology</a> and our section on <a href="layere3d7.html?topicId=1073866263" title="How e-commerce can benefit your business and information on the mistakes to avoid">e-commerce</a></li>
			<!-- cant find original file, could it be: /it-and-ecommerce/e-commerce.html -->
			<li>social networking or video advertising - you can offer special promotions for online customers and provide other news and information</li>
		</ul>

		<p>You could also consider attending dealer or showroom events, exhibitions or trade shows to take advantage of any sales opportunities that may present themselves there or if your target audience may also be attending them.</p>
		
		<h3>Selling to existing customers</h3>
		<p>You may be able to increase awareness of your product or service by marketing it to your existing customers. If you understand your current customers - particularly those that deal with you often or spend highly - you should be able to develop ways to target them and increase your brand awareness.</p>
		<p>You should also encourage existing customers to promote you to associates, as word of mouth recommendations can be very influential when developing a brand.</p>
		<p>For more information, see the page on how to <a href="/sales-and-marketing/selling/increase-your-market-share/sell-more-to-existing-customers.php">sell more to existing customers</a> in our guide on how to <a href="/grow-your-business/planning-how-to-grow/increase-your-market-share.php">increase your market share</a>.</p>
		
		<h3>Timing and measuring your campaigns</h3>
		<p>Whatever promotional strategies you use, you should time your activities to reach your target customers when they are most receptive. You should develop strategies that combine both long and short-term activities - eg special offers or competitions.</p>
		<p>Measuring the effectiveness of your strategy is also important. This can include asking new customers how they heard about you, or using surveys before and after every marketing campaign. You could also monitor your website traffic and use individual promotional codes for specific sales or offers. These can help you determine what is and isn't working and show you where you can improve your campaigns.</p>
		
