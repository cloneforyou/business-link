		<h1>Trade shows and exhibitions</h1>
		<h2>Using a trade fair to promote your product or service</h2>
		<p>At their most effective, trade fairs can be an important promotional tool for your business. Trade fairs can help to build new business by offering:</p>
		<ul>
			<li>a chance to demonstrate your product or service to customers face to face</li>
			<li>a direct way to test new markets or launch new products and services</li>
			<li>a chance to find out what customers want</li>
			<li>good opportunities for networking</li>
			<li>many potential customers concentrated in one place</li>
			<li>many potential suppliers concentrated in one place</li>
		</ul>

		<p>As well as buyers and sellers, trade fairs can attract journalists, media, politicians and other 'movers and shakers' in particular fields. It can be a good idea to invite your existing customers to visit your stand too, as face-to-face contact can revitalise relationships that exist mostly through email or on the phone.</p>
		<p>At their least effective, trade fairs can be an expensive waste of time. Make sure you know what you want to achieve. Make your <b>goals</b> specific and measurable - for example, 'sell &pound;x of products directly from the stand', or 'get 50 new sales leads'. Choose your fair carefully and give yourself plenty of time to plan. When it's over, follow up the contacts you made.</p>
		<p><b>Evaluate</b> what you got out of the trade fair (based on your goals) so you can build on your experience for next time.</p>
		
