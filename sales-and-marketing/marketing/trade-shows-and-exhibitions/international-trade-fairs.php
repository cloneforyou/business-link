		<h1>Trade shows and exhibitions</h1>
		<h2>International trade fairs</h2>
		<p>Attending <strong>trade fairs abroad,</strong> either as an exhibitor or a customer, can be an excellent way to test and open up foreign markets and find new suppliers.</p>
		<p>If you're thinking of exhibiting, it's a good idea to make an exploratory visit first, in order to make contacts, do some research and see the competition. You should then be well placed to find the right fair and position your business effectively.</p>
		<h3>Getting help</h3>
		<p>Some obvious issues to consider are differences in language and legislation. More difficult to pin down are cultural aspects of doing business in other countries. Try getting information from the country's embassy in the UK, international Chambers of Commerce or foreign trade associations. You can <a href="https://www.gov.uk/government/organisations/department-for-international-trade" target="_blank" rel="noopener noreferrer">read overseas sector reports on the UK Trade & Investment website</a>.</p>
		<p>UK Trade &amp; Investment's Tradeshow Access Programme helps businesses new to exporting take part in overseas exhibitions. Your business may be eligible for funding to help you attend overseas exhibitions. <a href="https://www.gov.uk/guidance/tradeshow-access-programme" target="_blank" rel="noopener noreferrer" title="Support for new exporters to help them attend overseas exhibitions">Find out about the TAP on the UK Trade &amp; Investment website</a>.</p>
