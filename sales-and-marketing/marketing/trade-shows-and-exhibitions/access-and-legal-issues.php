		<h1>Trade shows and exhibitions</h1>
		<h2>Access and legal issues</h2>
		<p>You must ensure your stand meets health and safety standards and accessibility requirements under the Equality Act 2010. You must also comply with the various regulations applying to the sale of goods. And if you collect any contact data at the event ensure you consult first with the Data Protection Act 1998 and the Privacy and Electronic Communications (EC Directive) Regulations 2003.</p>
		<p>When designing your exhibition stand you should ask yourself:</p>
		<ul>
			<li>Will every visitor be able to get to the stand and take part in any hands-on demonstrations?</li>
			<li>Is everything you want to do covered by the venue insurance?</li>
			<li>Have you made sure there's nothing for visitors to trip over or injure themselves on? For example, ensure there are no trailing leads and stow away promotional materials.</li>
			<li>Is the electrical supply suitable for your installations?</li>
		</ul>

		<p>If you are intending to sell goods or services directly from the exhibition, you should also consider:</p>
		<ul>
			<li>The Consumer Protection from Unfair Trading Regulations 2008 (which repealed some but not all requirements of the Trade Descriptions Act) and the Business Protection from Misleading Marketing Regulations 2008 - all descriptions, including those that are verbal, written, implied or given in an illustration, must be accurate and not misleading. <a href="https://www.gov.uk/online-and-distance-selling-for-businesses" target="_blank" rel="noopener noreferrer" title="Advice on distance and doorstep selling, trading schemes and product descriptions including pricing">Find information on the rules protecting consumers and businesses on the Department for Business, Innovation and Skills (BIS) website</a>. </li>
			<li>The Sale of Goods Act - this states that all goods or services must match the description given, be fit for their purpose and be of satisfactory quality. For further information, see our guide on <a href="/sales-and-marketing/selling-and-the-law/the-sale-of-goods-act.php">the Sale of Goods Act</a>.</li>
			<li>Product liability law - if anyone suffers damage to personal property or is injured or killed as a result of using your product you could be sued. See our guide on <a href="/workplace-health-and-safety/managing-the-welfare-of-people/product-liability.php">product liability</a>.</li>
			<li>Weights and measures - if any of your goods are sold by weight or measure you will have to comply with a number of rules. For more information, see our guide on <a href="/sales-and-marketing/selling-and-the-law/weights-and-measures.php">weights and measures</a>.</li>
			<li>Pricing - the Price Marking order and the Consumer Protection Act affect how prices must be given and protect customers from misleading pricing tactics. <a href="https://www.gov.uk/government/publications/consumer-rights-act-2015/consumer-rights-act-2015" target="_blank" rel="noopener noreferrer" title="How to avoid misleading your customers about the prices of your goods or services">Download a guide to pricing on the BIS website (PDF, 611K)</a>. You can <a href="/_files/dti_code-of-practice-for-traders-on-price-indications_10-2005.pdf" target="_blank" rel="noopener noreferrer" title="The guide on pricing practices">download the code of practice on misleading prices from the BIS website (PDF, 134K)</a>.</li>
		</ul>

