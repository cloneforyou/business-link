		<h1>Trade shows and exhibitions</h1>
		<h2>Exhibiting - doing it yourself</h2>
		<p>When arranging your own exhibit, you need to think through your strategy and aims and plan carefully how you will manage your exhibit.</p>
		
		<h3>Strategy and aims</h3>
		<ul>
			<li>Who do you want to attract?</li>
			<li>What will you tell them?</li>
			<li>How will you stand out among your competitors?</li>
			<li>What image do you want to create?</li>
			<li>Will you be promoting or demonstrating particular highlights?</li>
			<li>What promotional materials will you use - such as screens, boards, display stands, posters, leaflets, brochures, samples and merchandise?</li>
		</ul>
		
		<h3>Planning</h3>
		<ul>
			<li>Do some publicity. Tell potential customers why they should visit your stand. Ask the organisers which media will cover the fair, and send advance information for inclusion in any event programmes.</li>
			<li>Decide who will staff the stand. What technical know-how and communication skills do they need? Do they require any training?</li>
			<li>Allow enough time to order and produce everything you need.</li>
			<li>List what needs to be done, by when, and by whom.</li>
			<li>Make sure everyone understands their responsibilities.</li>
		</ul>
		
		<h3>Practical details</h3>
		<ul>
			<li>Find out what equipment the venue provides, eg screens, tables, chairs, platforms.</li>
			<li>Source the equipment you are providing yourself and organise getting it there.</li>
			<li>Book your space as early as possible to get the site you want.</li>
			<li>Make sure your display materials fit the space and work with existing equipment.</li>
			<li>Check fittings - such as power or internet connections - for the electrical equipment you are using.</li>
			<li>Make sure you have enough stock of products and promotional materials.</li>
		</ul>
		
		<h3>Budget</h3>
		<ul>
			<li>Work out a detailed budget to keep control of costs.</li>
			<li>Allow some contingency.</li>
		</ul>
		
		<h3>Merchandise - does it work?</h3>
		<p>Promotional merchandise is designed to remind customers of your business, and reinforce messages. But unless yours stands out above all the other free pens, coasters, plastic bags, badges and mouse mats, it will be a waste of money. Choose something your customers will use that creates the right image of your business.</p>
		<p>Try an internet search for suppliers. Before you choose one, get several quotes and ask for samples to check quality.</p>
		
