		<h1>Commercial property: responsibilities of business tenants</h1>
		<h2>Introduction</h2>
		<img src="/_img/HS0750.gif" width="94" height="62" alt="" />
		<p>If you are the tenant of commercial premises, it's important to be aware of the responsibilities you have for maintaining the premises and for the health and safety of occupants - particularly in areas such as gas and fire safety.</p>
		<p>Some responsibilities are laid down by statute and others have the force of law because they are set out in a lease.</p>
		<p>In some areas these responsibilities will be solely yours. In others they may be shared with your landlord.</p>
		<p>This guide explains how these important responsibilities are divided, either by the law or by the terms of the lease.</p>
		
