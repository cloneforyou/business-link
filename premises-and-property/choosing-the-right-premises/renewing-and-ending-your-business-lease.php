		<p>The Landlord and Tenant Act 1954 (Part 2) normally gives business tenants the right to renew the tenancy of their business premises when it comes to an end. The Act governs renewal and termination procedures and, where the parties cannot agree, provides courts with powers to order the renewal or termination of the tenancy. In the case of a renewal, the court will decide the terms of the new tenancy if the parties themselves cannot agree.</p>
		<p>This guide is for current and prospective business tenants and landlords. It explains which types of tenants have the right to end or renew their leases, and landlords' rights to end tenancies or obligations to grant new ones. It explains what rent reviews are and gives further guidance about specialist organisations you can contact and what tenancy forms you might need.</p>
		
