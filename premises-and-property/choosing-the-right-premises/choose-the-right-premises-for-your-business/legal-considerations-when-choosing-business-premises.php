		<h1>Choose the right premises for your business</h1>
		<h2>Legal considerations when choosing business premises</h2>
		<p>If you own or occupy business premises, you need to understand the legal obligations and restrictions that affect you. For example:</p>
		<ul>
			<li>The premises must have planning permission that allows them to be used for your type of business. For more information, see our guide: <a href="/premises-and-property/planning-permission-and-building-regulations/planning-permission-an-overview.php">an overview of the planning system</a>.</li>
			<li>You must comply with building, fire, and health and safety regulations.</li>
			<li>Stamp duty is payable on commercial leases and you are likely to be liable for business rates, though in rented premises these may be paid by the landlord. For more information, see our guides: <a href="/tax-payroll-and-company-information/business-rates/business-rates-an-overview.php">business rates - an overview</a> and <a href="/tax-payroll-and-company-information/stamp-taxes.php">stamp taxes</a>.</li>
			<li>You are responsible for the health and safety of employees and visitors. See our guide on <a href="/workplace-health-and-safety/what-workplace-facilities-do-you-need-to-provide.php">what workplace facilities do you need to provide?</a></li>
			<li>You also need to provide a suitable working environment.</li>
			<li>If you provide goods or services to the public, you must take reasonable steps to make your premises accessible. See our guide on <a href="/premises-and-property/access-and-facilities/access-and-facilities-for-disabled-people.php">access and facilities for disabled people</a>.</li>
			<li>You need to comply with the terms of any lease or licence agreement.</li>
			<li>For some businesses, you may require a licence to operate or to sell certain products.</li>
			<li>There may be restrictions on times when deliveries are allowed, noise and pollution levels, and how you or your customers dispose of waste.</li>
		</ul>

		<p>In licensed or leased premises, responsibilities are shared between the landlord and the tenant. For more information, see our guide on <a href="/premises-and-property/choosing-the-right-premises/commercial-property-responsibilities-of-business-tenants.php">commercial property: responsibilities of tenants</a>.</p>
		<p>Whatever premises you choose, you need to ensure that you are properly insured. For more information, see our guide on how to <a href="/finance-and-grants/insurance/insure-your-business-and-assets-general-insurances.php">insure your business and assets - general insurances</a>.</p>
		<p>If you are in any doubt about your legal obligations, you should take advice from your business adviser or solicitor.</p>
		
