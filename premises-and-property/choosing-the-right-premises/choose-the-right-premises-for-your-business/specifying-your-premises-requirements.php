		<h1>Choose the right premises for your business</h1>
		<h2>Specifying your premises requirements</h2>
		<p>Drawing up a list of what you need from your premises is a good way to start your search. This list might include the following points:</p>
		<ul>
			<li>size and layout of the premises</li>
			<li>structure and appearance, both internally and externally</li>
			<li>any special structural requirements, such as high ceilings</li>
			<li>facilities and comfort for employees and visitors - including lighting, toilets and kitchen facilities</li>
			<li>utilities, such as power and drainage, and any special requirements - for example, three-phase electricity</li>
			<li>permission, including planning permission, to use the premises for your type of business</li>
			<li>access and parking space - for deliveries or customers, including disabled customers</li>
			<li>whether you need the flexibility to alter or expand the premises</li>
			<li>your long-term business plans</li>
		</ul>

		<p>You also need to think about where you want your premises to be - see the page in this guide on how to <a href="/premises-and-property/choosing-the-right-premises/choose-the-right-premises-for-your-business/choose-the-right-location-for-your-business-premises.php">choose the right location for your business premises</a>.</p>
		<p>After drawing up your list of requirements, you may decide that working from home could suit you. However, there are important legal and practical issues you need to take into account - see our guide on how to <a href="/premises-and-property/choosing-the-right-premises/use-your-home-as-a-workplace.php">use your home as a workplace</a>.</p>
		<p>Your choice of premises will also depend on your budget. Whether you rent or buy, costs can include:</p>
		<ul>
			<li>initial purchase costs, including legal costs such as solicitor's fees and professional fees for surveyors</li>
			<li>initial alterations, fitting out and decoration</li>
			<li>any alterations required to meet building, health and safety and fire regulations</li>
			<li>ongoing rent, service and utility charges, including water, electricity and gas</li>
			<li>business rates - see our guide: <a href="/tax-payroll-and-company-information/business-rates/business-rates-an-overview.php">business rates - an overview</a></li>
			<li>continuing maintenance and repairs</li>
			<li>building and contents insurance</li>
		</ul>

		<p>Sellers and landlords are obliged to provide prospective buyers or tenants with an Energy Performance Certificate (EPC). An EPC indicates how energy efficient a building and its services are and can act as a good indicator of likely energy costs. For more information, see our guide on <a href="/environment-and-efficiency/cut-energy-costs/energy-performance-certificates-business-properties.php">Energy Performance Certificates - business properties</a>.</p>
		<p>If your requirements are too specific, you may find that your choice of premises is very limited or you cannot afford them. Think about which requirements are essential and which are desirable, and prioritise them accordingly to make your decision.</p>
		
