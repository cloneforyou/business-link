			<h2>"Here's how I chose the right premises for my business"</h2>

			<p><em>(Opening animation, title and jingle. Background music begins with footage of Pure Package branding, cut to Jennifer Irvine and footage of Pure Package bags being delivered)</em></p>

			<p>Jennifer Irvine: "I am Jennifer Irvine, founder of The Pure Package - the diet delivered to your door. We're located in a couple of railway arches in New Covent Garden Market - the biggest fruit and vegetable market in the UK."</p>

			<p><em>(Footage of vans and produce from the market, Jennifer walking through the market)</em></p>

			<p>"I literally started my company from the kitchen sink in my house. I had to move the business out of the house because it had outgrown my poor little kitchen. I was looking for a location which was close to our clients, close to our ingredients - the raw materials, and close to lots of wonderful qualified staff - so chefs and nutritionists."</p>

			<p><em>(Footage of chefs chopping vegetables, a large map, and Jennifer tasting produce in the market)</em></p>

			<p>"When I actually decided what I wanted from our premises, I was able to find them incredibly fast, so it took me about a month. One of the first things I did was get out a good old-fashioned map and have a look at that and decide, right, where is the centre of London and where are the markets located for my fresh fruit and vegetables? And as soon as I looked at the map it was quite obvious where we should locate."</p>

			<p><em>(Picture of New Covent Garden Market signpost, followed by footage of people working at computers in the Pure Package office)</em></p>

			<p>"It's really important that your business has some space to grow, and we have grown to five times the size we were when we first moved into the railway arches, and now we're looking at taking over the railway arch next door. The kitchen is the heart of our operation, so the premises are vital."</p>

			<p><em>(Footage of people working in the Pure Package kitchen - chopping and cooking food and then Jennifer walking around the market, testing produce)</em></p>

			<p>"The advice I'd give to somebody who was looking for new premises is to write a list of the requirements you want from your premises. So, it's really important to decide what you're looking for location-wise, what you're looking for from the premises themselves - do they need broadband? Do you need telephones? Do you need certain types of electricity - three-phase electricity? Do you need gas? All of these things are questions you should ask yourself. I've been incredibly lucky with finding my premises, in that there is not much I would've done differently."</p>

			<p>"The biggest challenge for me in choosing the right premises was taking that actual step - the commitment of taking on a lease. Business leases are filled with lots of legal terms, and are incredibly confusing and overwhelming, and I asked a solicitor to read mine for me. Really, what they were looking for was anything unusual, that one wouldn't expect. Most landlords aren't willing to negotiate on the lease particularly, but it's a good idea to know what you're getting into."</p>

			<p><em>(Footage of chefs in Pure Package kitchens, and Jennifer talking to a market trader)</em></p>

			<p>"Our premises work really well, and they actually enhance the service that we give. Nobody knows everything, so I take advice wherever I can."</p>

