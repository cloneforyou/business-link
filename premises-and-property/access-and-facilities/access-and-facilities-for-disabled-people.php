			<h1>Access and facilities for disabled people</h1>
			<h2>Introduction</h2>
			<img src="/_img/HS0710b.gif" width="94" height="62" alt="" />
			<p>There are around ten million disabled people in Great Britain. You must not discriminate against disabled people who use your goods or services. You are also required to make reasonable adjustments for disabled people.</p>
			<p>Reforms introduced by the Equality Act 2010 also make it unlawful to directly discriminate against someone wrongly perceived to be disabled or to treat someone less favourably because of an association with or link to a disabled person - for example refusing to serve a parent of a disabled child because the child is disabled.</p>
			<p>Businesses that provide a service to the public are legally required to make reasonable adjustments to the physical features of their premises where they put disabled people at a substantial disadvantage compared to non disabled people. This is in addition to other duties not to discriminate and to make reasonable adjustments.</p>
			<p>This guide covers ways of making your premises accessible to disabled people that would otherwise put them at a substantial disadvantage compared to non-disabled people in accessing to your services.</p>
			
